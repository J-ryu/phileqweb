                               <p>There’s no such thing as a free lunch. Nowhere is this truer than in the world of investments.
                                  When it comes to investing, gaining access to higher returns comes at a price. And that price is called risk.
                                  In investments, risk is commonly defined as the possibility and magnitude of incurring a loss. But actually,
                                  risk can be more broadly defined as the possibility of obtaining something other than what you expected. So,
                                  risk can actually work in your favor. In terms of investments, you could end up with a return that is higher
