                                </li>

                                 <li>
                                 <strong>Your investment time horizon or the length of time you are willing to keep your money invested. </strong>The longer your time horizon, the greater
                                 the risk you can take.The reasoning behind this is similar to the reason behind age: the longer the time horizon, the more time to
                                 recover from any losses.
                                </li>

                                <li>
                                <strong> Your past investment experience.</strong> This partly determines your attitude to risk. A bad experience from a past investment may make you more
