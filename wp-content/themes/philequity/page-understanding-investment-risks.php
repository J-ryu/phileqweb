                               <p>All this talk of risk may sound frightening. Does it mean that you should avoid risk? Especially when it comes
                               to your hard-earned savings? The answer to that question is a resounding…“it depends”. The amount of risk that you
                               are willing to take and are capable of taking is called your risk tolerance. Your risk tolerance is a function of
                               several factors that are unique to you and is thus different from the risk tolerance of another person.</p>  
                           <br>

                            <h4>Here are some factors to consider in determining your risk tolerance:</h4>

                            <ol>
                                <li><strong>Your return objectives.</strong> The higher the return you require in order to achieve your financial goals, the greater the amount 
                                of risk you will need to take.
                                </li>

                                 <li><strong>Your age.</strong>The younger you are, the greater the amount of risk you can take. Why? Simply because you have more time to recover
                                 from any losses that you incur and because you have more income-earning years ahead of you.
                                </li>


                                 <li>
                                 <strong>Your total assets.</strong>The larger your total assets, the greater the amount of risk you can take. This is because you have more to draw
