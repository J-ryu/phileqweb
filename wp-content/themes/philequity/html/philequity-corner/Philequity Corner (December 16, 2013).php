        <p align="center"><b>Peso will follow regional and global trends</b></p>
        <p align="justify">In last week’s Q&A (see <i>Yolanda and the Peso,</i> Dec. 9, 2013), we mentioned that while the peso weakened against the US dollar after Yolanda, it was not only the peso that depreciated but other currencies as well. In fact, the US dollar has been strengthening since the start of 2013 on signs that the US economy is improving. Starting October, the US dollar began another phase of strengthening after a brief pullback in September.</p>
        <p>In this 2nd part of the two-part series, we will look at long-term charts (5 years) to show you how the US dollar has been strengthening against most currencies, including peso.</p>
        
        <p align="center"><b>Japanese Yen</b></p>
        <p align="justify">Below is the chart of the Japanese yen which has fallen 5.8 percent since October. It is down 20.5 percent since the start of the year due to the expansionary economic policies instituted by Prime Minister Shinzo Abe or the so-called “Abenomics.”
        </p>

        <img src="image/121613chart.jpg" height="400" width="800">

        <p align="center"><b>Australian Dollar</b></p>
        <p align="justify">Commodity currencies are down sharply this year due to low commodity prices. Since October, the Australian dollar has declined 3.8%, bringing the year-to-date decline down to 15.9 percent.</p>

         <img src="image/121613chart1.jpg" height="400" width="800">

         <p align="center"><b>Brazilian Real</b></p>
        <p align="justify">Another commodity currency, the Brazilian real, is similarly down 5.1 percent since October. Meanwhile, it has depreciated 14.2 percent since the start of the year.</p>
        <p>The Brazilian real, together with the Indonesian rupiah, the Indian rupee, the South African rand and the Turkish lira, were declared this year by Morgan Stanley as the “Fragile 5” or the troubled emerging market currencies under the most pressure against the US dollar.</p>

         <img src="image/121613chart2.jpg" height="400" width="800">
         <p>High inflation, weakening growth, large external deficits, high dependence on fixed income inflows and in some cases exposure to China slowdown leave the “Fragile 5” vulnerable according to Morgan Stanley.</p>

         <p align="center"><b>Indonesian Rupiah</b></p>
        <p align="justify">Below is the chart of the Indonesian rupiah, the worst performing among ASEAN currencies. It has depreciated 6.1 percent since October and is down 23.6 percent since the beginning of the year.</p>

        <img src="image/121613chart3.jpg" height="400" width="800">

        <p align="center"><b>Indian Rupee</b></p>
        <p align="justify">Meanwhile, the Indian rupee which set an all-time low of 68.845 against the US dollar in August has improved slightly by 0.8 percent since September 30. But year-to-date it is still down 13.5 percent.</p>

        <img src="image/121613chart4.jpg" height="400" width="800">

        <p align="center"><b>Philippine Peso</b></p>
        <p align="justify">Six months after we correctly called the top of the peso (see <i>Peso Tops Out,</i> May 27, 2013), it is now trading near the higher end of our sweet spot (42 to 45). Like the other currencies shown above, the peso also weakened the last two months (although not as sharply), down 1.8 percent since October. Year-to-date, the peso is down 7.7 percent.</p>

        <img src="image/121613chart5.jpg" height="400" width="800">

        <p align="center"><b>Peso weakness is not surprising</b></p>
        <p align="justify">For the past 8 years, Filipinos have been accustomed to an appreciating peso since it bottomed out at 56 to the dollar in 2005. Hence, many are still puzzled and perplexed why the peso has been depreciating since the start of the year. </p>

        <p>For us at Philequity, however, the peso’s weakness is not surprising. In fact, we have written about
this as early as May 2013 when we called the top in the peso: <i>“We see the peso turning… With the
dollar strengthening and the yen weakening, how can the peso strengthen against the US dollar and the
Japanese yen both at the same time? If the yen is weakening, the peso should also weaken with the yen.”</i>
(see <i>Peso Tops Out,</i> May 27, 2013)</p>

        <p>More importantly, it is the broad-based dollar strength (not the weak peso per se) that is causing the peso
to depreciate, along with other currencies: <i>“It is not because the fundamental underpinnings of the peso
have turned negative, but because the fundamentals of the dollar have considerably improved which
caused the peso to depreciate along with most other currencies<i> (see<i> It’s the Strong Dollar, Stupid, </i>June
24, 2013).” </p>
    
        <p>Hence, while many expect the peso to appreciate towards the end of the year because of the Christmas
season and the surge of remittances and aid post-Yolanda, we at Philequity believe that it is the movement
in global and regional exchange rates that will largely determine the peso’s direction this time around.</p>

    <p>rom the chart formation and given that the long-term peso uptrend has been broken, it looks like the
peso will consolidate at these levels, or weaken and retest the August low 44.82 if the US dollar continues
to strengthen against other currencies. 
</p>


