        <p align="center"><b>Is the peso weak?</b></p>
        <p align="justify">Many of our readers and investors are concerned and are asking: “Why is the peso weak? Will it continue
to depreciate against the dollar?” The peso is now at 45.78 and is trading at 5-year lows. But before
making any conclusions, let us first look at how the dollar has fared against major currencies and against
currencies in the region.</p>

        <p align="center"><b>US dollar trend remains solidly up</b></p>
        <p align="justify">Since bottoming out in 2011, the US dollar index has already strengthened by 34.2 percent. The past year
alone saw the pace of US dollar appreciation quicken as central bank policies started to diverge in the 2nd
half of 2014. The Fed has been hinting a rate increase starting last year as the US economy strengthens
while other CBs are still in easing mode as their respective economies continue to struggle.</p>
        
        <p align="center"><b>Dollar against major currencies</b></p>
        <p align="justify">Policy divergence bets guided the US dollar higher against the Japanese yen and the euro. From its post-
2009 peak of 75.77, the yen has lost 64.8 percent against the greenback. Meanwhile, the euro has
depreciated 28 percent from a post-2009 peak of 1.512</p>
<p>The US dollar likewise sharply gained against commodity currencies as commodity prices collapsed to
multi-year lows after peaking in 2011. The Canadian dollar and the Australian dollar have declined 39.9
percent and 33.7 percent, respectively, from their post-2009 highs against the US dollar.</p>
        <img src="image/081015chart.jpg" height="300" width="800">
        <p align="justify"><i>Source: Bloomberg, Wealth Securities Research</i></p>

        <p align="center"><b>Commodity prices reach multiyear lows</b></p>
        <p align="justify">The sharp rise of the US dollar also caused commodity prices to drop thereby weakening the currencies of
commodity producers. Today, crude oil, copper, aluminum and zinc have slumped to 6-year lows. And
prices of soft-commodities such as corn, wheat, soybean, sugar, coffee and rice are at fresh multi-year lows.</p>
        <p>In addition, the slowdown of growth in China whose GDP is expected to grow at 7 percent this year (the
lowest level in nearly a quarter of a century) has also manifested in lower commodity prices. China has a
huge influence in commodities market since it is the world’s biggest importer of crude oil and accounts for
nearly half the global demand for nickel, aluminum and copper.</p>

        <p align="center"><b>EM commodity currencies under pressure</b></p>
        <p align="justify">Given the backdrop of a strong dollar and lower global demand for commodities, emerging market
economies heavily dependent on commodity exports suffered worse. Note the Russian ruble and Brazilian real which has depreciated more than 100 percent against the US dollar from their post-2009 highs.
Economic sanctions in Russia has compounded the decline in the ruble, while populist misrule in Brazil
has further aggravated the Brazilian real.</p>
<p>Other top commodity exporters such as South Africa, Indonesia, Chile and Malaysia have likewise
experienced sharp declines in their currencies. These economies rely a lot on their exports to shore up their
current account and growth.</p>
        <img src="image/081015chart1.jpg" height="300" width="800">
        <p align="justify"><i>Source: Bloomberg, Wealth Securities Research</i></p>

        <p align="center"><b>Socialist utopias, military conflicts & hyperinflation</b></p>
        <p align="justify">Making up the bottom are the currencies of socialist utopias suffering from hyperinflation such as the
Argentine peso and Venezuelan bolivar. Since the early 2000s, the Argentine peso has weakened more
than 800 percent against the greenback while the bolivar has weakened 500 percent.</p>
<p>But the worst performing in recent years are the Belorussian ruble and Ukraine hryvnia, two currencies
economically and politically tied to the Russian ruble. Military conflict and economic sanctions in the
region have led to capital outflows and sky-high inflation.</p>
        <img src="image/081015chart2.jpg" height="300" width="800">
        <p align="justify"><i>Source: Bloomberg, Wealth Securities Research</i></p>

        <p align="center"><b>The peso in proper perspective</b></p>
        <p align="justify">Putting things in proper perspective, the peso has actually been one of the more stable currencies in a strongdollar environment. <b>Year-to-date, the peso has weakened by only 2.1 percent against the greenback
compared to the 5.1 percent depreciation among Asian currencies and 6.9 percent decline among
major currencies.</b></p>
        <img src="image/081015chart3.jpg" height="400" width="800">

        <p align="center"><b>One of the strongest currencies these past few years</b></p>
        <p align="justify">In fact, the peso is one of the strongest currencies in Asia and the world these past few years. Asian
currencies have lost 25.9 percent and major currencies have fallen 35.2 percent on average since the dollar
bottomed out in 2011. In comparison, the peso has only lost 12.9 percent since reaching a post-2009 peak
of 40.54.</p>
<p>In other words, <b>the peso actually appreciated against most other currencies in recent years with the
exception of the US dollar and a few other currencies such as the Chinese yuan which is basically
pegged to the US dollar.</b></p>

        <p align="center"><b>Dollar strength, not peso weakness</b></p>
        <p align="justify"><i>So, is the peso really weak?</i> Yes it is weaker but it is mainly because of the strong dollar as we have written many times in the past (see <i>It’s the Strong Dollar, Stupid!,</i> June 24, 2013, <i>It’s the Strong Dollar, Stupid! –
Part 2,</i> Oct. 6, 2014 and <i>Philippine Peso: A Haven of Stability,</i> Feb. 2, 2015).</p>
<p>It is really the result of broad-based dollar strength and not peso weakness which is causing the depreciation
of the peso. The peso cannot afford to “stick out like a sore thumb” when all other currencies are
depreciating against the dollar.</p>

