        <p align="center"><b>PSEi Bottoms Out & the Peso Finds Support</b></p>
        <p align="justify">When Argentina devalued its currency by 15% in late-January, spreading turmoil across the emerging world and spooking even the developed markets, we wrote about the incipient signs of recovery in the Philippine stock market and the resilience of the peso (see <i>EM Currency Contagion</i>, January 27, 2014).</p>
        <p>A number of positive technical factors have been triggered since, each reinforcing the notion that the PSEi is already at the tail end of its bottoming out pattern. We now present the technical reasons why we think the PSEi is on the way up and why the peso is strengthening back towards 44 or even lower.</p>

        <p align="center"><b>PSEi outperforms EM index</b></p>
        <p align="justify">Since Bernanke’s “tapering speech” in May 2013 and the devastating effects of Typhoon Yolanda, the PSE index has underperformed emerging markets up until end-2013. However, a key development took place in January when the technical downtrend of the PSEi relative to the MSCI Emerging Market ETF was broken. This meant that the Philippines is finally being differentiated from problem EM countries such as Argentina, Turkey, Kazakhstan and Ukraine, and commodity-dependent countries like Brazil, South Africa and Russia.</p>
        <p>Below is the chart showing the PSE index relative to the MSCI Emerging Market ETF (EEM). The PSEi has outperformed EEM by 15.5 percent year-to-date.</p>

        <img src="image/030314chart.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com, Wealth Securities Research</i>

        <p align="center"><b>Foreign fund inflows are back</b></p>
        <p align="justify">We also note the inflection point in foreign fund flows last month. After reaching a cumulative net selling of nearly Php8 billion from the period January 2 to February 6, foreign fund flows reversed and turned positive on February 7. As of end-February, cumulative net flows were already at positive Php 1.2 billion year-to-date. Big increases in foreign net inflows were seen when the PSEi breached key resistance levels at 6,200 and 6,400.</p>

        <p align="center"><b>Philippine market, 2nd best in Asia</b></p>
        <p align="justify">Foreign flows started to revert to positive last month when fund managers and analysts realized that the effects of Typhoon Yolanda were limited to Eastern Visayas. Despite Yolanda, the country registered 4Q13 GDP growth of 6.5 percent, the 2nd fastest expansion in Asia next only to China. Hence, the PSEi jumped sharply by 6.4% in February, bringing year-to-date returns to 9.1 percent. This makes the Philippine stock market the 2nd best performing in Asia year-to-date after Vietnam. The PSEi also beat the indices of all developed markets.</p>

        <img src="image/030314chart1.jpg" height="400" width="800"><br>
