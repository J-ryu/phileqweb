        <p align="center"><b>Peso stabilizes as support at 45.50 holds</b></p>
        <p align="justify">In a previous article, we mentioned that while some foreign banks were saying that the peso will reach 48, the
weekly chart shows that it was nearing strong support at the 45.50 to 46 range. In addition, we noted that the
slope of the peso’s decline is becoming less steep which indicates a slowing down of momentum and a possible
turn (see <i>EM Currency Contagion</i>, January 27, 2014).</p>
<p>A week later, the peso indeed topped at 45.53 and has since appreciated towards 44.63. We see the peso
consolidating further and potentially going back to 44. Moving forward, if the peso can break below 44, then
we will see it back at its old sweet spot of 42 to 44.</p>
<p>The recovery and the stability of the peso is another bullish sign for the stock market. Below is the chart
showing the peso topping at 40.55 on March 2013 and the strong support at the 45.50 to 46 range.</p>

        <img src="image/030314chart2.jpg" height="400" width="800"><br>
        <i>Source: Wealth Securities Research</i>

        <p align="center"><b>PSEi targets 6,650 - 6,800</b></p>
        <p align="justify">The PSEi has completed a base and broken its downtrend line in early-February. Short- and medium-term indicators have turned bullish while the long-term trend remains neutral. The index may be capped at 6,500 initially or the 50 percent retracement of the May 2013 peak to August 2013 low. Corrections may occur because of the steep run-up the past few weeks but we expect it to eventually retest the tougher resistances at 6,650 and 6,800.</p>
        <p>Longer-term, a push above 6,800 would signal the resumption of the long-term bullish uptrend and a move towards new all-time highs. Below is the PSEi chart showing the basing formation and bottoming out at the 5,560 to 5,750 level.</p>

