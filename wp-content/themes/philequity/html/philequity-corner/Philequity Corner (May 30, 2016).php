    	<br><p><b>Groundswell in the stock market</b></p></span>

        <div align="justify" class="page">
            <p>In the lead-up to the elections, we wrote an article about the Duterte groundswell phenomenon. We
tried to understand how Rodrigo Duterte captured the imagination and adulation of Filipinos from all
walks of life. We talked to various people from different classes and backgrounds – from taxi drivers and
messengers to office employees and businessmen – in order to appreciate the reasons behind Duterte’s
sudden popularity. Moreover, we were amazed at how his grassroots campaign was fuelled by the
citizenry itself. The Duterte groundswell took everyone by surprise as the combined efforts of dedicated
volunteers and ardent supporters overcame the established machinery and political connections of his
opponents. We said that this phenomenon is something that sociologists and political analysts should
study and comprehend (see <i>Groundswell,</i> April 18, 2016). After it catapulted the Punisher to be our next
president, we have seen that the groundswell has started spreading to our stock market.</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Groundswell reaches the stock market</b></p></span>

        <div align="justify" class="page">
            <p>As the elections drew closer, several analysts and houses turned extremely bearish with regards to
Duterte’s impending win. Many painted doomsday scenarios, predicted a sharp stock market correction
and expected the worst out of a Duterte presidency. However, the index actually went up 6% three
weeks after the elections despite encountering brief corrections. These short-term downmoves were
triggered by contemplated contentious Cabinet appointments and the recent overnight placement of JG
Summit Holdings (JGS). Yet, many analysts still insist that the recent market move is just a technical rally
especially since the PSE Index is still far away from its all-time closing high of 8,127. They also cite that
our index has reached technical resistance at the 7,400 to 7,500 level. <b>However, it may have come
unnoticed that many index stocks have recently reached all-time highs. The failure of the PSE Index to
reach new highs has masked the underlying strength of Philippine stocks.</b></p>

