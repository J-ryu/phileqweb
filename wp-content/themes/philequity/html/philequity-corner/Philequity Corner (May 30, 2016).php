confidence resulting from the electoral victory of the Punisher. Though most currencies weakened
against the US dollar, the peso has managed to stay firm and has posted a gain vs. the dollar. <b>Thus, the
peso has been a clear outperformer vs. major currencies and other Asian currencies in the past month
