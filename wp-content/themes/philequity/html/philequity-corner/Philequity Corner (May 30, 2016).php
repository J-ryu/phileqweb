        <p align="justify">Apart from these, there are other index stocks that recently reached multi-year highs such as Metro
Pacific Investments Corp (MPI), Meralco (MER) and GT Capital Holdings (GTCAP). Moreover, if not for the substantial drop of PLDT (TEL), the PSE Index would now be trading at much higher levels. <b>Lastly, the
fact that many index stocks are trading at all-time or multi-year highs is a manifestation of a vibrant
bull market.</b></p>


<div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>JGS placement shows strong demand for Philippine stocks</b></p></span>

     
        <p align="justify">Another evidence of our stock market’s strength is the overnight placement of JGS last week. JGS
Chairman Emeritus John Gokongwei sold the equivalent of $250m or ~P11.7b, priced at P82.10/sh. The
fact that a local conglomerate can conduct a placement of such amount over a few hours shows that
there is strong demand not only for JGS shares but also for Philippine stocks in general.</b></p>

