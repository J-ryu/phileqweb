<div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Understanding the groundswell in the stock market</b></p></span>

     
        <p align="justify">The Duterte groundswell started to spread to the stock market on the very first day after the elections.
Before the elections, many adopted a wait-and-see stance because of negative expectations from a
Duterte presidency. Thus, investors were pleasantly surprised when the Duterte team made its first
pronouncements after winning the elections. The incoming administration stated that it would continue
current successful macroeconomic policies, accelerate infrastructure spending and address bottlenecks
in project implementation. Brewing confidence in the stock market was spurred by the presentation of
the Duterte government’s 8-point economic agenda. The appointments of Sonny Dominguez as DOF
Secretary, Art Tugade as DOTC Secretary, Ernesto Pernia as NEDA Chief, Silvestre Bello III as DOLE
Secretary, as well as other credible people to key Cabinet posts, further buoyed stock market sentiment.</p>


<div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Inspiring confidence</b></p></span>

     
        <p align="justify">A very important member of the Duterte team is incoming DOF Secretary Sonny Dominguez. He has a
solid reputation among businessmen and people who know him. He knows how business works since he is a businessman and was president of various big companies. He also knows how the government works, having served as Agriculture Secretary during the Cory Aquino administration.</p>
<p align="justify">Sonny has been closely coordinating with outgoing Finance Secretary Cesar Purisima to ensure the continuity of ongoing initiatives. On top of this, he has been busy doing the rounds, holding meetings with different sectors and various captains of industry. In those meetings, Sonny has been consultative and has always been willing to listen. Moreover, he has proactively sought feedback and suggestions in order to formulate policies that will work best for our country and economy. Thus, many view him as the right conduit to explain the Duterte enigma to businessmen. <b>Sonny’s presence in the Duterte team has inspired confidence from the business and investment community.</b></p>


<div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Capitalizing on the groundswell</b></p></span>

     
        <p align="justify">Our country continues to benefit from strong fundamentals, as seen in our economy’s 6.9% GDP growth in 1Q16. Robust domestic consumption continues to drive our economic growth. Meanwhile, our stellar fiscal position ensures that the government has the leeway and the resources to fund and support our country’s ongoing structural transformation. Thus, the incoming administration is in a prime position to capitalize on the Duterte groundswell and his strong mandate. The Duterte team should therefore continue to build on our country’s strengths. <b>Key initiatives include improving our country’s business competitiveness, opening various sectors to foreign investors and implementing a comprehensive tax reform package. We note that these will not only strengthen our economy but will also serve as positive catalysts for the ongoing Philippine secular bull market.</b></p>

<div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Long-term trajectory is up</b></p></span>

     
        <p align="justify">A correction is possible considering the recent post-election surge of Philippine stocks. This may be triggered by technical reasons (as the PSE Index is trading at resistance levels), additional placements by listed companies that may sap market liquidity and a probable Fed rate hike within the next few months. Notwithstanding, we believe that our stock market’s long-term trajectory is up. If Duterte successfully capitalizes on the groundswell that propelled him to the presidency, then our stock market may reach new highs in the next year or so. As we have shown, many index stocks have already reached new all-time highs in the few weeks after the elections. <b>It is thus incumbent upon us Filipinos to unite and support incoming President Duterte and his team, as they seek to implement initiatives that will result in a better Philippines, a healthier economy and a stronger stock market.</b></p>



