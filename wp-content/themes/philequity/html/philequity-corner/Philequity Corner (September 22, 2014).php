        <p align="center"><b>Alibaba</b></p>
        <p align="justify">Ali Baba is a popular character from the folk tale “Ali Baba and the Forty Thieves.” He is a
wood cutter who stumbled upon a secret treasure chamber owned by thieves. He used the magic
phrase “open sesame” to unlock the chamber’s entrance and gain access to the thieves’ treasures.
However, this article is not about Ali Baba and his treasures. Instead, we are writing about
Alibaba – the Chinese e-commerce giant which listed in the New York Stock Exchange (NYSE)
last Friday.</p>

        <p align="center"><b>Biggest IPO in history</b></p>
        <p align="justify">Alibaba made history when its initial public offering (IPO) was priced at $68 per share. This
enabled the company to raise $21.8 billion, making it the biggest IPO in world history. This
surpassed previous listings of mammoth Chinese banks such as ABC Bank or ICBC Bank, or
similarly large IPOs of NTT Mobile in Japan or Visa in the US. Last Friday, Alibaba closed at
$93.89 per share, 38 percent higher than its IPO price. At this price, Alibaba is valued at $231
billion, easily making it one of the biggest companies in the world despite being a newcomer to
the stock market. Alibaba is owned by Japan’s Softbank (34 percent), Yahoo (23 percent) and
Jack Ma, the company’s chairman and founder (8.9 percent).</p>

         <p align="center"><b>Open Sesame</b></p>
        <p align="justify">Alibaba is China’s biggest e-commerce company. Launched in 1999, Alibaba now controls 80
percent of the Chinese e-commerce market. In a nutshell, the company connects Chinese sellers
and buyers to the rest of the world. The following are Alibaba’s three main online platforms:</p>
<blockquote>1) Taobao is China’s largest online shopping destination. It is a website that would
probably be comparable to eBay, allowing customers to buy and sell directly from other
customers.<br>2) Tmall is a platform that allows international brands to sell their products directly to
Chinese customers.<br>3) Alibaba.com is the world’s largest online wholesale trading platform for small
businesses. Through this website, small businesses can buy or sell almost anything from
other small businesses.</blockquote>

        <p>A related company to Alibaba is Alipay, practically China’s version of Paypal. Considering this,
Alibaba is actually the Chinese version of eBay, Amazon and Paypal all rolled into one. The total
value of transactions on its online sites amounted to $248 billion last year, more than eBay and
Amazon combined. Alibaba’s revenues come from advertisements placed on its websites as well
as from fees that are generated as a percentage of transaction volume.</p>

        <p>According to the company’s website, the name Alibaba brings to mind the words “open sesame.”
This is an allusion to the phrase that opened the treasure den for the folk tale character Ali Baba.
However, in Alibaba ‘s case, the phrase represents how the internet giant’s platforms open a
doorway to fortune for small businesses in China and elsewhere in the world.</p>

        <p align="center"><b>Crazy Jack</b></p>
        <p align="justify">Alibaba’s ingenious business model and catchy name is clearly a reflection of its founder and
chairman’s colorful personality. Jack Ma is a former English teacher who did not have a degree
from pedigreed Western schools. Moreover, despite being the founder of one of the most
successful tech ventures of all time, Ma does not have coding experience, nor does he consider
himself tech savvy. Despite these obvious handicaps and repeated failures early in life, Ma
would not be deterred. In 1999, Ma started Alibaba in his apartment with 17 of his friends, with a
mission to connect China to the rest of the world via e-commerce.</p>
    <p>Because of his scrawny build, many might be surprised to discover that Ma is a self-confessed
martial arts fanatic. This kind of philosophy and discipline contributed to shaping his thinking
and values, thereby influencing the company’s strategy and execution over the past years. And in
the company’s 10th year anniversary, just as everyone was focused on how Ma would address
his employees, he pulled off a surprise. Wearing a Mohican wig and dressed in a funky leather
costume, Ma sang Elton John’s “Can You Feel the Love Tonight?” to a jampacked audience full
of his employees.</p>
<p>It is because of this unique, quirky and charismatic personality that people have started to call
him “Crazy Jack.” Crazy or not, Ma’s creativity and ingenuity have already made him the richest
man in China and the 2nd richest in Asia.</p>

        <p align="center"><b>Crocodiles of the Yangtze River</b></p>
        <p align="justify">Even as Alibaba was starting, Ma had often shared his long-term vision with his first employees.
He clearly told them that their competitors are not the ones in China but are instead the
innovators from California’s Silicon Valley. Though Alibaba initially faced handicaps in terms
of size, scale and funding compared to its well-entrenched competitors, Ma never backed away
from the challenge. In going up against tech giant eBay, Ma eloquently said, “If eBay are the
sharks in the ocean, we are the crocodiles of the Yangtze river.” It is this willingness to take on
bigger competitors that drove Alibaba to come up with inventive solutions that connected China
to the rest of the world.</p>

        <p align="center"><b>BABA going up</b></p>
        <p align="justify">Alibaba listed in the NYSE using the ticker “BABA.” In Filipino, “baba” means two things – it
as a reference to one’s chin or jawline and it is also an adjective or noun denoting direction, i.e.
down, downward or downstairs. The former can be used by Filipinos to refer to Jack Ma’s
prominent jawline, one of the main facial features that one would notice upon seeing him. The
latter may actually be harder to connect, as the stock was up sharply on its opening day.
Alibaba boasts of ~40 percent revenue growth and ~50 percent margins, thereby driving
explosive profit growth. It is a classic “toll road” business, similar to the likes of wildly
successful companies such as Google, Mastercard and Visa. Moreover, Alibaba is a direct play
on two strong trends in China – growing internet penetration and rising domestic consumption.</p>
<p>Considering these, the future is bright for Alibaba. Hence, we do not discount the possibility that
in the future, Alibaba may overtake bigger and more established stocks to become the most
valuable franchise in the world. Most probably, this BABA is one that will keep going up.</p>

        <p align="center"><b>An Alibaba in the Philippine stock market</b></p>
        <p align="justify">Alibaba is undoubtedly an admirable success story. When we look at Alibaba, we hope that our
country will also find its own Alibaba story. We look forward to the time when there would be a
local IPO that can be comparable to Alibaba in terms of size, attractiveness and growth potential.
A Filipino company like this would capture everyone’s attention and may increase the visibility
of the Philippine stock market, attract more global investors and entice the local public to
participate more in the stock market.</p>

        <p align="center"><b>More big-ticket IPOs</b></p>
        <p align="justify">While the country has seen some very successful local IPOs over the past few years, we believe
that the government and our regulators can do more to encourage more big-ticket listings in the
stock market. For one, the impending moves by the Congress through charter change to raise or
remove foreign ownership limits in sectors such as property, utilities, telcos, media,
infrastructure and banking will encourage the entry of bigger and more established foreign
companies. If this is pursued, mandating these foreign companies that will operate in the country
to list in our stock market may be a sensible requirement.</p>

        <p align="center"><b>Big multinationals listing in the PSE</b></p>
        <p align="justify">We also believe that the government and the Philippine Stock Exchange (PSE) should strongly
encourage big multinational companies such as Shell, Nestle, Unilever and P&G that are already
operating in the Philippines to list in our stock market. Moreover, foreign companies operating
in the country that have received various incentives and perks from the Board of Investments
(BOI) should also be mandated to list locally. Since these foreign companies are already
operating in Philippine soil, it is just fair that we require them to be represented in our stock
market. Doing this will widen sector representation and deepen our stock market. Aside from
providing more investment options to global investors, this will also allow local small investors
to invest in solid, reputable and profitable multinational companies that are operating in the
country. These will lead to a bigger stock market which hopefully will enjoy increased
participation and interest from local retail investors. We believe that this is a feasible win-win
scenario that will benefit everyone concerned.</p>

 
    
