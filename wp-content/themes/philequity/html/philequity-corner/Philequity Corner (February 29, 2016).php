        <p align="center"><b>PLDT’s spectacular bull run</b></p>
        <p align="justify">Below, we show PLDT’s long-term chart from 1984 to 1994. Note that the prices shown are not adjusted
for stock splits and stock dividends. Since there was no PSE Index yet during those times and most of the
trading then was in oil and mining stocks, PLDT was considered as a proxy for the stock market. After
being in the doldrums for many years, PLDT’s stock price surged after the EDSA revolution. If one bought
PLDT shares on February 21, 1986 (last trading day prior to the EDSA revolution), his investment would
have increased by 39% after one day, doubled in one week and quadrupled in six months. Moreover, his
investment would have grown 7x in one year, 49x in five years and 146x in eight years.</p>
    
