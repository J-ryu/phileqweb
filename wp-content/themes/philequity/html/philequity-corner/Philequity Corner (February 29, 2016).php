<p align="center"><b>Learning from history</b></p>
        <p align="justify">The lost years of the early 1980s can be seen thru economic numbers and other financial data. We
believe that our country’s experiences during those times can serve as important lessons not only for
politicians, historians, economists, businessmen and stock market investors, but also for the common
Filipino. There is much to be learned from looking back and studying the lessons of the past. History
provides us with precedents that can guide us in our present and future decisions in economics, politics,
governance, fiscal management and stock market investing. As the popular saying goes, “Those who
cannot remember and learn from the past are condemned to repeat it.”</p>
        
