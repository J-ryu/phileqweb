 <p align="center"><b>Jobo bills – interest rates at 40%</b></p>
        <p align="justify">In 1983, the Philippine government was unable to meet interest payments on its foreign-currency debt,
prompting it to ask for a moratorium on payments. The government received an emergency bailout
from the International Monetary Fund (IMF) in exchange for strict conditions to devalue the peso and
tighten domestic credit. As financial and economic conditions turned from bad to worse, more and more
creditors refused to lend to the Philippine government. The country also suffered from massive capital
flight. These prompted the central bank to issue the Jobo bills (named after then Central Bank Governor
Jose “Jobo” Fernandez). These were central bank bills which fetched astronomical yields of 40%.</p>

        <p align="center"><b>Crowding-out of private sector lending</b></p>
        <p align="justify">Local banks stuffed their balance sheets with Jobo bills, as these offered exorbitant and extremely
attractive yields. This was a time when heavy government borrowing absorbed the local banking sector’s
available lending capacity, thereby crowding-out lending to the private sector. Consequently, the banks
were left with no money for private sector lending. In turn, business activity eventually grinded to a halt,
resulting in the pronounced economic downturn that was witnessed in 1984 and 1985.</p>

        <p align="center"><b>115% peso depreciation in two years</b></p>
        <p align="justify">The table above shows that the peso depreciated against the US dollar by 115% in two years (1983 to 1984). We note, however, that this is the reference or official rate. The black market rate was much higher for the peso as it was extremely difficult to get hold of US dollars. These were signs of extremely tight financial conditions during those times.</p>
        <p>In present terms, the peso’s move from 1983 to 1984 is equivalent to the peso depreciating from the current level of 47.50 to 102.30 in just two years.</p>

        <p align="center"><b>A much different stock market</b></p>
        <p align="justify">The stock market then was much different from the one that we have now. There was no Philippine Stock Exchange (PSE) yet. Instead, the country had two exchanges – the Manila Stock Exchange and the Makati Stock Exchange. There was also no PSE Index yet. Instead, there were three sector indices – the commercial and industrial index, the mining index and the oil index. Most of the trading then was in mining and oil stocks. In the years leading up to the EDSA revolution, value traded was extremely low in the stock market. In most days, value traded was less than a million pesos per day. In fact, many traders and brokers were just playing chess and backgammon in the trading floor during trading hours. Understandably, there was no heavy participation from foreign investors, as our country was grappling with a massive contraction in economic activity, exorbitantly high interest rates and a sharply depreciating currency.</p>

        <p align="center"><b>A jolt of life for the market</b></p>
        <p align="justify">The EDSA revolution not only brought political and economic change but also provided a much needed jolt for the stock market. As we show in the table below, local stocks surged in the weeks and months following the People Power revolt. There was also increased stock market activity, as the change in government and the restoration of democracy served as catalysts for an economic turnaround. The Philippines became the best performing stock market in the world in the eighteen months that followed the 1986 EDSA revolution. However, the post-EDSA bull run was temporarily halted by the coup attempts in 1987 and 1989.</p>

