        <p align="center"><b>The 1986 EDSA revolution and the Philippine stock market</b></p>
        <p align="justify">Last week, our country celebrated the 30th anniversary of the 1986 EDSA revolution. For some, the EDSA
revolt ended an authoritarian rule and restored democracy. For others, 1986 marked the start of our
country’s multi-decade structural transformation. Our country’s population has a median age of 23.4
years. Hence, most of our country’s youth were still very young or were not even born yet when the
EDSA revolution took place. There is also sparse economic and financial data available. These are
probably some reasons why many do not know or remember what happened to our country and our
stock market during those times. In this article, we provide a recap of how financial and economic
conditions were during those times and the effect of People Power on our country’s stock market.</p>

        <p align="center"><b>A bankrupt economy</b></p>
        <p align="justify">Looking at the available economic and financial data clearly shows that our country went through very
agonizing times in the years leading up to the EDSA revolution. In the table below, we show that our
economy went on reverse mode as GDP growth dropped 7.3% in the years 1984 and 1985. Moreover,
the peso depreciated by more than 150% in five years (1981 to 1985). Interest rates also skyrocketed in
1984 and 1985, a clear evidence of tight and stressful financial conditions during those times.</p>

