        <p align="center">Ba de ya, say do you remember<br>Ba de ya, dancing in September<br>Ba de ya, never was a cloudy day</p>

        <p align="justify">The lyrics above are from the refrain of the song “September” by Earth, Wind and Fire. Unfortunately, unlike this upbeat song, September 2015 was a month that traders would rather not remember. There was definitely no dancing for joy and it certainly was a very cloudy time for the stock market.</p>

        <p align="center"><b>What is it with September?</b>
        <p align="justify">Like the song above, there are so many other songs with “September” in the title. Examples of these are “See You in September” by the Happenings, “September in the Rain” by The Beatles, “September Morn” by Neil Diamond and “September Song” by Frank Sinatra. There are also songs without September in the title yet have a September theme, like “When Summer is Gone” by Gary Lewis & The Playboys, which people commonly mistake as “I’ll See You in September” because the song starts out with those words.</p>
        <p align="justify">However, in contrast to the joyful and upbeat songs of September, statistics show that the stock market tends to be subdued and gloomy during this month.</p>

