        <p align="center"><b>Worst 3rd quarter in years</b></p>
        <p align="justify">As the 3rd quarter of 2015 came to a close last week, analysts scrambled to make sense of the carnage. Despite quarter-end window dressing, the MSCI All Country World Index, which is the weighted average of all stock markets in the world, had its steepest quarterly drop in 4 years, falling 6.6%. European indices also had their sharpest 3rd quarter descent since 2011, with corrections of between 7-12% in just 3 months.</p>

        <p>However, equities were not the only victim of this brutal sell-off. Commodities and currencies fell precipitously as well. The Bloomberg Commodity Index logged a 16% drop in the 3rd quarter. At the same time, many emerging market currencies depreciated by double-digit percentages against the US dollar. <b>If there is anything memorable about September 2015, it would be the savage sell-off across all asset classes.</b></p>     

        <p align="center"><b>Emerging markets - first annual net outflow in 27 years</b></p>
        <p align="justify">One reason behind this sell-off is the sheer amount of money leaving emerging markets. According to the Institute of International Finance, emerging market investors will likely end the year with a total outflow of $540 billion from emerging markets. This would be the first annual net outflow since 1988 and comes during a commodity rout which has led to a sustained slowdown in emerging market growth.</p>     

        <p align="center"><b>Philippines least vulnerable, least exposed to external risks</b></p>
        <p align="justify">Looking at the 2nd and 3rd tables above, it is important to note that although the Philippines is not immune from the sell-off, Philippine stocks and the Philippine peso are the clear outperformers in the region. In a report released last week, the S&P assessed the Philippines as the “least vulnerable” to capital outflows, unwinding of domestic credit and China’s growth slowdown. In a similar report, Fitch said that the Philippines is the “least exposed” to external risks. This explains why, despite a deteriorating growth outlook, the Philippines has been outperforming its neighboring countries.</p>

        <p align="center"><b>Santa Claus is coming to town</b></p>
        <p align="justify">We have seen that the performance in the 3rd quarter has been horrendous. Fortunately for market participants, Christmas season is coming. Based on our data, December is the strongest month of the year, with an average return of 4.4% and a 76% chance of being positive. Assuming statistics and probabilities run true to form, it looks like Santa Claus will come to town and rescue our stock market.</p>

