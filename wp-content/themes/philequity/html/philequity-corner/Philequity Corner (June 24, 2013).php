        <p align="center"><b>It’s the strong dollar, stupid</b></p>

        <p>Back in 2005, when we said that the Philippine peso has bottomed against the US dollar (see <i>Peso, The Strongest Currency in Asia,</i> Oct. 24, 2005), many of our readers and investors doubted our call. People then were so accustomed of seeing the peso lose value against the dollar that the thought of the peso strengthening was so far-fetched and unbelievable.</p>

        <p>But now the table has turned. Everyone is so used to seeing the peso appreciate against the dollar that just a couple of months ago many were expecting it to fall below 40. So when the peso started to move in the opposite direction, just when the Philippines received an “investment grade” rating (from Fitch and S&P) for its sovereign debt, many were puzzled.</p>

        <p>Investors are now shocked and worried to see the peso decline 8 percent in just a short period of time. From a low of 40.55 last March, the peso-dollar rate hit an intra-day high 44.17 before closing at 43.72 last Friday.</p>
      
        <p align="center"><b>Dollar strength, not peso weakness</b></p>

        <p>While others are dumbfounded by the recent weakness of the peso, we in Philequity saw it coming. In an article we wrote last April, we noted that despite favourable macro fundamentals arguing for continued peso appreciation, the inherent strength of the US dollar is giving the peso a respite (see<i> Win-win for the Peso,</i> April 8, 2013).</p>
        <p>In another article, we discussed the fundamental reasons behind the resurgence of the US dollar (see<i> The Peso Tops Out,</i> May 27, 2013). We said that it is not because the fundamental underpinnings of the peso have turned negative, but because the fundamentals of the dollar have considerably improved which caused the peso to depreciate along with most other currencies.</p>
        
        <p align="center"><b>Abenomics = Weak Yen</b></p>

        <p align="justify">Among major currencies, the Japanese yen declined the most against the US dollar after the Bank of Japan (BOJ) introduced bold measures of quantitative and qualitative monetary easing or the so-called “Abenomics” which effectively weakens the yen.</p>

        <img src="image/062413chart.jpg" height="300" width="500"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p align="center"><b>Weak yen = Weak Asian currencies</b></p>

        <p>The yen’s sharp decline warranted a corresponding fall in the currencies of manufacturing-heavy Asian economies like South Korea, Singapore and Taiwan in order for them to stay competitive with Japan.</p>

        <img src="image/062413chart1.jpg" height="300" width="500"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>  

        <p align="center"><b>Europe’s recession, China’s soft-patch & decelerating global growth</b></p>
        <p>Recent data also show that global economic growth is decelerating which is bad news for emerging
economies. Decelerating global growth is bad for emerging economies since falling export prices often
necessitate currency depreciation to mitigate profitability squeeze among exporters.</p>

        <p align="center"><b>Lower commodity prices = Weak commodity currencies</b></p>
        <p>Falling commodity prices also weighed heavily on commodity currencies such as the Australian dollar,
New Zealand dollar, South African rand, Brazilian real and Chilean peso. Commodity price weakness is
reflective of weaker global growth and slumping demand for commodities.</p>

        <img src="image/062413chart2.jpg" height="300" width="500"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i> 

        <p align="center"><b>Deleveraging and unwinding of carry trades</b></p>
        <p>The recent spike in the yields of JGBs and US treasuries, in addition to the Fed’s plan to taper QE have
caused a de-leveraging of funds from emerging markets back to the US. The unwinding of carry trades
also caused the sudden appreciation of the US dollar vis-a-vis emerging market currencies (see<i> The Big
Unwind,</i> June 17, 2013).</p> 

        <p>Since most global investors target currency exposures to countries with strong fundamentals such as the
Philippines, Indonesia, Malaysia, Thailand, Mexico and Turkey, these countries succumbed the most to
liquidation pressure. The tables above show the sharp week-on-week drops in their respective currencies
following Ben Bernanke statement last Thursday that the Fed indeed plans to start tapering QE this year.</p>
        
        <p align="center"><b>Peso to stabilize from here</b></p>
        <p>In an article we wrote three weeks ago, we said that after 8 years of peso appreciation which brought the
peso-dollar rate to a low of 40.55, we expect the peso to trade back towards the 42 to 44 range. This we
said is the sweet spot for the peso (see <i>Back to the Sweet Spot</i>, June 3, 2013). In less than a month, the
peso-dollar rate hit the upper end of our target range.</p> 
<p>We now believe that the peso’s decline is overdone. The peso is back at the level where it was trading at
when QE2 started in Nov. 2010. At pre-QE2 peso levels, not only is the QE taper fully priced in, but the
whole improvement in domestic macro fundamentals is being discounted.</p>

    <p>Based on technical analysis, the 44 -44.20 should provide a strong level of resistance. After the recent
sharp move, we expect the peso to consolidate from here on. A pullback towards 42.80 (equivalent to
38.2% retracement) or even 41.90 (equivalent to 61.8% retracement) is highly likely.</p>

    <img src="image/062413chart3.png" height="400" width="800"><br>
