    <p align="center"><b>Peso at current levels is a blessing</b></p>
        <p>While the peso is now considerably weaker against the US dollar compared to the levels three months
ago, it is not something to worry about. In fact, we believe that the peso trading at these levels is a
blessing. While people fret to see the peso at current levels, we in Philequity are actually more concerned
with the peso going to 40 which we believe is counterproductive and generally bad for the economy.</p>
    <p>Peso fundamentals have not changed. And since it is the strengthening of the US dollar against most
currencies which caused the peso to depreciate, we are not concerned. Given the choice, we actually
prefer the peso at current levels than the peso going back to 40. Even if inflation creeps up, overall we
believe this is good for the whole economy.</p>


