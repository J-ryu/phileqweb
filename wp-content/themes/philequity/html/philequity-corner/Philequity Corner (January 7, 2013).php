        <p align="center"><b>Global Bull Market</b></p>
        <p>Global equities posted strong weekly gains to start the new year, with S&P 500 index closing at a five-year high after the much dreaded US Fiscal Cliff event was avoided. Friday’s US jobs report data also showed that the economy is adding jobs at a moderate pace.</p>
        
        <p>The S&P 500 index gained 4.6 percent to 1,466.47 for the week, reaching its highest level since December 2007. The Dow Jones Industrial Average (DJIA) rallied 3.8 percent for the week to close at 13,435.2, while the Nasdaq Composite index climbed 4.8 percent to 3,101.66.</p>

        <p>On the domestic front, the PSE index (PSEi) closed at 5,971.45 (a new all-time high) for a weekly gain of 2.7 percent. For a yearly start, last week’s gain was the PSEi’s biggest in three years.</p>

        <p>Before this, the PSEi had a gain of 5.98% during the first week of 2009 to kick off this global bull market which we in Philequity correctly called. (see <i>Opportunity of a Generation,</i> Nov. 3, 2008 and <i>666 on 3-6-9</i>, April 13, 2009)</p>

        <p align="center"><b>Bang!</b></p>
