        <p align="center"><b>Climbing a wall of worry</b></p>
        <p>After the US fiscal cliff was averted last week, investors will now focus on upcoming battles in the US Congress, including the likelihood of more dramatic struggles over spending cuts and the raising of the federal debt ceiling. Europe continues to be a problem with EU manufacturing sinking deeper into recession in December. There is also a new concern that the Fed will end its $85 billion monthly bond buying program before 2013 ends.</p>
        <p>Meanwhile, the US economy is still experiencing very slow growth. Europe is still trying to fix their house, especially with heavily indebted countries like the PIIGS (Portugal, Ireland, Italy, Greece and Spain). China is still trying to avert a hard-landing. With this background of slow growth, fiscal crises and other macroeconomic fears, central banks will continue to be on their toes (keeping interest rates low and innovating with unconventional policies such as QE) until the global economy regains traction.</p>
        <p>We have, time and again, reminded our readers of the wise saying <i>“don’t fight the Fed”</i> because they have a lot of tools in their arsenal (see<i> Whatever It Takes,</i> Aug. 27, 2012 and <i>Central Banks Winning,</i> Sept. 17, 2012). In fact, this has been our main investment thesis for this global bull market that we are enjoying.</p>

        <p>With the central banks at your back, we believe that the global bull market will continue and that stocks will continue to climb a wall of worry.</p>

