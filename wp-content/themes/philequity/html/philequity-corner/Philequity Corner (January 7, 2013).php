        <p align="center"><b>The Greatest Gift of All</b></p>
        <p>In a period of slow global economic growth and looming deflation brought about by the European debt crisis and the US subprime crisis, we now consider quantitative easing (QE) and low interest rates as the greatest gift to us by Bernanke and other central bankers.</p>

        <p>Using every weapon at their disposal, from rate cuts to outright bond purchases, global central banks led by Fed and the ECB initiated the “Great Monetary Easing” (see <i>The Great Monetary Easing,</i> October 22, 2012 and <i>Christmas Presents,</i> Dec. 24, 2012).</p>

        <p>Following the ECB’s announcement of Outright Monetary Transactions (OMT) and the Fed’s 3rd round of quantitative easing (QE3) in September 2012, the Bank of Japan stepped up its own Asset Purchase Program (APP) in October 2012.</p>

        <p>Other central banks, from countries such as China, Brazil, India, Australia and even the Philippines, did their share using conventional tools such as rate cuts. In mid-2012, the People’s Bank of China (PBoC) surprised many by cutting benchmark rates twice in a span of one month. The PBoC also cut reserve requirements last year and promised to do more this year.</p>

        <p>Thanks to the central banks, we are now witnessing a global bull market in stocks.</p>

        <p align="center"><b>New high means higher</b></p>
        <p>In a past article, we discussed how Newton’s Law of Inertia can be applied to stock markets (see<i> Newton’s Law of Inertia Applied to the Stock Market,</i> Oct. 30, 2006). The law teaches us that a market trending upwards (or downwards) will continue moving up (or down) until a drastic change in fundamentals and sentiment create an equal and opposing reaction.</p>
        <p>As long as central banks continue to ease and maintain interest rates low, the global bull market will continue. The markets will continue to trek higher unless fundamentals drastically change or central banks reverse course. Newton’s law of inertia suggests that “a new high” means higher for the markets.</p>

