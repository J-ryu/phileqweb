        <p align="center"><b>Peso Back to the Sweet Spot</b></p>
        <p>Last week, we presented the fundamental reasons why we believe the peso has topped against the US dollar. These are the strengthening of the US dollar and the weakness of the yen. We enumerated 12 reasons why we think the dollar will continue to strengthen. We also said that Abenomics will keep the yen weak as Japan tries to recover from a two decade-long economic stagnation, and that the peso should weaken with the yen (see <i>Peso Tops Out,</i> May 27, 2013).</p>

        <p>In just five days since making that call, the peso lost 2.52% against the yen, 2.25% against the euro and 1.59% against the US dollar.</p>

        <p>We now present the technical reasons why we think the peso has reached an important top against the US dollar.</p>

        <p align="center"><b>Resistance level at 42 was decisively broken</b></p>
        <p>In our article last week, we said that after 8 long years the peso has topped out, or in other words, <u>the USD:PHP rate has bottomed</u>. This happened when the USD:PHP rate hit 40.55 last March 2013.</p>
        <p>Subsequently, the USD:PHP rate broke above its initial resistance level at 41 in April this year. And last week, it broke decisively above the resistance level at 42, reaching an intraday high of 42.63 last Wednesday before closing at 42.26 on Friday. The USD:PHP rate continues to trend higher and after more consolidation, the next level of major resistance is now seen at 44 (see chart below).</p>

        <img src="image/060313chart.jpg" height="400" width="800"><br>
        <i>Source: Wealth Securities Research</i>

        <p align="center"><b>Moving averages were breached</b></p>
        <p>Aside from breaking above the resistance level at 42, the USD:PHP rate also breached the 50-day, 100-day and 200-day moving averages. In technical analysis, moving averages smooth the price data to form a trend following indicator. Technicians use 50-day, 100-day and 200-day moving averages as guides to the short-term, medium-term and long-term trends, respectively. The USD:PHP rate going above these moving averages reinforces our call that the peso has indeed topped out against the greenback.</p>

        <img src="image/060313chart1.jpg" height="400" width="800"><br>
        <i>Source: Wealth Securities Research</i>

        <p align="center"><b>Downtrend line poised to be broken</b></p>
        <p>The weekly chart below also shows that the peso is attempting to break above the long-term downtrend line established since we called the TOP in the USD:PHP rate back in 2005. If it convincingly breaks, it confirms that the range for the next few years would be 42 to 44.</p>

        <img src="image/060313chart2.jpg" height="400" width="800"><br>
        <i>Source: Wealth Securities Research</i>

        <p align="center"><b>Pull back after huge gains since 2005</b></p>
        <p>One can also make an argument that the peso is just pulling back or giving back some of its gains since bottoming out against the US dollar in 2005.</p>
        <p>Note that since we called the top in the USD-PHP rate back in 2005 (see <i>Peso: The Strongest Currency in Asia,</i> Oct. 24, 2005), the peso has gained 26.9% against the US dollar. In fact, the peso has gained against all major currencies with the exception of the Australian dollar.</p>
        <p>The peso also performed strongly against its Asian counterparts over the same period except for the Thai baht. It has appreciated as much as 39.3% against the Indian rupee and 29.5% against the Korean won since October 2005.</p>

        <img src="image/060313chart3.jpg" height="400" width="800"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p>From a long-term perspective, the peso appears to be giving back just a small part of the huge gains it has registered against most currencies since 2005. Against the US dollar, it has given back just 4.2% since topping out at 40.55 last March, which is miniscule compared to its 26.9% gain against the greenback since 2005.</p>

        <p align="center"><b>Healthy and not a cause for concern</b></p>
        <p>Most people have been accustomed to the peso appreciating for the past 8 years. That’s why the depreciation of the peso last week caused a lot of questions and surprise.</p>

        <p>BSP Gov. Tetangco said last week that the peso trading at these new levels <i>“is still healthy and should not be a cause for concern.”</i> He also added that <i>“the wider trading range does not alter the fundamentals of the peso.”</i></p>

        <p>Meanwhile, Finance Secretary Cesar Purisima added that the peso is dictated by market forces. He remarked: <i>“So long as the changes in the exchange rate are smooth and within the band of competitiveness vis-a-vis our peer currencies, whatever the market sets it at should be the right rate.”</i></p>

        <p>Back to the Sweet spot</p>

        <p align="center"><b>Back to the Sweet spot</b></p>
        <p>One of the major rules of technical analysis says that “once a resistance level is broken, it becomes a new level of support.” This means that the previous resistance level at 41.80 to 42 that was broken last week now becomes support.</p>
        <p>The USD:PHP rate is now expected to trade back to its “sweet spot” between 42 and 44 (see <i>Peso’s Sweet Spot,</i> Jan. 30, 2012). Technical analysis reinforces the view that the peso will trade at these levels in the next few years.</p>

