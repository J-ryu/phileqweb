        <p align="center"><b>It’s the Strong Dollar, Stupid! – Episode 3</b></p>
        <p align="justify">The turmoil in stocks, commodities and currencies continued last week with US stocks registering the worst
10-day start in history. As we have explained in the past, one of the primary reasons for the decline and
extreme volatility in risk assets is the strong US dollar. Last week we showed how stocks were pummeled
by recent moves of China to depreciate its currency (see <i>China Pummels Global Markets,</i> Jan. 11, 2016).
This week we show the rout that happened in currencies.</p>
        
         <p align="center"><b>Episode 3 of the Strong Dollar Saga</b></p>
        <p align="justify">Recall that as early as 2013, we wrote about the strong resurgence of the US dollar and the consequent
topping out of emerging market currencies, including the Philippine peso. In fact, we have written about
this in <b>Chapter 7, The Peso Tops Out,</b> of our book <b>“Opportunity of a Lifetime”,</b> as well as in numerous
articles in Philequity Corner.</p>
<p>With sequels and trilogies gaining popularity in movies recently (e.g. in Star Wars, The Avengers, The
Hunger Games, etc.), we are following up with our own sequel on the US dollar. Hence, we now have
Episode 3 of this three-year long saga (see <i>It’s the Strong Dollar, Stupid!,</i> June 24, 2013 and <i>It’s the Strong
Dollar, Stupid!, Part 2,</i> Oct. 6, 2014).</p>

        <p align="center"><b>“R” currencies rumble</b></p>
        <p align="justify">In the past, we wrote about the “Fragile 5” and the “Troubled 10” currencies. Today, we have the
“Rumbling R-currencies”. Most of these currencies which start with the letter “R” suffered the most vicious
losses this year against the US dollar.</p>
<p>The South African rand which is down 8.5 percent year-to-date against the dollar hit an all-time low last
week. The Russian ruble which dropped 7.1 percent since the start of the year is back to the record low
levels it reached in 2014.</p>
<p>The South African rand, the Russian ruble and the Brazilian real are now down more than 150 percent from
their respective peaks. The persistently falling oil and commodity prices, together with high current
account deficits and high inflation have exacerbated the fragility of these currencies.</p>
<p>Meanwhile, the renminbi which caused the rout in stocks and currencies this year has dropped 8.9 percent
from its peak. While the drop is not much, it has caused much chaos because it has been pegged to the
dollar for the longest time. It also shows the growing importance of China to the world economy and to
the financial markets.</p>

        <img src="image/011816chart.jpg" height="300" width="800">

        <p align="center"><b>Emerging Market currencies crash</b></p>
        <p align="justify">Emerging market currencies crashed in the first two weeks of 2016 led by the Nigerian naira which slumped
to a record low of 305 against the US dollar in the black market. While the Central Bank of Nigeria has
pegged the naira to 199 to the US dollar since March 2015, the shortage of foreign currency has led to the
weakening of the naira in the black market in recent months. Among all currencies, the naira is the worst
performing since the start of the year with a 53.2 percent decline.</p>
<p>The Mexican peso and the Kazakh tenge, which are both trading at historic lows against the US dollar, are
down 6.1 percent and 6.0 percent year-to-date, respectively.</p>
       
        <img src="image/011816chart1.jpg" height="400" width="800">

        <p align="center"><b>Largest decline in oil price history</b></p>
        <p align="justify">Exacerbating the downward slide in EM currencies is the plunge in oil prices. Both WTI and BRENT crude
prices fell toward 30 dollar per barrel level, their lowest in 12 years. <b>THE 75 PERCENT DECLINE IN
CRUDE PRICES IS NOW THE LARGEST DECLINE IN HISTORY.</b></p>

        <img src="image/011816chart2.jpg" height="400" width="800">

        <p align="center"><b>Commodity currencies free-fall</b></p>
        <p align="justify">Currencies of commodity-exporting countries such as the Australian dollar, Canadian dollar and New
Zealand dollar continued to free-fall. The Canadian dollar dropped 11 straight days to register a 13-year
low against the US dollar. Meanwhile Aussie dollar declined to its lowest since 2009. Together with the
New Zealand dollar, these commodity currencies are down more than 5 percent year-to-date.</b></p>

             <img src="image/011816chart3.jpg" height="400" width="800">

         <p align="center"><b>Yen rises on “safe-haven” buying</b></p>
        <p align="justify">The Japanese yen is the only major currency that have risen against the US dollar as investors looked for a
safe haven among developed market currencies. While the euro, Swiss franc and Danish krone have
dropped substantially from their peaks, they are relatively flat this year.</b></p>
    
             <img src="image/011816chart4.jpg" height="400" width="800">

            <p align="center"><b>Philippine peso, down but not out</b></p>
        <p align="justify">Many were caught off-guard by the swift decline of the peso this year. But as we explained in the past, the
peso is down because of the drop in other emerging market currencies. The peso is down by 1.9 percent
year-to-date and down 17.7 percent peak-to-date.</b></p> 
        <P>In comparison, many emerging market currencies are down between 2 to 6 percent year-to-date and down
over 50 percent from their respective peaks.</P>
<P>In order to understand the weakness of the peso you have to understand why the dollar is strengthening.
Please read our archived articles <I>Peso Tops Out</I> (May 27, 2013) and <I>Best House in a Bad Neighborhood</I>
(Sept. 2, 2013), and our book <B>Opportunity of a Lifetime</B>. Our book was published for our readers not
only as a guide for reading past archived articles but also as a learning tool for their investment strategies.</P>


        <p align="center"><b>Peso’s next support at 48-48.50, followed by 50</b></p>
        <p align="justify">The Philippine peso closed at 47.78 vs. the US dollar last week, its lowest level in 6 years. Technical
analysis points at 48-48.50 as the next major support for the peso, followed by 50.</b></p> 
        
