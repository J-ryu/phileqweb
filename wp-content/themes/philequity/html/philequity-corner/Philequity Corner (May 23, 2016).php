    <div align="center" class="page">
    	<span FONT-SIZE: 25pt">
    	<br><p><b>Sudden Stop</b></p></span>

        <div align="justify" class="page">
            <p>Right after the elections, our stock market surged ahead despite the lackluster performance of global equities. Last week, however, the PSE Index suddenly stopped moving upwards. From being one of the strongest performers immediately after the voting, the PSE Index turned into one of the worst performing indices over the past few days.</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>From Bullish to Tentative</b></p></span>

        <div align="justify" class="page">
            <p>In our previous article, we said that incoming President Duterte and his team buoyed investor sentiment after baring their economic plans (<i>The Punisher Shines, Big Win for Stocks,</i> May 16, 2016). The announcement of the administration’s 8-point economic agenda created a bullish and upbeat environment for stocks and resulted in the stock market run-up that we saw immediately after the elections. However, Duterte’s recent pronouncements of offering four key Cabinet positions to communists suddenly made not a few fund managers and investors lose their enthusiasm.</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Diverse Cabinet</b></p></span>

        <div align="justify" class="page">
            <p>There have been talks that Duterte and his team wanted to form a diverse Cabinet, similar to that of Canadian PM Justin Trudeau. The goal is to gather competent, dedicated and experienced individuals from different sectors of the society in order to make a Cabinet that is truly inclusive. We believe that the intention behind forming a diverse Cabinet is good. However, the Duterte team must also tread carefully in appointing Cabinet members since they will be the ones to implement and carry-out the administration’s primary goals and economic agenda.</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Communists in the Cabinet</b></p></span>

        <div align="justify" class="page">
            <p>Duterte’s recent offering of key Cabinet positions to communists came as a startling surprise to the business and investment community. Duterte said that he is willing to accept nominees from the Communist Party of the Philippines (CPP) for the following departments: Department of Environment and Natural Resources (DENR), Department of Agrarian Reform (DAR), Department of Social Welfare and Development (DSWD) and Department of Labor and Employment (DOLE).</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>‘Strike capital of the world’</b></p></span>

        <div align="justify" class="page">
            <p>Businessmen, fund managers and investors became increasingly wary about the possible appointment of hardliners or extremists. They are also worried about the implementation of anti-business policies that can destroy business confidence and dampen investor sentiment. Businessmen recalled a period when the country was called the ‘strike capital of the world.’ This was a time when the Philippines had the most number of labor strikes globally. However, we believe that incoming President Duterte would not allow something like this to happen again.</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Cooperative, Not Combative</b></p></span>

        <div align="justify" class="page">
            <p>CPP founder Joma Sison said that the Cabinet secretaries for DENR, DAR, DSWD and DOLE do not necessarily have to be communists. Instead, they should have competence, diligence and willingness to serve the people. The appointment of communists is actually not the concern. The concern is about the possible appointment of combative individuals who espouse radical policies that may endanger our growth prospects. Appointing such people will run contrary to the Duterte administration’s primary goals. Instead, the appointees should be cooperative team players who can implement their departments’ objectives while still working in consonance with the government’s programs.</p>  

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Labor and Employment</b></p></span>

        <div align="justify" class="page">
            <p>The main source of concern among businessmen, fund managers and investors is the appointment of a DOLE secretary who is antagonistic to business. While the DOLE secretary will be tasked to protect the rights of workers, it should not come at the expense of business and employment growth. The prospective DOLE secretary should have a fair and holistic perspective in order to properly balance the interests of various stakeholders instead of favoring a single sector too much. We also need a DOLE secretary who can inspire confidence from all sectors, including business owners and managers, investors and workers.</p> 

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Pro-business and pro-labor</b></p></span>

        <div align="justify" class="page">
            <p>We believe that different people from various backgrounds can harmonize their advocacies while working towards common goals that will benefit the most people. This does not have to be a polarizing battle between two opposing sides (i.e. business vs. labor) where one side wins and the other side loses. One can be pro-business and also be pro-labor. However, one cannot be pro-labor and anti-business or vice versa, because such a scenario will ultimately lead to the deterioration of businesses and employment prospects.</p>   

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Pro-agriculture</b></p></span>

        <div align="justify" class="page">
            <p>In the same vein, being pro-agrarian reform does not necessarily mean shunning the benefits of big corporate farming. In fact, even Duterte himself said that the country’s land reform program was a failure. We thus hope that agriculture is something that the Duterte administration can fix. The problem should be carefully studied and the goal should be to increase the utilization of farm lands and improve agricultural production. If these initiatives are implemented correctly, the sector may contribute an additional 1-2% to GDP growth while also uplifting the lives of many poor farmers.</p> 

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Pro-environment and pro-mining</b></p></span>

        <div align="justify" class="page">
            <p>Similarly, one can be pro-environment but also be pro-mining. Instead of shunning the mining industry as a whole, the government should mandate mining companies to practice responsible mining and suspend irresponsible miners. Our country’s mines have huge untapped potential. Thus, reviving the sector will contribute in increasing economic output, improving tax revenues and creating more jobs.</p>  

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Market corrects despite 6.9% 1Q16 GDP growth</b></p></span>

        <div align="justify" class="page">
            <p>Last week, official data showed that our economy grew 6.9% in 1Q16. This is one of the fastest growth rates in Asia. According to Finance Secretary Cesar Purisima, this is the first time in 27 years that we outpaced China’s GDP growth. While 1Q16 growth was buoyed by election spending, it was still primarily driven by robust domestic consumption and was accompanied by an uptick in government spending. Despite the good news about our 1Q16 GDP growth, our stock market still corrected.</p> 

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Attracting Investments</b></p></span>

        <div align="justify" class="page">
            <p>One of the Duterte administration’s main goals is to improve the country’s business competitiveness. It was also stated that the incoming administration wants to attract more FDIs, which was lauded by the investment community. However, the recent pronouncements of Duterte to appoint communists in the Cabinet dampened sentiment from businessmen and investors. These sparked renewed concerns about the types of Cabinet appointments that will be made, which stands in contrast to the confidence that many people exhibited when the Duterte team bared its economic agenda. We note that business climate and investor sentiment are very important because we need to attract more capital to support and boost our economic growth.</p> 

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Delicate Balancing Act</b></p></span>

        <div align="justify" class="page">
            <p>We appreciate the Duterte team’s efforts in balancing the interests of different sectors of the society. As it finalizes appointments for Cabinet secretaries, we hope that the incoming administration can appoint individuals who can cooperate in harmonizing various interests and advocacies while unifying to achieve common goals for the country. Thus, Cabinet members should adopt a holistic perspective and come up with fair and well-thought initiatives to support the Duterte administration’s economic program.</p>

    <div align="center" class="page">
        <span FONT-SIZE: 25pt">
        <br><p><b>Bull Market Will Continue</b></p></span>

        <div align="justify" class="page">
            <p>If the Duterte team does what it set out to do, we believe that it has a realistic chance of matching or even exceeding the 6.9% GDP growth that we saw in 1Q16. Implementing the Duterte administration’s economic agenda and addressing bottlenecks in infrastructure are initiatives that will further strengthen our economy while improving the lives of millions of Filpinos. Though the PSE Index has experienced a temporary correction, we believe that our stock market can resume its upward trajectory if the Duterte team appoints Cabinet members that will inspire the confidence of investors and the business community.</p>




