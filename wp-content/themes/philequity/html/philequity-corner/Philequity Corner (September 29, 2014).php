        <p align="center"><b>Risk and Reward</b></p>
        <p align="justify">Investment approaches vary according to the risk one is willing to assume. Respectable private bankers and
mutual funds want to know their client’s investment philosophy to evaluate risk appetite. If the goal is just to
conserve capital and perhaps get a modest return for income enhancement, the portfolio recommendation will
favor fixed-income bonds or defensive dividend-paying stocks. The latter has an upside for appreciation too. If
the risk appetite is heartier (there are carnivores as well as vegans among investors) the mix may tilt towards
aggressive growth stocks that may have both high upside potential as well as downside risks. These profiles can
have interesting designations from “afraid to cross the street on a Sunday” to “does not use the elevated walkway
when crossing C-5 during rush hour.”</p>

        <p>Risk and reward go together. The higher the return anticipated, the higher the risk of losing the entire principal
or a big chunk of it. There are no sure ways to get rich quick. Here are some thoughts to consider.</p>

<p>Cash itself is not entirely risk free. There is a mistaken belief that cash is the safest form of investment. This
belief disregards inflation, and if the cash is in one currency, the effect of depreciation as in recent days can
erode the capital’s value. While cash is seldom kept under a pillow, as this will make it too lumpy to allow sleep,
the interest rate from even a special deposit account, which is low even with recent notches upwards, may not
cover the twin effects of inflation and currency risk. So, “parked cash” entails its own hazard. Still, cash has the
ability to pounce on opportunities such as the fire sale of a property or the price correction of a good stock.</p>

<p>Opportunity cost too has to be considered. Keeping investments in one form necessarily leads to giving up
potential yields for the alternatives. Especially if the present cash hoard is the result of liquidating a position in
equities by taking profits or limiting losses, waiting in the sidelines entails opportunity cost. If the equity markets
are surging, hanging on to cash too tightly means giving up profits from a stock run-up. This loss of opportunity
translates to foregone paper profits. Of course, opportunity cost is theoretical. Loss from an investment is real.</p>

<p>Even the most conservative investor does not keep his total assets in cash. Part of it is invested in real property,
as in the home he lives in. It’s possible to spread the risk and allocate even a third of the assets to equities. To
further cushion the risk impact, it is conservative to invest only in dividend-paying stocks. This allows the investor
to participate in a stock rally, even if modest, while still looking forward to handsome yields. This balanced
approach can work for the sedate money manager.</p>

<p>The metaphor of the stock market as a casino may be too simplistic even if the results in terms of losses can be
identical. Bets in a casino are theoretically decided purely by the luck of the draw and there are no underlying
fundamentals to balance off the risk being undertaken. There is no holding period at all as profit and loss is
handed out quickly in each round. There is too no possibility of having the bets managed by a professional. The
only professional in the casino is the dealer.</p>

<p>It is leveraging that uses margins or loans to make investments that can exacerbate steep declines in value. Here
it is possible to lose more than your original investible funds. Margin calls and redemptions of panicky mutual
fund investors can also accelerate the selling pressure into a panic mode when loans need to be liquidated.</p>

<p>Emotion plays a part in increasing risk. The indifference of the market even to the supposedly good news from
some corporate development like the sale of a risky asset for cash at a profit has introduced a new kind of risk
to novice investors. The anticipation of a stock bump that does not occur is bound to disappoint. As a sage banker
puts it succinctly, “Even when the market is wrong, it is still right.” Even when the price does not reflect the true
value of a stock, it is still the price it trades in.</p>

<p>Funds for investment should not be parked money intended for such operating expenses as payment of
amortizations for the house, tuition fees for the kids, or groceries. Now, the budget for a Mediterranean cruise or
the purchase of a new car to add to the fleet — those can be considered investible funds. If the bulls go on a
rampage, the vacation can be deferred and then reinstated later to include shopping money and that new car.
But that is also a risky thought.</p>

<p>Is cash waiting in the sidelines ready to go back to work? Is there still cash left from the renovations?</p>

<p>The investor has to ask himself what risk levels he is willing to accept. Staying with a strategy of 100-percent
cash in one’s investment portfolio may be a risky proposition too and can lead to investor’s remorse. The same
goes for selling too soon a stock that just keeps on climbing after having gone into hibernation six months earlier.</p>

<p>The time horizon for one’s investment is critical. The risk of selling before it’s right is lessened by a longer time
frame.</p>

<p>As in all things from installing a heater for the shower to managing assets, the help of experts can be handy.
Things are always more complicated than they look. But the dabbler in personal finance always wants to try his
hand and do it himself. Sometimes, he gets lucky.</p>
    
