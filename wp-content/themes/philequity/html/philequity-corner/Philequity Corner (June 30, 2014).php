        <p align="center"><b>Lone Supplier of Nickel Ore to China</b></p>
        <p align="justify">Thus, Indonesia’s move to ban the export of raw nickel ore last January is a very positive development
for our country. In the past few years, Indonesia and the Philippines accounted for 50% and 45% of
China’s nickel imports, respectively. With China now starved of half its supply, it had no choice but to
buy practically all its nickel from the Philippines. Because of our unique situation, we are by default the
only supplier of nickel to China – an unprecedented event. This is confirmed by recent trade data from
China which shows that in May 2014, nearly 99% of the 4M tons of nickel it imported came from the
Philippines.</p>

        <p align="center"><b>Nickel Bonanza</b></p>
        <p align="justify">As a result of Indonesia’s export ban, nickel prices have skyrocketed. See below a 10-year chart of nickel
prices in the London Metal Exchange (LME). After hitting a low of $6/pound in 2013, LME nickel prices
have since risen by 42% to $8.55/pound. Though many foreign research firms expected nickel prices to
rise, the meteoric rise in nickel prices caught them by surprise. At one point, nickel prices reached as
high as $9.80/pound this year.</p>

