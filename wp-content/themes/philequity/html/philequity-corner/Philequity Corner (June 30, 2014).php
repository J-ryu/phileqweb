        <p align="center"><b>From Trash to Cash</b></p>
        <p align="justify">As a result, even low grade nickel ore which was trash and worth nothing in the past is now being
bought by Chinese nickel refiners. Before 2014, only saprolite ore with >1.5% nickel grade were saleable.
Anything with a lower grade than that was treated as waste material. However, in the past month, China
has been purchasing these mounds of dirt to feed its nickel pig iron plants. In fact, our country’s largest
nickel mining company was able to sell this waste material at $40/WMT, providing a windfall for the
company. This goes to show that there is simply not enough nickel to go around.</p>

        <p align="center"><b>Nickel supply deficit worse than 2007</b></p>
        <p align="justify">In the 2 years prior to Indonesia’s raw nickel ore export ban, the nickel market was experiencing an
oversupply, hence the low nickel prices during that period. However, as a result of the ban, the nickel
supply-demand balance swung into a deficit. In order to estimate where nickel prices will go from here,
we would have to look at 2007, when the nickel market was also at a deficit (see chart above). Back
then, LME nickel prices peaked at $24/pound. With consensus expecting the supply deficit for nickel in
2015 to reach a record, there is a strong possibility that nickel prices will go much higher from present
levels.</p>

