        <p align="center"><b>An Extraordinary Blessing</b></p>
        <p align="justify">In a previous article, we have written about how the Philippines is blessed in many different ways (see
<i>There’s More in the Philippines</i>, 13 January 2014). We pointed out the many good things in our country,
such as the strong GDP growth brought about by OFW remittances and BPOs because of the unique
talent and personality of the Filipino. We also cited the reforms that were instituted and PNoy’s focus on
governance, which created a secular bull market for stocks. For this article though, we will put the
spotlight on one of our country’s biggest blessings – mineral resources.</p>

        <p align="center"><b>More Minerals in the Philippines</b></p>
        <p align="justify">In order to highlight our natural resources, we came out with a series of articles about our country’s
main metal exports (see <i>What is Nickel?</i>, 22 November 2010, <i>The ‘Red Metal’</i>, 29 November 2010, and
<i>Gold Sparkles</i>, 13 December 2010). Although these were written more than 3 years ago, the fact
remains that much of our country’s wealth remains underground. According to the DENR, the
Philippines is ranked as the 5th most mineralized country in the world. Rich in many metals, research
firms have estimated that our total mineral reserves amount to as much as US$1.5 trillion – more than
5x our total GDP!</p>
      
        <p align="center"><b>The Unique Philippine Nickel Situation</b></p>
        <p align="justify">Although the Philippines is known for being rich in gold and copper, it is also rich in another mineral –
nickel. Nickel’s main characteristic is its high melting point and exceptional resistance to corrosion,
making it a primary component in the manufacturing of stainless steel. Due to the many applications of
stainless steel, there has been strong demand for nickel, especially from China. In fact, China consumes
more nickel than the rest of the world (see chart below). Fortunately for us, more than 90% of China’s
nickel imports are supplied by only 2 countries – Indonesia and the Philippines. Although Australia and
Russia also produce large amounts of nickel, China does not source nickel from them. The main reason
behind this is the type of ore they produce. Russia and Australia are rich in nickel sulfide, which is not
usable by the ferronickel plants which Chinese nickel refiners favor. Thus, with abundant nickel reserves
and a close proximity to China, our country’s unique position is an extraordinary blessing.</p>

