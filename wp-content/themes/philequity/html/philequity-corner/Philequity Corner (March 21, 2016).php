        <p align="center"><b>Peso stabilizes below 47</b></p>
        <p align="justify">Earlier this year, several foreign banks and investment houses forecasted the Philippine peso to reach 49 to 50 by end-2016 as the global markets suffered its worst two-week start in history. We believe, however, that the peso has hit its low for the year when it reached the low end of our 48-48.50 target (see <i>It’s the Strong Dollar, Stupid!,</i> Episode 3, January 18, 2016).</p>
        <p>The Philippine peso continues to gain ground against the US dollar since bottoming at 48.05 in mid-January. It has since risen 3.6 percent to 46.33 as of Friday’s close.</p>

        <p align="center"><b>Reversal of US dollar strength</b></p>
        <p align="justify">Note that as early as February, we have been seeing that the US dollar strength has reversed, and we quote:</p>
        <p><i>“While we have been bullish on the US dollar since 2013, we are now seeing early indications that the US dollar may be changing its character.” – </i>(see <i><b>Philippines, a sanctuary amidst global turmoil?,</b></i> Feb. 15, 2016)</p>
        <p><b>The Fed’s statement that it is concerned of increased volatility in the global financial markets and indications of a slower pace of Fed rate hikes this year halted the US dollar momentum. These statements effectively lessened the “policy divergence” theme which had supported the dollar higher the past two years.</b></p>
        <p>Furthermore, despite negative rates and an expanded QE program for both BOJ and ECB, both the yen and the euro are now trading stronger against the US dollar.</p>

        <p align="center"><b>From policy divergence to policy convergence</b></p>
        <p align="justify">This shift from “policy divergence” towards “policy convergence” was reinforced last week when a dovish Fed reiterated that they intend to move gradually, signaling an interest rate target between 0.25% and 0.5%. Fed expectations now point to a shallower rate hike path for 2017 and 2018.</p>

        <img src="image/032116chart.jpg" height="400" width="800">

        <p align="center"><b>Dollar unwind supportive of peso, EM currencies & other risk assets</b></p>
        <p align="justify">The pause in the Fed tightening plan spurred a dollar unwind. The weaker dollar is supportive of risk
assets, leading to a sharp rebound in commodities, global equities, DM & EM currencies, including the
Philippine peso.
Prices of WTI crude oil, which at one point was down 30 percent for the year, has rebounded 50 percent
from its lows of $26/barrel and is now up 6 percent year-to-date.</p>

        <p align="center"><b>DM currencies vs. US dollar</b></p>
        <p align="justify">Among developed market currencies, the Japanese yen strengthened the most against the US dollar, up
7.1 percent year-to-date despite negative interest rates. Similarly, the Nordic countries, Switzerland and
the EU, which all have negative rates, have also seen their currencies appreciate against the US dollar this
year. Meanwhile, commodity currencies such as the Australian dollar and Canadian dollar have
rebounded sharply on the back of stabilizing oil and commodity prices.</p>    
        
        <img src="image/032116chart1.jpg" height="400" width="800">

            <p align="center"><b>ASEAN currencies lead regional rally</b></p>
            <p align="justify">The greatest beneficiary of this dovish Fed stance are the currencies of emerging market economies,
            especially the commodity exporters.</p>
        <p>In emerging Asia, ASEAN currencies are leading the rally against the US dollar. The Malaysian ringgit
        and the Indonesian rupiah, Asia’s worst performing currencies last year, have rebounded sharply from
        their lows. Meanwhile, the more resilient Singapore dollar, Thai baht and the Philippine peso have
        likewise rallied together with the region.</p>

           <img src="image/032116chart2.jpg" height="400" width="800">

        <p align="center"><b>Other EM currencies</b></p>
        <p align="justify">Other EM currencies were led by the Brazilian real, Russian ruble and the Chilean peso which have
rebounded sharply on the back of rising commodity prices.</p> 

        <img src="image/032116chart3.jpg" height="400" width="800">

        <p align="center"><b>Surge in EM equity inflows, biggest since 2013</b></p>
        <p align="justify">In addition, portfolio fund inflows into EM equities are on the rise, which in turn is positive of EM
currencies. Emerging market ETF assets surged the most in 3 years during the first two weeks of March
2016. Meanwhile, net foreign inflows to the PSEi amounted to Php 8.2 billion month-to-date, negating
the Php 6.1 billion net foreign outflows during the first two months of the year.</p> 

        <p align="center"><b>PSE Index and EM equities enter a bull market</b></p>
        <p align="justify">Global equity indices which experienced their worst two-week start in history have now turned positive
for the year. In fact, emerging market equities have technically entered a bull market with the iShares
MSCI Emerging Markets ETF (symbol: EEM) up 23 percent since bottoming in mid-January.</p>
<p>A bull market is defined as a market that has risen by 20 percent or more from the low. Thus, the PSE
Index is also back in bull territory after climbing 20 percent from the low of 6,084 registered in January.</p>
    
        <p align="center"><b>Peso’s new range: 45.50 to 47.50</p>
        <p align="justify">Recent signs showing the reversal of the US dollar, bottoming out of EM currencies and EM
equities, including the PSE index, indicate that we have seen the worst for the Philippine peso this
year.</b></p>
<p>The technical picture reinforces this view. Looking at the daily chart, the peso has broken below 47.50
and then 47.00, reversing the trend it established since mid-2015.<b> We now see the peso consolidating
between 45.50 and 47.50 in the short- to medium-term.</b></p>
        
