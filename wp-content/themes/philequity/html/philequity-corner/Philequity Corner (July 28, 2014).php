        <p align="center"><b>SONA 2014 in Tables and Graphs</b></p>
        <p align="justify">The PNoy administration is now on his fourth year. We would like to show you in tables and graphs how the
economy has performed under PNoy for the past year and since he took over in 2010.</p>
        <p>The PSE index was below 3,400 when President Aquino took over in June 2010. It has since increased by 104.8
percent as of last week. This is equivalent to a 19.25 percent compounded annual growth rate (CAGR).</p>

