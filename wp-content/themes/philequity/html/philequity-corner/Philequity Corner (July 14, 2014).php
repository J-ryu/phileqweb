        <p align="center"><b>Philippine Peso: On the way to 42?</b></p>
        <p align="justify">Two months ago, we presented scenarios for the peso (see <i>Scenarios for the Peso</i>, May 5, 2014), one of which calls for the peso breaking below 44, then heading towards 43 near-term and potentially 42 in the intermediate-term. That scenario is now coming into fruition. Last week, the peso went as high as 43.22 vs. the US dollar before closing the week at 43.56.</p>

        <p>With most ASEAN currencies strengthening, the peso will also strengthen and our near-term target of 43 will be tested soon. And as mentioned in our previous articles, a break below 43 would point to a next target of 42 for the peso (see <i>Scenarios for the Peso</i>, May 5, 2014 and <i>BSP firmly in control</i>, May 19, 2014).</p>

        <p><b>USDPHP Daily Chart</b></p>
        <img src="image/071414chart.jpg" height="400" width="800"><br>
        <i>Source: FXstreet.com, Wealth Securities Research</i>

        <p align="center"><b>ASEAN currencies recover from their year-lows</b></p>
        <p align="justify">Since hitting a year-low of 45.49 against the US dollar, the peso has since recovered 4.2 percent. The peso’s recovery is just at par with its ASEAN counterparts which on average also strengthened 4.2 percent from their year-lows against the greenback.</p>

        <img src="image/071414chart1.jpg" height="200" width="700"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p>Below are the ASEAN currency charts with our near-term technical targets.</p>

        <p align="center"><b>Indonesian Rupiah</b></p>
        <p align="justify">The Indonesian rupiah was the worst performing ASEAN currency last year, depreciating by 24 percent against the US dollar. It registered a year-low of 12,268 last January and has since rallied 5.5 percent. Technicals point to the rupiah testing the 11,260 level after breaking below 11,672 last week.</p>

        <img src="image/071414chart2.jpg" height="300" width="700"><br>
        <i>Source: FXstreet.com, Wealth Securities Research</i>

        <p align="center"><b>Malaysian Ringgit</b></p>
        <p align="justify">The Malaysian ringgit hit a year-low of 3.3511 against the US dollar early this year and has since rallied 4.9 percent. The ringgit broke below 3.24 in May and is on its way to retest the 3.14 level.</p>

        <img src="image/071414chart3.jpg" height="300" width="700"><br>
        <i>Source: FXstreet.com, Wealth Securities Research</i>

        <p align="center"><b>Singapore Dollar</b></p>
        <p align="justify">The Singapore dollar was the strongest ASEAN currency last year, depreciating by only 3.3 percent. It reached a year-low of 1.283 last January and has since strengthened by 3.3 percent. After breaking below 1.257 last April, the next technical target lies at 1.234.</p>

        <img src="image/071414chart4.jpg" height="300" width="700"><br>
        <i>Source: FXstreet.com, Wealth Securities Research</i>

        <p align="center"><b>Thai Baht</b></p>
        <p align="justify">Compared to its ASEAN counterparts, the Thai baht has not rallied as much, up by only 2.9 percent from a year-low of 33.11 after it was sidetracked by the military coup that transpired in the country last May. Judging from last week’s price action, it appears that the baht will soon break below 32 which would then point to a new objective of 31.</p>

        <img src="image/071414chart5.jpg" height="300" width="700"><br>
        <i>Source: FXstreet.com, Wealth Securities Research</i>

        <p align="center"><b>Strengthening ASEAN currencies will help the peso break below 43</b></p>
        <p align="justify">At current prices, ASEAN currencies are (on the average) just 2 percent off their near-term targets. Among the five currencies, the Indonesian rupiah and the Thai baht have the best upside. Looking at the charts above, it is highly likely that the Indonesian rupiah and the Thai baht will follow the lead of the Singapore dollar, Philippine peso and the Malaysian ringgit which recovered much faster.</p>

        <p>Going forward, if near-term targets are breached, we are looking at intermediate targets of 30.50 for the baht, 10,757 for the rupiah, 3.07 for the ringgit, 42 for the peso and 1.227 for the Singapore dollar. These targets are (on average) 4.1 percent from current levels.</p>

        <img src="image/071414chart6.jpg" height="300" width="700"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p align="center"><b>Peso appreciation is good for the stock market</b></p>
        <p align="justify">At this point, we believe that a peso appreciation is good for the economy because it will help contain inflation. An appreciating peso will also be a catalyst for the stock market to move higher.</p>


