        <p align="center"><b>Are low oil prices here to stay?</b></p>
        <p align="justify">Last week, we wrote about the fundamental reasons why the price of crude oil is swiftly plunging and how its impact on countries can last for a long time (see <i>The Game Chaner</i>, January 19, 2015). We now present the technical reasons why we think crude oil have just made a major top and why prices can stay low for years or even decades to come.</p>

        <p align="center"><b>2-year Trend line broken</b></p>
        <p align="justify">Crude oil indicated that a possible major top was in place back in August 2014 when prices broke below a two-year trend line, signaling a change in long-term trend. This is shown in the following chart when the price of WTIC Light Crude Oil fell below $95 / bbl.</p>

        <img src="image/012615chart.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.co, Wealth Research</i>

        <p align="center"><b>Declining moving averages</b></p>
        <p align="justify">A major top in oil was confirmed when the 50-day, 100-day and 200-day moving averages of BRENT Crude Oil started trending down. In technical analysis, chartists use the 50-day, 100-day and 200-day moving averages as guides to the short-term, medium-term and long-term trends, respectively.</p>

        <img src="image/012615chart1.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com, Wealth Research</i>

        <p align="center"><b>How low can oil prices go?</b></p>
        <p align="justify">In recent weeks, the decline in crude oil has entered the target zone of the 4-year symmetrical triangle top completed in October which is around $44 / bbl. However, we do not expect it to turn back up any time soon. Any bounce back would now face stiff resistance after several support levels were broken.</p>

        <img src="image/012615chart2.jpg" height="400" width="800"><br>
        <i>Source: Wealth Research</i>

        <p align="center"><b>Broken support turns into resistance</b></p>
        <p align="justify">A principle of technical analysis stipulates that support can turn into resistance. Once the price breaks below a support level, the broken support level can turn into resistance.</p>
        <p>In the case of crude oil, the support levels at 90, 85, 80, 70, 60 and 50 that were broken on the way down will now become resistance levels.</p>

        <p align="center"><b>Oil at $100 was an “aberration”</b></p>
        <p align="justify">Respected billionaire-investor Saudi Prince Alwaleed Bin Talal said in interviews last Friday that crude at $100 was an “aberration.” In fact, he had come out publicly to say that we “will never see crude at $100 again.”</p>
        <p>To put this into perspective, we show that from 2011 to mid-2014 and including a brief period in 2008, oil prices were at historically high levels, higher than where it was during the period 1979 to 1983 when the Iranian revolution and the Iran-Iraq war disrupted oil supplies. And that’s after adjusting for inflation.</p>

