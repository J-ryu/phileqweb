        <p align="center"><b>How long can oil prices stay low?</b></p>
        <p align="justify">Moves like this in crude oil can last for years and decades. The bull market in oil which started during the Arab oil embargo and peaked during the Iran-Iraq War lasted for 8 years (1973 – 1981). It was then followed by a 17-year bear market which lasted up to 1998 when oil hit below $10 / bbl.</p>
        <p>Oil began a new bull market from 1999 onwards and culminated with an all-time high of $147.27 on July 11, 2008. Looking at the chart above, it is clear that we are just on the cusp of another major bear cycle in oil that will last for years or even decades to come.</p>
        <p align="center"><b>Quadruple blessings</b></p>
        <p align="justify">Last week the Philippines received blessings from Pope Francis. In the capital market, the Philippines likewise received multiple blessings. Last Friday, the PSEi closed at an all-time high of 7,548.93. The peso surprisingly appreciated swiftly to 44.14 against the dollar even as the dollar strengthened against the euro to its strongest in 11 years. And crude oil closed at its lowest level in 5 years which is a blessing to Philippine consumers.</p>

       
