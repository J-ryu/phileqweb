        <p align="center"><b>The Global Bull Market Continues</b></p>
        <p align="justify">Many are calling for a correction or even worse a reversal in the Philippine stock market citing many reasons such as valuation, GDP growth, inflation, political upheaval, geopolitics (Iraq war, South China Sea tensions), rising oil prices, etc. But a correction is difficult to predict and it comes in many forms. It may be deep, it may be shallow, or it may even be sideways. It can also be in the form of rotation or shifting from one sector to another. Even more difficult is trying to predict the magnitude and the duration of a correction.</p>
        <p>But while there will always be corrections along the way, we believe it is difficult for the Philippine stock market to go into a deep correction because of the global bull market run that we are witnessing. We believe that the path of least resistance for the Philippine stock market is upwards and that the trajectory going forward is up.</p>
        <p>We now show you the bullish chart formations of global stock markets.</p>

        <p align="center"><b>Bulls on parade</b></p>
        <p align="justify">Global equities continue to smash records last week on just about every major stock market index on the back of strong US economic data and the ECB promising cheap money for years. The MSCI All Cap World Index (ACWI) which tracks 23 developed markets and 23 emerging markets is up 6.14 percent year-to-date. It recently broke above its 2007 high and has set its 4th consecutive record high last Friday.</p>

        <img src="image/070714chart.jpg" height="400" width="800"><br>
        <i>Source: www.msci.com</i>

        <p>In the US, the S&P 500 is up 7.8 percent year-to-date and almost 200 percent since the low in 2009. It is trading at a new record high. Meanwhile, the Nasdaq Composite Index is up 8.3% year-to-date and up 254 percent since the financial crisis low. The Nasdaq is at a 12-year high and is just 10 percent off its tech bubble peak in 2000.</p>

        <img src="image/070714chart1.jpg" height="400" width="800"><br>
        <i>Source: www.stockcharts.com</i>

        <p>In Europe, the STOXX Europe 600 Index, up 6 percent year-to-date, is at a record high. The German DAX, up 4.8% year-to-date, is also at a new high. Meanwhile, the rest of Europe (including the troubled PIIGS) is at multi-year highs.</p>

        <img src="image/070714chart2.jpg" height="400" width="800"><br>
        <i>Source: www.stockcharts.com</i>

        <p>Emerging markets are likewise set to join the global bull market party. The MSCI Emerging Market Index appears to be starting the next leg of its bull market after a three-year consolidation.</p>

        <p>India’s SENSEX Index which is up 22.6% year-to-date is trading at a new all-time high. Taiwan’s TWSE index and Korea KOSPI are at multi-year highs while Brazil’s BOVESPA index is breaking out of a three-year downtrend. Meanwhile, China is holding support and showing some near-term strength.</p>

        <p>ASEAN markets led by the Philippines’ PSE Index, which is up 18.2 percent year-to-date, are on their way to retest previous highs after a one-year consolidation.</p>

        <img src="image/070714chart3.jpg" height="400" width="800"><br>
        <i>Source: www.stockcharts.com</i>

        <p align="center"><b>PSE Index targets 7,400 by 1H2015 and 8,100 by 1H2016</b></p>
        <p align="justify">After consolidating between 6,600 and 6,900 for three months, the PSE index convincingly broke above 6,900 last week. This means that the next upward leg in the PSEi’s long-term bull market is already in progress. Philequity’s technical targets for the PSEi are 7,400 by 1H2015 and 8,100 by 1H2016.</p> 

        <img src="image/070714chart4.png" height="400" width="800">

        <p align="center"><b>The most unloved bull-market ever</b></p>
        <p align="justify">We are continually amazed how this bull market has been unloved and why many investors remain hesitant, or underinvested. In fact some have not even participated in this global bull market. Just look at the charts above and see where they are going rather than trying to over-read or overanalyze the markets.</p> 

        <p>With major central banks continuously wanting to ease and whose primary goal is to bring growth to the global economy, we believe that the global bull market will continue on its upward path.</p>

