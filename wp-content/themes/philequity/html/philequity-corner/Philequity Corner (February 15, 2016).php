        <p align="center"><b>Philippines, a sanctuary amidst global turmoil?</b></p>
        <p align="justify">While other global markets were falling sharply the past two weeks, we noticed that the Philippine markets were relatively stable. In fact, the peso appreciated against the US dollar and the stock market was steady.</p>
        <p>Has the Philippine market become a sanctuary of stability?</p>
        <p>What we are witnessing in the Philippine market are early signs of stability. These may indicate that we are not affected as much by the increased volatility in global markets.</p>

        <p align="center"><b>Calmness in ASEAN markets</b></p>
        <p align="justify">We also noticed a relative calmness in the ASEAN stock markets over the past two weeks as US, Europe and Japan were fiercely sold. Major global banks such as Deutsche Bank, Credit Suisse, Bank of America and Citigroup traded well below tangible book value. Surprisingly, there was not much of an aggressive foreign selling in ASEAN stock markets.</p>

        <p align="center"><b>Reversal of US dollar strength?</b></p>
        <p align="justify">While we have been bullish on the US dollar since 2013, we are now seeing early indications that the US dollar may be changing its character. Note that during the past two weeks, the US dollar index fell 3.6 percent, its sharpest two-week decline since the dollar bull-run started in 2011 (see chart below). The yen got off to its best two-week run since 2008, while crude oil posted its biggest one-day gain in 7 years.</p>
        <p>While the dollar index is still in a range and it is too early to call for a reversal in the US dollar index, the pause has certainly given the Philippine peso, EM and commodity currencies a much needed reprieve.</p>
