         <p align="center"><b>US rate hike expectations toned down</b></p>
        <p align="justify">Recently, a slew of negative news and the testimony of Fed Chair Janet Yellen to the US Congress have reduced Fed rate hike expectations. Last Feb.3 the US ISM Non-Manufacturing PMI data missed the estimate triggering a decline in US Treasury yields and a sharp sell-off in the US dollar.</p>

        <p align="center"><b>Japanese yen bottoms out</b></p>
        <p align="justify">The Bank of Japan’s announcement of negative interest rates (NIRP) last month failed to stop the yen’s appreciation. After a knee-jerk push to 121.69 on January 29, the yen has since appreciated by 7 percent to 113.2 against the US dollar. This is the yen’s biggest two-week rally since 1998. Many of the carry trades using the yen as the funding currency are now being reversed.</p>
        <p>The weekly chart of USD/JPY below shows a completed head & shoulders pattern last week, pointing to a target of 105.50. Despite NIRP, the chart shows that the yen may have bottomed out against the US dollar.</p>

