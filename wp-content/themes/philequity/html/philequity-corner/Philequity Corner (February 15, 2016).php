         <p align="center"><b>Euro and other DM currencies strengthen vs. the US dollar</b></p>
        <p align="justify">In addition to the yen, a growing number of developed market currencies are already up year-to-date against the US dollar as shown below. This is a complete reversal of their weak performance last month. The yen and the euro are the top performers with a 5.8 percent and 3.6 percent year-to-date gains, respectively.</p>
<img src="image/021516chart2.jpg" height="400" width="800">

        <p align="center"><b>ASEAN currencies lead EM currencies</b></p>
        <p align="justify">ASEAN currencies are mostly up against the greenback year-to-date with the Philippine peso trying to catch up. The peso has since rallied to 47.61 against the US dollar after hitting a 6-year low of 48.05 last month. ASEAN currencies have significantly outperformed other EM currencies since the start of the 2016.</p>

        <img src="image/021516chart3.jpg" height="400" width="800">

        <p align="center"><b>Commodity currencies continue to catch up</b></p>
        <p align="justify">Commodity currencies continue to catch up, more so if the strong upsurge in oil and other commodity
prices last Friday are sustained.</p>

        <img src="image/021516chart4.jpg" height="400" width="800">

        <p align="center"><b>Crude oil and commodities post biggest gain in 7 years</b></p>
        <p align="justify">Crude oil rallied 12 percent last Friday, registering its biggest one-day gain in 7 years. Among the
reasons cited for the rally are the renewed optimism of coordinated production cuts by OPEC and the
lower rig count data released last week.</p>
<p>Meanwhile, the Reuters/Jeffries CRB index rallied by 3.5 percent last Friday. It was led by gold and
silver which were up by 5.5 percent and 4.9 percent last week, respectively.</p>
<p>Crude oil and commodity prices generally have an inverse correlation with the US dollar, which means
that as the dollar falls, oil and commodity prices rise.</p>


        <p align="center"><b>BSP’s steadfast stance</b></p>
        <p align="justify">Unlike the US which may have had a premature hiking of interest rates or the ECB, BOJ & other central
banks trying frantically to lower interest rates, the BSP has been steadfast on its interest rate and monetary
policy.</p>
        <p>We would like to congratulate the BSP who remained steady on its policy stance, neither hiking nor
adding stimulus.</p>

        <p align="center"><b>Recent signs of stability may indicate a bottom in the PSEi</b></p>
        <p align="justify">Recent signs showing the Philippines as a sanctuary of stability may indicate that we may have seen the
worst for the Philippine stock market. As we indicated during our investors briefing last January 30, the
Philippine market may have reached the bottom when the PSEi hit 6,084.</p>

