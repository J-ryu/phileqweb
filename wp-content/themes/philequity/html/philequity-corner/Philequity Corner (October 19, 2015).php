        <p align="center"><b>ASEAN Currencies, Blow-off Tops</b></p>
        <p align="justify">Last week, we noted the dramatic reversal of emerging market currencies, especially among ASEAN currencies such as the Indonesian rupiah, Malaysian ringgit, Singapore dollar and the Philippine peso (see <i>Reversal,</i> October 12, 2015). The increased likelihood of a further delay in the long-awaited Fed rate hike following a disappointing US jobs report has spurred sharp rallies in EM currencies.</p>
        <p>Last week, we noted the dramatic reversal of emerging market currencies, especially among ASEAN currencies such as the Indonesian rupiah, Malaysian ringgit, Singapore dollar and the Philippine peso (see Reversal, October 12, 2015). The increased likelihood of a further delay in the long-awaited Fed rate hike following a disappointing US jobs report has spurred sharp rallies in EM currencies.</p>

        <p align="center"><b>Indonesian Rupiah</b></p>
        <p align="justify">USD/IDR shot up to 1997 Asian Crisis levels, after a parabolic move last month rocketed it towards 14,753.2. It has since declined 9.2 percent near its weekly trend line support at around 13,500.</p>
        <p>A look at the weekly chart would show that a decisive break below this trend line would validate the topping pattern in USD/IDR. On the daily chart, one can see that the rupiah has recovered all of what it lost since the devaluation of the Chinese yuan last August 11.</p>

        <img src="image/101915chart.jpg" height="400" width="800">
    
        <p align="center"><b>Malaysian ringgit</b></p>
        <p align="justify">A similar parabolic rise is seen on the USD/MYR weekly chart. It reached 4.45 last month, way past its 1997 peg. But it has since collapsed 7.7 percent towards 4.13. On the daily chart, one can see a rare “inverted triangle top.” In this case, the rising hypotenuse should act as resistance at around 4.25 – 4.30 on subsequent rallies.</p>

        <img src="image/101915chart1.jpg" height="400" width="800">
        
        <p align="center"><b>Singapore dollar</b></p>
        <p align="justify">USD/SGD met its “measured move” target of 1.44 as shown in the weekly chart. It has since declined 4 percent to 1.3817. Similar to the ringgit, the daily chart of the Singapore dollar shows an “inverted triangle top” or “broadening top” as some technicians call it.</p>

        <img src="image/101915chart2.jpg" height="400" width="800">

        <p align="center"><b>Philippine Peso</b></p>
        <p align="justify">USD/PHP meanwhile has made a double top at 47 as seen on the daily chart. We expect it to retest support at the 45 – 45.50 range. Similar to the rupiah and the Singapore dollar, the peso has completely unwound the effects of the Chinese yuan devaluation.</p>
        <p>Looking at the weekly chart, a break below the 45 level which is both a trend line support and a previous resistance level (now turned support) would be bullish for the peso. If this happens, we see USD/PHP further stabilizing and going back to its previous trading range of 43 to 45.</p>

        <img src="image/101915chart3.jpg" height="400" width="800">

        <p align="center"><b>Crowded trades</b></p>
        <p align="justify">The “short EM currencies” and “long US dollar” were the most crowded trades in September and October, according to the recent Bank of America Merrill Lynch Survey of Global Fund Manager Survey. Hence, it is not surprising to see sharp turnarounds in EM currencies as global fund managers unwound these crowded trades.</p>

        <p align="center"><b>Philippine peso and stocks bottom out</b></p>
        <p align="justify">The rally in EM & ASEAN currencies, including the peso, have been accelerating, increasing the likelihood of additional gains in stocks. As we have noted in previous articles, there is a correlation between currency movement and stocks. The break of September lows (as shown above) indicates a top in place for the dollar and a low in these four ASEAN currencies. This reinforces our call that we may have seen the low for Philippine stocks in this correction period.</p>

