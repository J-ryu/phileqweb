            <p align="center"><b>Rupee weak due to structural problems</b></p>
            <p>The Indian rupee, meanwhile, is unlikely to regain much ground against the US dollar due to structural problems in the Indian economy. India’s twin fiscal and current account deficits and weak reserves continue to weigh on the currency.</p>
            <p>From 2004 to 2008 (fiscal year ending March), the government budget deficit averaged 3.25 percent of GDP. But from 2009 to 2013, the deficit ballooned to an average of 6.1 percent of GDP. Meanwhile, the current account deficit to GDP averaged just 0.4 percent from 2004 to 2008. But from 2009 to 2013, it swelled to an average of 3.4 percent of GDP</p>
