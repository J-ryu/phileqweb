            <p align="center"><b>44-level should hold</b></p>
            <p>Compared to India, the Philippines appear to be ahead in terms of structural reforms (see <i>Secular Bull Market,</i> January 28, 2013). During the past two decades, a number of structural reforms were passed, such as the implementation of EVAT, oil deregulation, and the abolition of the Oil Price Stabilization Fund (OPSF), which eased the government’s fiscal balances. On top of this, robust OFW remittances and BPO revenues have shored up the country’s current account.</p>
            <p>Our country’s foreign exchange reserves now stands at $82.9 billion or equivalent to one-year worth of imports. In comparison, India’s $280 billion of reserves covers just 7 months worth of imports. This gives the Bangko Sentral ng Pilipinas (BSP) more dry powder to curb foreign exchange volatility.</p>
            <p>While the peso may not be immune to future volatility and further weakening of emerging market currencies when the US Fed starts to implement QE tapering, we believe the peso will show more stability.</p>
            <p>Near-term, we expect the peso to strengthen due to the encouraging growth rebound in China and the subsequent rally in emerging market currencies. Unless emerging market currencies resume their sharp losses against the US dollar, we expect the 44 support level to hold</p>

