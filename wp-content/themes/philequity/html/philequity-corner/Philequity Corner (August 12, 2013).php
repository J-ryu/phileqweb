        <p align="center"><b>Peso support at 44 holding ground</b></p>
        <p align="justify">It appears that the peso’s support at the 44 level will hold ground despite weakness in some regional currencies last week. Many were concerned that the peso might breach the upper level of its sweet spot (42 to 44) against the US dollar after the Indian rupee registered new record lows against the greenback while the Malaysian ringgit and the Indonesian rupiah reached new 3-year lows. But strong China data last Thursday spurred recoveries in emerging market currencies.</p>

        <p align="center"><b>China showing early signs of recovery</b></p>
        <p align="justify">The trade data released last week showed early signs that the slowdown in China was nearing an end after imports and exports rebounded in July. In addition, manufacturing companies reported increased expansion in Chinese factory activity last month. Copper and other metal prices surged following the news. Meanwhile, the so-called commodity currencies such as the Australian dollar, Canadian dollar, and the Brazilian real rose to their highest levels this month.</p>
        
        <p>The Australian dollar found support at 0.885 against the US dollar after declining as much as 14.3 percent this year. It closed at 0.9206 last Friday.</p>

