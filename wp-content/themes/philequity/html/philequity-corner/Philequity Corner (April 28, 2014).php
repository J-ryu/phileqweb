        <p align="center"><b>Time to grow</b></p>
        <p align="justify">What can we gather from this investor’s experience? It is the significance of one’s time in the market – how long you actually hold on to your investments. From 1994 to present, Philippine stocks have gone through three bear markets – the 1997 Asian Financial Crisis, the 2000 Philippine political crisis and the 2008 US Sub-Prime Crisis. Yet, this investor stayed put. Would you have done the same? Assuming you chose a fund that invests responsibly in fundamentally sound stocks, do you give your investments enough time to grow?</p>
        <p>This investor has supported the old adage of "Time in the market is better than timing the market." It is very difficult, even for professional traders, to catch extreme peaks and troughs in the market. Hence, we recommend emulating our "one-time-big-time" investor in terms of his long-term frame of mind. Not only has it proven to be profitable, but it also seems very stress-free.</p>

        <p align="center"><b>Diversification and Expertise</b></p>
        <p align="justify">We would like to point out that this buy and hold strategy would not work in all cases. If the investment was in purely speculative and illiquid stocks, then it could have resulted in dreadfully large losses.</p>
        <p>Investing in a fund allows investors to diversify their exposure to a basket of stocks. Moreover, they can avail of the expertise of a fund manager who can choose stocks that will do well in the long term. Also, a seasoned fund manager would have a better sense of when to increase or reduce exposure in the market. He has the ability to substantially reduce equity exposure during severe market meltdowns. He can shift to a defensive portfolio in market corrections. Or he can significantly increase weighting in stocks with higher beta during bull markets.</p>

        <p align="center"><b>The case for a competent fund manager</b></p>
        <p align="justify">The two investment cases we had presented show clearly the reasons for investing in stocks. However, it is equally important that your investment is managed by an experienced and competent fund manager. Below, we show the performance of the PSE index as compared to our Philequity Fund. Our Philequity Fund has returned 3,200% since inception. The PSE index in contrast has a return of 139% over the same period. Including cash dividends and reinvesting these in the index would have returned around 252% for the PSE Index.</p>

