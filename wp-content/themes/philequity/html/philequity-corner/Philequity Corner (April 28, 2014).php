        <p align="center"><b>Time in the Market vs. Timing the Market</b></p>
        <p align="justify">In our article four weeks ago, we showed how people can profit from the Peso Cost Averaging method (<i>Example of a Real-Life Investor</i>, March 31, 2014). We recounted the inspiring story of an actual investor of the Philequity Fund who invested P5,000 to P10,000 on a weekly basis. In our article today, however, we show the ledger of another investor who employed a different style of investing but still made a lot of money.</p>

        <p align="center"><b>Recalling the Peso Cost Averaging method</b></p>
        <p align="justify">Like we discussed previously, Peso Cost Averaging is an investing technique which involves buying a fixed peso amount of stocks on a regular schedule. As we showed during our March 8 Investors’ Briefing and in our article last March 31, this method is a stress-free way of investing. This is because it allows people to grow their investment position gradually instead of committing a large sum all in one go.</p>

        <p align="center"><b>One time, Big time</b></p>
        <p align="justify">Let us now look at the statement of account of another investor that we presented during our Investors’ Briefing. As you can see, he invested P1.56 million back in 1994 (when the Philequity Fund began). He has neither withdrawn from nor added to his investment since. Today, his lump sum or “one-time-big-time” investment has grown to over P51 million! This translates to a CAGR of 19% for the past 20 years.</p>

