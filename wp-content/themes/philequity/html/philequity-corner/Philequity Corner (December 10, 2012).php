        <p>For the benefit of our readers, we shall discuss in this article how we value banks. We will explain how
we compute a bank’s book value and the criteria that we use to assign the appropriate Price to Book
Value (P/BV) multiple for different banks.</p>

        <p align="center"><b>Valuing the Book</b></p>
        <p>The balance sheets of different banks are more or less similar in composition. They are primarily
composed of assets and liabilities that can be valued at market. The book value of a bank therefore
reflects its liquidation value. This is why banks are primarily valued based on book value as opposed to
other counters or companies, which are valued mostly based on earnings or cash flows.</p>
        <p>One can push this analysis further by carefully scrutinizing the financial statements of a bank. Different computations of a bank’s book value may be arrived at depending on the level of one’s analysis. Below, we enumerate the different ways of computing for a bank’s book value.</p>

        <blockquote><b>1. Book Value.</b>&nbsp;This is the simplest and most commonly-used computation. Book value refers to the bank’s capital that is attributable to its common shareholders. This computation is the one that is primarily used in valuing listed local banks because most of their balance sheets are already clean and strong.</blockquote>
        <blockquote><b>2. Tangible Book Value.</b>&nbsp;This computation excludes a bank’s intangible assets such as goodwill and deferred charges. Intangible assets are excluded because the bank can neither sell nor earn from them. This is the method used to value US banks because most of them have significant amounts of intangible assets in their balance sheets.</blockquote>
        <blockquote><b>3. Adjusted Book Value.</b>&nbsp;On top of excluding a bank’s intangible assets, this computation assumes a certain write-off of the bank’s non-performing assets, such as loans and repossessed assets. This is the basis that we used when we said that PNB-Allied could fetch a takeover price of P118, which is equivalent to 2.0x P/BV of our 2012 Adjusted Book Value per Share estimate of P59. We view this as a realistic estimate of the takeover price for PNB-Allied because it is at the lower end of the 2.0 to 2.5x P/BV valuation range of previously acquired local banks.</blockquote>
        <blockquote><b>4. Fully Adjusted Book Value.</b>&nbsp;This usually involves due diligence of the bank’s assets and a line-by-line analysis of the bank’s financial statements. This is typically used to arrive at a precise valuation of the bank when it is being acquired by another entity.</blockquote>

        <p>Even though a bank’s book value gives an estimate of the bank’s liquidation value, it still does not give a complete picture of what the bank should actually be worth. The book value computation does not consider the ability of the bank to create value for its shareholders.</p>

        <p align="center"><b>The Magic Multiple</b></p>
        <p>A bank’s management is expected to create value from the bank’s assets. Though the balance sheets of banks are really similar in nature, some banks appear to create more value for their shareholders than others. A bank’s ability to create value for its shareholders may be quantified by looking at certain metrics and qualities. We enumerate some of the criteria that we use below.</p>

        <blockquote><b>1. Return on Equity (ROE).</b>&nbsp;We usually prefer banks that are more profitable than others based on ROE. This ratio measures how big a bank’s profit is in relation to its capital. At a steady-state level, ROE gives us an idea about the rate at which the bank is organically growing its capital.</blockquote>

        <blockquote><b>2. Quality and consistency of earnings.</b>&nbsp;Among banks that deliver relatively high ROE, we prefer those that have strong core banking operations. We look for banks that can consistently deliver good quality earnings as measured by Recurring ROE.</blockquote>

        <blockquote><b>3. Size.</b>&nbsp;In a past article, we explained how size matters and how size is usually an advantage for Philippine banks (<i>Philippine Banking Giants,</i> June 13, 2011). This is the reason why the biggest local banks (BDO, BPI and MBT) trade at a premium relative to the other listed local banks.</blockquote>

        <blockquote><b>4. Growth.</b>&nbsp;We like banks that make clear efforts to grow their assets and earnings. Asset growth signifies that the management wants to leverage its operations, attain economies of scale and widen its market presence. Earnings growth, on the other hand, shows that the bank’s management is optimizing bank operations in order to deliver more profits to its shareholders.</blockquote>

