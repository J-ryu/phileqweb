        <p align="center"><b>Banking on Banks</b></p>
        <p>Two weeks ago, we wrote about the possible BPI-PNB-Allied merger (<i>Hunt for Banking Supremacy</i>,
November 26, 2012). The proposed merger between the country’s 3rd and 4th biggest banks is expected
to give rise to the largest bank in the country. Though many expected the merger to be finalized shortly
after it came out in the news, it seems that the merger may have hit a stumbling block, probably
because of one or more of the following:</p>
        <blockquote>1. Acquisition price and valuation of PNB-Allied<br>2. Post-merger shareholder structure of BPI-PNB-Allied<br>3. Board representation in the merged BPI-PNB-Allied<br>4. Other personal or family matters that might have affected the proposed merger</blockquote>

        <p>Though there have been no recent updates on the proposed BPI-PNB-Allied merger, local banking stocks
still continued to move higher. Normally, the stock prices of potential acquirers go down when there is
an acquisition in play. Surprisingly, however, the opposite has happened as potential acquirers such as
BPI, BDO and MBT continued to make new all-time highs. This is likely due to the market perception that
PNB-Allied will probably be a good acquisition for any of the country’s three biggest banks.</p>

