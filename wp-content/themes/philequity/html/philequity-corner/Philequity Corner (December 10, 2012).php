        <p align="center"><b>Banks Drive the PSE Index</b></p>
        <p>It seems that the local banking sector is ripe for another wave of M&A activity. Local banks currently
have relatively higher capitalization ratios compared to their regional counterparts. Moreover, their
balance sheets are significantly stronger now compared to the previous times (1999 to 2000 and 2005 to
2007) when the sector experienced heightened M&A activity. Against this backdrop, the merger talks
involving some of the country’s biggest banks have propelled not only banking stocks but also the local
stock market to new highs. This is probably a telltale sign that the stock market perceives these mergers
as beneficial not only for the possible acquirers and acquirees but also for the country as a whole.</p>

<p align="center"><b>Secular Bull Market</b></p>
        <p>Last week, the PSE Index closed at 5,794, almost reaching our end-2012 target of 5,800. As we discussed
in previous articles (<i>PSEi, All-Time High, </i>July 18, 2011 and 5000, March 5, 2012), the country’s
fundamentals remain strong, providing an impetus to the continued resilient performance of our stock
market. With that said, we firmly believe that the secular bull market is intact and this bull market that
started in March 2009 (<i>Opportunity of a Generation, </i>November 3, 2008 and <i>666 on 3-6-9,</i> April 13,
2009) will continue to surge on for many years to come. Considering this, we would like to re-emphasize
what we have repeatedly advocated in previous articles. Investors should continue to use a buy-on-dips
strategy and view market corrections as buying opportunities.</p>


