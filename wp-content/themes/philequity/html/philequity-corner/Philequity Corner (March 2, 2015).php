        <p align="center"><b>Interim target of 7,900 met</b></p>
        <p align="justify"><i>Last week, this interim target of 7,900 may have been met when the PSE index reached 7,862 or just 0.5 percent short of our “measured move” price target. The market now appears to be taking a breather, pulling back 1.2 percent week-on-week.</p>
            <p>Note that this is only the PSE index’s first weekly loss after a blistering pace of nine consecutive weekly gains.</p>

        <p align="center"><b>Corrections are hard to predict</b></p>
        <p align="justify"><i>Time and again, we have said that corrections come in many forms and are difficult to predict. A pullback or correction may be deep like those in the 2nd half of 2013, or shallow like in the 1st half of 2014. Corrections can also be sideways or range bound like that from September to December 2014. It can also be in the form of rotation or shifting from one sector to another.</p>
            <p>Trying to predict the magnitude and the duration of corrections, therefore, is not only difficult but futile.</p>

        <p align="center"><b>A restive and healthy pause</b></p>
        <p align="justify"><i>Healthy trending markets have multiple pullbacks or corrections within a longer term uptrend. Just like in a marathon where runners need a second wind, a bull market also needs restive and healthy pauses (whatever form or shape), especially after an extended and exhaustive run which took the market 1,000 points up in 40 days.</p>

        <p align="center"><b>The long-term trajectory is higher</b></p>
        <p align="justify"><i>While we don’t know how the next correction will look like, what is important is that the long-term trend of the Philippine stock market continues to be bullish. And although the market now appears to be pausing or consolidating after nearing our “measured move” target of 7,900, <u>it does not mean that the move ends here</u>.</p>
            <p>The next technical move will depend on what kind of consolidation pattern comes about. It is really the fundamentals that will carry the market to the next level. However, what is important to note is that <u>the long-term trajectory is higher</u>.</p>
       
