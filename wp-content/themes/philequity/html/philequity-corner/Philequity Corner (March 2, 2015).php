        <p align="center"><b>1,000 points in 40 days</b></p>
        <p align="justify"><i>“What we are currently witnessing is a bull market in Philippine stocks.”</i> – (<i>Stock Market Outlook for 2015</i>, Philequity Corner, February 9, 2015)</p>
        <p>In just 40 trading days, the PSE index rallied nearly 1,000 points. From a low of 6,882 in mid-December, the index surged 14.2 percent to an all-time high of 7,862 last Wednesday - just a tad short of our interim target of 7,900 for the PSE index</p>
        <p>Given the steep market run-up in such a short time, many of our readers are asking what will happen to the market next? How did we come up with a PSE index target of 7,900 in the first place? Will the index reverse, correct, or continue upwards after hitting this level?</p>

        <p align="center"><b>Using of technical analysis as a tool</b></p>
        <p align="justify"><i>While fundamental analysis (financial statement analysis, valuation, etc.) is the core of our investment decision making, we at Philequity use technical analysis to gauge market direction by identifying trends and patterns in charts. Technical analysis also serves as a guide post to time our buying and selling decisions.</p>
        <p>We also use technical analysis to “project” potential targets based on confirmed pattern breakouts. Thus, we came up with an interim target of 7,900 for the PSE index which is based on a “measured move” following the index breakout to new all-time highs above 7,400.</p>

        <p align="center"><b>What is a measured move?</b></p>
        <p align="justify"><i>A measured move is a basic concept in classical technical analysis. In essence, a measured move is a projection or forecast of the upcoming price move after a chart event, e.g. a completion of a pattern.</p>
            <p>In the case of the PSE index, a “rectangle” pattern was formed when it traded in a 500-point range between 6,900 and 7,400 from September 2014 to December 2014. The break above 7,400 or top part of the rectangle last January projected a 500-point rise to 7,900 as the target of the subsequent rise (see chart below).</p>

