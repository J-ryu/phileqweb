        <p align="center"><b>Scenarios for the Peso</b></p>
        <p align="justify">Since we started this column in Feb. 2005, Philequity has made three major calls on the peso. First was our call
of the peso’s bottom in 2005 (see <i>Peso, the Strongest Currency in Asia</i>, Oct. 24, 2005). Second was our call of
the peso’s top last year (see <i>The Peso Tops Out</i>, May 27, 2013). </p>

    <p>Third and most recent was our call that the peso’s support level at 45.50 – 46 will hold (see EM Currency
Contagion, January 27, 2014). The peso indeed found support at 45.485 on Feb. 4, 2014, a week after we
published our call. Since then, the peso has strengthened by 2.3% against the US dollar to close at 44.50 last
Friday. </p>
        
        <img src="image/050514chart.jpg" height="400" width="800">

        <p align="center"><b>Monetary conditions to tighten</b></p>
        <p align="justify">Following the US Fed’s latest move to step up the gradual removal of its bond-buying program, the Bangko
Sentral ng Pilipinas is closely monitoring domestic credit/liquidity conditions. </p>
<p><i>“If liquidity growth continues to be strong even as the inflation outlook stays manageable, we will not hesitate to
again consider further pre-emptive macroprudential measures,”</i> according to BSP Governor Amando Tetangco.</p>

        <p align="center"><b>Not by raising rates but by other tools</b></p>
        <p align="justify">With inflation expectations broadly aligned with their target of 3-5% in 2014 and 2-4% in 2015, the BSP has
kept its overnight borrowing rate at 3.5 percent. It, however, raised bank reserve requirements to 19 percent
from 18 percent last month. While it avoided tweaking policy rates, the hike in reserve requirements was
intended to guard against potential price pressures that could arise from continued strong liquidity growth and
rapid credit expansion.</p>

        <p align="center"><b>Verbal intervention</b></p>
        <p align="justify">Another tool that central banks, like the BSP, have successfully used to influence the market is giving forward
guidance thru verbal intervention (see <i>Don’t Fight the Bangko Sentral</i>, Feb.17, 2014). This means telegraphing or hinting future actions by using words to show what it intends to do.</p>
    <p>The peso rose last week after market participants heeded BSP’s hints that further adjustments in monetary policy
tools are being considered. By giving forward guidance, the BSP intends to influence and put to maximum
effect its policy tool adjustments. </p>

        <p align="center"><b>BSP at the helm</b></p>
        <p align="justify">While the BSP remains market-oriented, it was successful in smoothing out excessive volatility when fears of
another emerging market currency crisis were high following the Argentine bolivar’s devaluation early this year
(see <i>EM Currency Contagion</i>, January 27, 2014). With all the tools available in its arsenal in adjusting to
challenging global conditions, the BSP remains clearly at the helm. </p>

        <p align="center"><b>EM currencies recover</b></p>
        <p align="justify">As the peso rallied against the US dollar the last three months, other emerging market currencies recovered as
well. In fact, the “Fragile Five” currencies (Brazilian real, South African rand, Turkish lira, Indonesian rupiah
and the Indian rupee), which were last year’s worst performers, led the rally this year.</p>
<p>As mentioned in a previous article, troubled emerging market economies which suffered last year when the US
Fed started tapering its bond buying program have started fixing their problems (see <i>Best House in an Improving
Neighborhood</i>, Feb. 24, 2014). India and Indonesia have reduced their current account deficits while South
Africa and Brazil have raised rates.</p>
        <img src="image/050514chart1.jpg" height="400" width="800"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p>The table above shows that Fragile Five currencies gained an average of 9.6 percent from their recent lows.
Meanwhile, the peso has rebounded 2.2 percent from its low which is at par with the region’s 2.3 percent move
against the greenback.</p>
    
        <p align="center"><b>Possible scenarios for the Peso</b></p>
        <p align="justify">Given the tightening monetary conditions and strengthening regional EM currencies, we believe that the peso
can further strengthen towards 44 or even lower. From a technical standpoint, we also believe that we have seen
the intermediate lows of the peso at 45.50 vs. the US dollar. </p>
    <p>We now see two likely scenarios unfolding for the peso.</p>

    <p><b>Scenario 1:</b>&nbsp;The first scenario calls for the peso going as high as 44 to the dollar. It would then trade in the 44 –
45.50 range.</p>
    
    <img src="image/050514chart2.jpg" height="400" width="800">

<p><b>Scenario 2:</b>&nbsp;: The second scenario calls for the peso breaking below 44 and heading towards 43. The rally of the
PSE index, EM stocks and EM currencies will be the catalysts for this move. In case the PSE index rallies to
new highs, we may even see a sharper appreciation towards 42. In this scenario, we may see the peso ranging
between 42 and 45.50 longer term.</p>

