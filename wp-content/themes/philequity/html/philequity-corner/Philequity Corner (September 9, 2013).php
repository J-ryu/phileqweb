        <p align="center"><b>Peso Bull Run Is Over</b></p>
        <p align="justify">We have been receiving a lot of questions about the peso lately. Why is it depreciating? What is the cause of the sudden and sharp decline? When will it stabilize?</p>

        <p>How swiftly things change. Early this year, many were concerned that the peso was appreciating too much. Major foreign investment banks were calling for the USD/PHP rate to break below 40 and appreciate towards the 37.50 to 38 range. This prompted us to write <i>Peso Win-win </i> (April 8, 2013) when we first noticed the US dollar displaying broad-based strength. We followed it up with <i> Peso Tops Out </i> (May 27, 2013) when it became clear that the peso’s long-term uptrend was over.</p>

        <p>But people have gotten so accustomed to a rising peso the past eight years that the thought of it reversing seems so farfetched. This is why many were surprised and continue to be puzzled by the sudden depreciation of the peso, falling 7.3 percent since the May 22 “Fed taper” speech by Bernanke.</p>

        <p align="center"><b>Changing old mindsets</b></p>
        <p align="justify">It is very difficult to change old mindsets. Just like in 2005, when the era of depreciating peso ended and the peso reversed, many were caught by surprise. People were so used to the peso depreciating against the greenback that a reversal was difficult to comprehend.</p>
        <p>As you can see from the historical chart above, the peso continuously lost its value from Php 2 per dollar when it was still pegged in 1961 to Php 56.50 per dollar by 2004.</p>

