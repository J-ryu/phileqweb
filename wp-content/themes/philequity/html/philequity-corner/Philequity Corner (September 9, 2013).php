            <p>Today, fundamentals are also aligning with technicals with regard to the peso. As you can see from the
chart above, it also took eight years before the long-term uptrend of the peso was broken. This was in
May when we called peso’s top (see <i> Peso Tops Out,</i> May 27, 2013).</p>

            <p>Coinciding with the technical break, the following fundamental factors argue for a weaker peso:</p>

            <blockquote>1) End of QE tapering<br>2) Rising interest rates<br>3) Fund deleveraging which trigger selling of emerging market assets<br>4) Broad based US dollar strength (vs. yen, commodity currencies and emerging market currencies)<br>5) Contagion from ASEAN/INR weakness (see <i>The Best House in a Bad Neighborhood,</i> Sept 1, 2013)</blockquote>

            <p>In fact, in a previous article we discussed in detail the reasons why the peso is already depreciating (see <i>
It’s the Strong Dollar, Stupid,</i> June 24, 2013).</p>

            <p align="center"><b>Peso will move in tandem with regional currencies</b></p>
        <p align="justify">To answer the questions at the start of the article:</p>

            <blockquote>1)<i> Why is the peso depreciating?</i>&nbsp;We believe that the long-term uptrend in the peso which lasted for
eight years has ended. Fundamental factors as mentioned in previous articles and technical
analysis all point towards the peso reversal. This means that it may be a long time before we see
the peso near 40 again.</blockquote>

            <blockquote>2)<i> What is the cause of the sudden and sharp decline?</i>&nbsp;The reasons for the sudden and sharp decline
are external in nature. While domestic fundamentals remain positive (strong GDP growth, low
inflation, high GIR, current account surplus, etc.), global and regional dynamics (rising interest
rates, broad-based US dollar strength, weakness of the ASEAN currencies & rupee, etc.) are
dictating the movement of the peso.</blockquote>

            <blockquote>3)<i> When will the peso stabilize?</i>&nbsp;There are initial indications that EM equities are turning. If the
strength in EM equities continues, we see the peso stabilizing and even improving towards the 43
level.</blockquote>

