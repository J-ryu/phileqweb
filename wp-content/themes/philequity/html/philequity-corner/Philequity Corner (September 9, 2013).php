        <p>Since time immemorial, Filipinos have long considered the US dollar as a safe haven. Throughout the
years, many would buy the greenback as part of their long-term savings.</p>

        <p>So when we wrote that timely report in 2005, calling for the peso’s bottom and urging investors to buy
peso and peso assets for the long-term (see <i> Peso, the Strongest Currency in Asia, </i> Oct. 27, 2005), many
were sceptical. In fact, some of our readers thought it was just a joke and were looking for the punch line.</p>

        <p>Similarly, today, an appreciating peso has been ingrained in most people’s mindset that the peso declining
seems difficult to understand.</p>

        <p align="center"><b>Fundamentals aligning with technicals</b></p>
        <p align="justify">In the foreign exchange market, trends have a tendency to last for several years. The previous chart
shows that it took eight years (from the 1997 Asian Financial Crisis to 2005) before the long-term
downtrend of the peso reversed. Note that the trend line break in 2005 coincided with major fiscal
reforms at that time, particularly the implementation of EVAT, in addition to other fundamental catalysts
(see <i> Peso Heading to 54, </i> Oct. 31, 2005).</p>


