        <p align="center"><b>New All-time High</b></p>
        <p>The market made a new all-time high last week after three months of consolidation. The Philippine Stock Exchange Index (PSEi) closed at a record high 5,443.74 last Thursday before pulling back 3.9 points to close at 5,439.84 on Friday. It broke above the previous high of 5,403.16 established last July 2012.</p>
        <p>Based on the most recent chart pattern, we are now looking at 5,700 as the next price target (see chart below). This should bring us closer to our upgraded forecast for the PSEi of 5,800 as discussed in a previous article (see <i>Italy defeats Germany</i>, July 2, 2012).</p>

        <img src="image/100812chart.jpg" height="400" width="800">

        <p align="center"><b>Other markets catching up is bullish for Philippine stocks</b></p>
        <p>In prior articles we pointed out that global equity markets have turned “risk on” after ECB’s Draghi pledged to do whatever it takes to save the euro (see <i>Whatever It Takes,</i> August 27, 2012) and other central banks like the FED, BoJ and BoC vowed more quantitative easing (see <i>Central Banks Winning,</i> Sept. 17,2012).</p>
        <p>Since Draghi’s “whatever it takes” speech last July 25, higher risk markets like Greece, Spain and Italy have rallied sharply, gaining 41.1 percent, 32.5 percent and 26.9 percent, respectively.</p>

         <img src="image/100812chart1.jpg" height="400" width="700"><br>
         <i>Source: Bloomberg, Philequity Research</i>

         <p>High-beta Asian markets like South Korea, India and Hong Kong have also gained a lot, increasing 12.8 percent, 12.4 percent and 11.3 percent, respectively.</p>

         <p>Other stock markets that lagged going up are actually bullish for the stock market. If these markets did not catch up, the Philippines will stick out like a sore thumb looking expensive.</p>
        
        <img src="image/100812chart2.jpg" height="400" width="700"><br>
         <i>Source: Bloomberg, Philequity Research</i>

        <p align="center"><b>From PIGS back to TIP</b></p>
        <p>After making huge gains from higher risk markets like the PIGS (Portugal, Italy, Greece and Spain), funds now appear to have flowed back to defensive and fundamentally sound markets like the TIP (Thailand, Indonesia and Philippines). In fact, Thailand and the Philippines have recorded new all-time highs last week, while Indonesia is poised to make a new high.</p>
        <p>ASEAN economies such as the Philippines, Thailand and Indonesia are considered “defensive” because they have been relatively resilient amidst declining global growth. Strong domestic demand and better fundamentals have shielded these emerging markets from the weaknesses in the more advanced economies of Europe and the US.</p>

        <p align="center"><b>Stay the course, Buy on dips</b></p>
        <p>Profit taking usually occurs after big run-ups like what we saw the past week. Corrections are actually healthy for the market. But as we have advocated in previous articles, investors should be buying on dips during these corrections (see <i>A Rollercoaster Ride,</i> October 17, 2011) and continue to favor fundamentally sound companies (see<i> 5,000! What Now?,</i> March 26, 2012). We have also urged investors to stay the course so they can reap the fruits of this bull market that started on March 6, 2009 (see <i>666 on 3-6-9,</i> April 13, 2009 and<i> Staying the Course,</i> February 6, 2012).</p>

        <p align="center"><b>Capital flows keeping peso strong</b></p>
        <p>A strong peso usually coincides with a strong stock market, and vice versa. So when the PSEi made a new high last week, the peso also closed strong at 41.46 vs. the US dollar. With all the capital inflows coming in, it is becoming very hard to keep the peso inside its sweet spot between 42 and 45 (see <i>Peso’s Sweet Spot,</i> January 30, 2012). Last July, the peso breached 42 for the first time in four years.</p>

        <p align="center"><b>BSP to lower interest rates?</b></p>
        <p>The BSP has so far been partially successful in tempering the peso’s strength. But it might be wise for the BSP to consider lowering interest rates soon like what the Reserve Bank of Australia did last week. If not, speculative funds or so-called “hot money” will just come in.</p>
        <p>The smart money, mutual fund managers and hedge funds always are on the lookout for countries with high yield and balanced growth. The Philippines will be a lucrative place to park this hot money if the interest rate differential is too high.</p>
        <p>Too strong a peso is counter-productive since it will greatly affect our BPO industry, the OFWs, and the country’s the export and tourism sectors (see <i>Too much of a good thing,</i> July 9, 2012).</p>

