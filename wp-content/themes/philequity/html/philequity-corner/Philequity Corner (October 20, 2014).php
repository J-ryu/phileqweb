        <p align="center"><b>Epidemic of Fear</b></p>
        <p align="justify">The last three weeks have proven to be one of the most volatile and painful times for stock investors. European
indices from Germany to Italy were gripped with fear, losing more than 10 percent over this time period. By the
2nd week of October, the Dow Jones had wiped out its entire return for 2014. Even the Philippines was not
spared, with the index falling as much as 3.8 percent for the week and breaking the crucial 7,000 level before
recovering.</p>

        <p align="center"><b>Global slowdown</b></p>
        <p align="justify">The proximate cause behind the weakness in equities is the slowdown in global growth. In our previous
presentations, we highlighted the ISIS conflict and the Russia-Ukraine crisis as key geopolitical risks that may
affect global growth if left unabated. We said then that if sanctions against and from Russia escalate, then it is
not only Europe’s growth that will be affected, but the rest of the world as well. Unfortunately, these fears did
come to pass. ISIS continues to wreak havoc with thousands of people murdered, while Russia, Western
Europe and the US remain in a cold war.</p>

        <p align="center"><b>Too strong a dollar not welcome</b></p>
        <p align="justify">Two weeks ago, we wrote about the strong dollar and its impact on different currencies (see It’s the strong
dollar, stupid! Part II, Oct. 20, 2014). Commodity-exporting countries continue to suffer as their exports drop in
value. Many other countries have voiced their apprehension over the sharp strengthening of the world’s reserve
currency. Even the US has aired its concern over the strong dollar. In the Fed’s September minutes, Federal
Reserve chairman Janet Yellen was quoted as saying that “further appreciation of the dollar could have
adverse effects on growth.”</p>

        <p align="center"><b>Epidemic of ebola</b></p>
        <p align="justify">As these concerns over global growth grabbed headlines, another unforeseen threat reared its ugly head –
ebola. First reported in 1976, this year’s ebola outbreak has been the deadliest yet, infecting more than 8,000
people and killing more than half of them. The epicenter of the current epidemic is in West Africa, with Liberia,
Sierra Leone and Guinea bearing the brunt. Although one needs direct exposure to bodily fluids to get infected
by this disease, research shows that the Ebola virus’ historical mortality rate is 70 percent.</p>

        <p align="center"><b>Ebola exacerbates global slowdown</b></p>
        <p align="justify">Then, two weeks ago, ebola hit US shores. After first going to a hospital in Texas due to fever, Thomas Duncan
was reported to have been diagnosed with ebola after travelling from Liberia. Airports then began screening
every passenger who came from Africa. People travelled less and screening procedures both in hospitals and
airports became more stringent. Every time news on ebola would come out, short term stock traders would sell
down. Thus, with news of ebola flooding the airwaves, equities in the US kept getting hammered repeatedly as
traders feared that the worst is yet to come. Now faced with fear of the unknown, people ran for the exits,
sending the Dow Jones down nearly 300 points on numerous occasions. This led to sharp corrections in equity
indices around the world as ebola exacerbated fears of a global slowdown.</p>
    
        <p align="center"><b>State of paranoia</b></p>
        <p align="justify">At this point, the public was already in a state of paranoia. When a passenger on a plane bound for LA reported
vomiting, the aircraft was put on lockdown. When a hospital in Boston admitted a patient with headaches and
muscle aches, it was evacuated. Although both cases turned out to be negative for ebola, a different disease
had already spread across the US – an epidemic of fear.</p>

        <p align="center"><b>Outbreak of worry</b></p>
        <p align="justify">Eventually, Duncan succumbed to the illness, but not before spreading the infection to the very health workers
who were treating him. So when it was reported that one of his nurses was infected, jittery investors started to
panic. The day after it was reported that a 2nd nurse was infected, markets sold off sharply. Equity indices
around the world fell by nearly three percent on panic selling. With more questions than answers, an outbreak
of worry ensued with many traders cashing out as they deemed that a global slowdown exacerbated by ebola
was too much for them. The epidemic of ebola has now transformed into an epidemic of fear in capital markets.</p>

        <p align="center"><b>The fear index</b></p>
        <p align="justify">This outbreak of worry caused a breakout in the so-called “fear index.” The Chicago Board Options Exchange
Volatility Index is a measure of the market’s expectation of stock market volatility. Also called by its ticker
symbol VIX, it tends to spike during times of uncertainty. As fear over Ebola mounted, the 15 to 20 percent
moves in the VIX became commonplace. Last week, the VIX spiked by as much as 30 percent within the day.
On that same day, Oct. 15, the Dow Jones fell by 460 points before recovering much of its losses. See below a
chart of the VIX. Note how it doubled, going from 15 to 31, in just one week.</p>

