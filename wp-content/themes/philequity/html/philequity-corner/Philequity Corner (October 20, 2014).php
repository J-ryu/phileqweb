        <p align="center"><b>Liquidity evaporates</b></p>
        <p align="justify">During this three-week carnage, many equity indices fell by double-digit percentages. With fear engulfing the
markets, buyers vanished and liquidity dried up. At one point, dark pools refused to accept orders. Many
market pundits blamed the lack of liquidity on some trading days as the reason behind the stock market’s sharp
drop. With barely any bids, panicky sellers would have no choice but to drive down the price if they want to exit
their position.</p>

        <p align="center"><b>Leveraged hedge funds forced to unwind</b></p>
        <p align="justify">While some investors decided to sit tight and ride the volatility out, there were those who had no choice but to
sell. If one was leveraged, the sharp drops in the past weeks are likely to have triggered margin calls. With
leveraged hedge funds forced to sell their holdings immediately, prices fell precipitously, leading to even more
margin calls. This vicious cycle continued for days as many traders just decided to capitulate as leverage
began working against them, amplifying their losses. This reminded everyone of an oft-repeated saying in the
stock market – “The market can stay irrational longer than you can stay liquid.” The Philippine market was no
exception. Last week, we saw hedge funds forced to close out their position, giving orders to sell at market.
This was more evident last Friday when they were giving orders to sell at any price at the close.</p>

        <p align="center"><b>Buy when others are fearful</b></p>
        <p align="justify">With all stocks, good and bad, sold down indiscriminately, many companies became too cheap to ignore.
Those with cash and courage took advantage of the epidemic of fear and used it as an opportunity to buy
stocks at bargain prices. Although one can never catch the bottom, it pays to buy when selling is borne out of
panic and fear, not fundamentals. As legendary stock market investor Warren Buffett said, “Sell when others
are greedy and buy when others are fearful.”</p>

        <p align="center"><b>A rational and calculated approach</b></p>
        <p align="justify">While fears of a global slowdown and ebola pandemic are not to be underestimated, we believe that the effects
of these are temporary. In time, the market will get back to its senses and realize that their fears have been
exaggerated. However, the market may continue to remain volatile, shaking the confidence of even the most
stalwart investor. Thus, one has to pick stocks with good fundamentals and a solid growth trajectory that is
insulated not only from ebola but the global slowdown as well. If one holds the right stocks, history shows that
one will be able to withstand this temporary volatility. When the market is dropping because of an epidemic of
fear, a rational and calculated approach to investing is warranted.</p>


