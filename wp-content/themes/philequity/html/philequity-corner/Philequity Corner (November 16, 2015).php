        <p align="center"><b>December liftoff imminent</b></p>
        <p align="justify">In last week’s article (see <i>Every Breath You Take,</i> November. 8, 2015), we mentioned that monetary policy divergence continue to be the dominant factor in the currency market, especially after the strong US jobs report number increased the likelihood of a rate hike in December. A recent survey by the Wall Street Journal shows that 92 percent of business and academic economists now expect the Fed to raise rates during their Dec. 15 -16 policy meeting.</p>

        <p align="center"><b>Dollar tips Fed’s hand</b></p>
        <p align="justify">The dollar may have tipped the Fed’s hand when the US dollar index resumed its upward trend this month after breaking out of an 8-month consolidation from March to October. It now appears that the correction from the March high was just a consolidation in preparation for the next advance.</p>

        <img src="image/111615chart.jpg" height="400" width="800">

        <p align="center"><b>ECB hints of further accommodation</b></p>
        <p align="justify">Driving this month’s US dollar rally is euro weakness, driven by expectations of further monetary expansions by the ECB when they meet in December.</p>
        <p>President Mario Draghi reiterated ECB’s stance last week saying that the ECB remains flexible with its monetary expansion beyond September 2016 if inflation targets are not met. In addition, Draghi said that the ECB is open to the use of “other instruments”, i.e. more asset purchases and even negative interest rates, to strengthen the impact of their measures.</p>

        <p align="center"><b>Migrant crisis and security threats exacerbate Euro weakness</b></p>
        <p align="justify">The European migrant crisis and increasing regional security threats which was exposed by the Nov. 13 Paris massacre would further exacerbate euro weakness. It now appears that the euro’s rally from the March lows was just a bear market correction. Looking at the chart below, the sharp decline below the rising channel points to further euro weakness going forward.</p>

        <img src="image/111615chart1.jpg" height="400" width="800">

        <p align="center"><b>Peso weakness is unwarranted</b></p>
        <p>The peso closed at 47.10 last week, its lowest weekly closing since 2009. The peso’s weakness is unwarranted according to the Bangko Sentral ng Pilipinas. Nevertheless, the BSP is also aware that sentiment and even technical charts sometime come into play.</p>
        <p><i>“How can one have a continuously depreciating currency when your balance of payments is in surplus, when your current account is in surplus? Negative sentiment brought about by offshore factors is more dominant than actual market fundamentals that should really support a stable currency,”</i> according to BSP Deputy Governor Diwa Gunigundo.</p>

        <p><i>“There may be short-term volatility in the peso before the Fed moves but surpluses in the current account and balance of payments should support the Philippine currency,”</i> BSP Governor Amando Tetangco said.</p>

        <p align="center"><b>Our timely call</b></p>
        <p>We, at Philequity, have been extremely constructive on the US dollar for more than two years, and in fact, we pointed this out very early. In Chapter 7, Peso Tops Out, of our book<b><i> “Opportunity of a Lifetime”</i></b>, we cited this emerging monetary policy divergence that is impacting the currency markets:</p>
        <p><i>“The US is ahead of the curve in terms of QE… ECB cuts interest rates for the first time in 10 months … Japan is embarking on its own QE program to end deflation and stimulate its economy … Other central banks have also started cutting rates… While US growth is still low by historical standards, it is growing faster than other countries</i> (see <i>The Peso Tops Out,</i> May 27, 2013).</p>

        <p align="center"><b>Peso remains relatively stable amidst dollar strength</b></p>
        <p align="justify">Philequity’s bullish call on the dollar caught the bulk of the greenback’s move the past two and a half years. Since bottoming-out, the US dollar index has rallied 36.2 percent. This year alone, the US dollar index gained 9.3 percent. Among major currencies, the commodity-dependent Canadian dollar and the Aussie dollar are the worst performers this year, depreciating 14.6 percent and 12.8 percent, respectively. Meanwhile, the euro weakened 11 percent over the same period.

        <img src="image/111615chart2.jpg" height="400" width="800">

        <p>In Asia, the Malaysian ringgit and the Indonesian rupiah weakened the most this year, down by 25.2b percent and 10.5 percent, respectively. The Philippine peso, meanwhile, is relatively more stable, declining by only 5.3 percent year-to-date.</p>

       <img src="image/111615chart3.jpg" height="400" width="800">

        <p>Short-term, given the increasing expectations of a Fed rate liftoff this December, we expect the peso to move in unison with regional currencies as the dollar resumes its uptrend and global currency volatility expands. Longer-term, however, we agree with the BSP that the positive fundamentals will kick back in and provide support & relative stability to the peso.</p>

