        <p align="center"><b>Factors weighing on the euro</b></p>
        <p align="justify">A number of factors continue to weigh on the euro. Among them is the continued monetary easing as Europe tries to stave off a deflationary crisis. The inflation rate in the Euro Area was recorded at -0.30 percent in February 2015, following -0.60 percent in January 2015 and -0.2 percent in December 2014.</p>
        <p>Adding to euro’s weakness is the ECB’s QE bond buying program. It officially started last week when the ECB bought 10 billion euro worth of bonds. The ECB has said it would buy a total of 60 billion euros worth of Eurozone government bonds a month up until September 2016.</p>
        <p>In addition, fears of a possible Greek exit of the euro and the conflict in Ukraine are increasing uncertainty, further pressuring the euro.</p>

        <p align="center"><b>Divergent central bank policies</b></p>
        <p align="justify">On the flipside, the US dollar has surged not only against the euro but also against most developed-market currencies as investors anticipate the Fed raising interest rates for the first time since 2006 (see chart below).</p>
        <p>Note that since the 2nd half of 2014 the pace of US dollar appreciation has quickened as central bank policies started to diverge with the Fed leaning towards tightening while other central banks continued with their easing frenzy. Since the start of the year alone, 24 central banks have eased policy rates in one form or another.</p>

