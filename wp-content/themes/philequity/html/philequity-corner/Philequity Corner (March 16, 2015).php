        <p align="center"><b>Surging dollar, slumping euro</b></p>
        <p align="justify">The euro slipped to a new 12-year low against the US dollar last week as investors bet on the normalization in US monetary policy ahead of the Fed’s policy meeting this week.</p>
        <p>The euro slipped to a new 12-year low against the US dollar last week as investors bet on the normalization in US monetary policy ahead of the Fed’s policy meeting this week.</p>

        <p align="center"><b>Longest & sharpest decline in euro history</b></p>
        <p align="justify">The recent slump in the euro is the longest and sharpest in its entire history (since its birth in 1999). The beleaguered currency has lost around 25 percent of its value against the US dollar since July 2014. It is also on its way to recording its 9th consecutive monthly loss against the greenback.</p>
