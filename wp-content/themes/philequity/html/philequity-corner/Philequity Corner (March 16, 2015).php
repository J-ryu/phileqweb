        <p align="center"><b>Peso: not too hot, not too cold, but just right</b></p>
        <p align="justify">Given the dollar strength and the recent bout of currency volatility among major currencies, we are closely monitoring the potential impact on the peso. But so far, so good. Our stable peso, as well as our strong stock market (which reached new all-time highs last week), is a manifestation of the strength of the Philippine economy.</p>
        <p> Thanks to the BSP who has successfully smoothed out excessive volatility these past years, the Philippine peso is right at its “sweet spot,” just as we have been saying these past years (see <i>Peso’s Sweet Spot,</i> Jan. 30, 2012, <i>Peso Back to the Sweet Spot,</i> June 3, 2013, <i>The Goldilocks Peso,</i> Nov. 4, 2013).</p>

        <p>The peso is “not too hot, not too cold, but just right” as Goldilocks, the BSP, Philequity, investors and the Philippine economy in general would have preferred it to be.</p>
     
