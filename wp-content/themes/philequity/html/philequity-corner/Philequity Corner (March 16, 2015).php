        <p align="center"><b>Fed raising rates soon?</b></p>
        <p align="justify">The recent strong US jobs data from February is widely expected to persuade the Fed to hint that a rate increase will come as early as June. Investors are wagering the removal of the word “patient” at next week’s FOMC meeting. This would then be seen as another step in the process of policy normalization and a powerful catalyst for further US dollar strength.</p>

        <p align="center"><b>Peso: stable against the dollar and strong against the euro</b></p>
        <p align="justify">As discussed in our article a month ago (see Philippine Peso: A Haven of Stability, Feb. 2, 2015) and again shown in our Investor Briefing last Saturday, the peso has been a haven of stability in the midst of wild swings in the currency market. The peso has been stable against the dollar while actually strengthening against the euro and most major currencies.</p>
        <p>Last week, the peso rallied to 46.90 against the euro, its strongest level in 12 years. Below, the long-term EURPHP chart points to further peso strength against the euro going forward.</p>

