        <p align="center"><b>A pause that refreshes and strengthens</b></p>
        <p align="justify">Corrections and consolidations are normal in a healthy bull market. Just like in a marathon where runners need a second wind, a bull market also needs restive and healthy pauses to regain strength, especially after exhaustive and extended runs.</p>
        <p>After a blistering run which drove the PSE index (PSEi) up by 18.2 percent the past four months, the market took a much needed breather last week.</p>
        <p>The PSE index recorded an all-time intraday high of 8,134.38 before pulling back as much as 307 points or 3.8 percent. It found support at 7,826.80 last Thursday and bounced back to close at 7,946.89 on Friday as the market enters a corrective phase.</p>

        <p align="center"><b>Interim targets reached</b></p>
        <p align="justify">In our article last February, we mentioned that Philequity had interim PSEi targets that ranged from 7,900 to 8,100 based on technical analysis (see <i>Philippine Stock Market: The Outperformer</i>, Feb. 9, 2015). Those targets were met sooner than most had anticipated as the market experienced a bull-run as shown below.</p>
        <img src="image/042015chart.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com</i>

        <p>The first leg of the rally which started mid-December 2014 sent the index surging 1,000 points in just 40 trading days. It fell just a tad short of 7,900 which is the low-end of our interim target (see <i>1,000 Points in 40 Days,</i> March 2, 2015).</p>

        <p>After a brief sideways consolidation which found support at 7,671.63, the market climbed another leg up, ascending 463 points to reach a new record high of 8,134. It briefly breached the high-end of our interim target at 8,100 before correcting last week.</p>

        <p align="center"><b>Support lies at 7,800, followed by the 7,400 level</b></p>
        <p align="justify">Classical technical analysis teaches us that prior resistance levels, once broken, become areas of support. So referring to the chart above, immediate support lies at 7,800. If this fails to hold, a much stronger support level is seen at the 7,400 level which marked the previous all-time record high that stood from May 2013 to January 2015.</p>

        <p align="center"><b>Focus on the big picture</b></p>
        <p align="justify">Whether the market reaches the 7,400 level or not depends on many short term factors that are impossible to anticipate. Corrections are extremely difficult to predict and it is often futile to try to do so. Thus, it important not to lose focus on the big picture. Refer to last weekâ€™s article where we enumerated the fundamental reasons why the secular bull market in Philippine stocks continues to remain strong (see <i>8000</i>, April 13, 2015).</p>
        <p>While the next technical move will depend on how the correction/consolidation unfolds, it is really the fundamentals that will drive the market to the next level.</p>

        <p align="center"><b>Technical measured move is 9,300</b></p>
        <p align="justify">Looking at the weekly chart below, the long-term bullish trend remains intact and strong. So while the market may be consolidating in the near-term, the long-term trajectory is clearly higher. The confirmed breakout of the previous all-time high of 7,400 points to a measured move targeting 9,300 for the medium-term.</p>

        <img src="image/042015chart1.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com</i>

        <p align="center"><b>Global bull-run continues</b></p>
        <p align="justify">The Philippine stock market is also being aided by the global bull market in stocks. The US stock market indices, while appearing to undergo a correction near term, are trading just below all-time highs. And so do the German DAX and the Eurostoxx index. Meanwhile, Japan and China are at new multi-year highs. Emerging markets which generally have lagged in this bull-run have now recovered and hit a new seven-month high.</p>
        <p>The number of global equity indices that have powerfully broken out of patterns or are poised to complete large congestion zones continue to increase. In fact, the MSCI All-Country World Index which is represented by ACWI ETF below show a completed reverse head and shoulders continuation pattern, targeting a 10-percent upward move.</p>
        <img src="image/042015chart2.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com</i>

        <p>The speed of the move in Philippine stocks warrants a healthy correction and a much needed consolidation. However, the bullish trend in global stocks will counterbalance any short-term weakness. The strength of the global stock market will be supportive of our own stock market.</p>

