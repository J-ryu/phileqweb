        <p align="center"><b>Yolanda and the Peso</b></p>
        <p align="justify">Since we wrote the articles on Yolanda and its effect on the stock market (see<i> Eye of the Storm, </i>Nov. 18, 2013; <i>Yolanda – A Black Swan,</i> Nov. 25, 2013;<i> Rebuilding Process for Country and Stock Market,</i> Dec. 2, 2013), we have received a lot of questions from our readers with regard to our outlook on the peso, especially after Yolanda.</p>

        <p>So we decided to write a two-part article. We will answer most of our readers’ questions this week. And next week, we will present the charts and our forecasts.</p>

        <p>1) How did Typhoon Yolanda affect the peso?</p>
        <p>The Philippine peso depreciated after Yolanda, declining 1.07 percent after one week and 1.52 percent after two weeks. But on closer look, it was not only the peso that depreciated, other currencies dropped as well.</p>

        <img src="image/120913chart.jpg" height="400" width="600"><br>
        <i>Source: Bloomberg, Wealth Securities Reseach</i>

        <p>2) Is it just a regional currency weakness we are seeing against the US dollar or something more broad-based?</p>

        <p>It is actually a broad-based strengthening of the US dollar against most other currencies that we have been seeing since the start of the year, especially more after May 22 when news of Fed tapering first went out. Emerging market- and commodity-currencies such as the South African rand, the Brazilian real, the Australian dollar, the Norwegian krone and the Canadian dollar have declined sharply this year (see table below).</p>
        
        <img src="image/120913chart1.jpg" height="300" width="600"><br>
        <i>Source: Bloomberg, Wealth Securities Research (prices as of 12/5)</i>

        <p>In fact, we have noted this as early as April when most foreign banks and major houses were forecasting that the peso will hit 37 to 38 by end-2013. We at Philequity disagreed and said that the peso is likely to hold above 40 due to the inherent strength of the US dollar (see<i> Peso Win-win,</i> April 8, 2013).</p>

        <p>In May, we concluded that the peso has topped out against the US dollar (see Peso Tops Out, May 27, 2013). And in a follow-up article we said the peso is likely to depreciate back towards the 42 to 44 range (see <i>Back to the Sweet Spot,</i> June 3, 2013).</p>

        <p>3) So why is the US dollar getting stronger when their economy still remains fragile?</p>

        <p>In May, we already enumerated the fundamental reasons why the US dollar has been strengthening since the start of the year (see <i>Peso Tops Out,</i> May 27, 2013).</p>

        <p>The major reasons worth noting are:</p>

        <blockquote>&bull;&nbsp;the US being ahead of the curve in terms of QE while Japan and Europe is just in the initial stages<br>&bull;&nbsp;the sharp decline of the Japanese yen due to Abenomics or Japan’s own version of QE whose goal is to end deflation and stimulate the Japanese economy<br>&bull;&nbsp;the US economy (while still weak) is strong enough that the Fed is already discussing tapering while Europe, for example, is so weak that the possibility of negative rates is now on the table<br>&bull;&nbsp;the US is showing signs of accelerating economic growth compared to other developed countries which are slowing or stagnant<br>&bull;&nbsp;the ISM manufacturing index at 2 ½ year highs<br>&bull;&nbsp;3Q13 GDP growth of 3.6 percent, beating expectations<br>&bull;&nbsp;increasing non-farm payrolls and unemployment rate of 7%<br>&bull;&nbsp;the US trade deficit has been shrinking which is lifting overall exports to all-time highs<br>&bull;&nbsp;the low inflation and low commodity prices is benefitting the dollar as commodity currencies like the South African rand, the Brazilian real and the Australian dollar have weakened<br>&bull;&nbsp;US stocks are among the best performing stocks in the world which is attracting portfolio inflows<br>&bull;&nbsp;lastly, with global stocks (US, Europe & Japan) in a bull market and with inflation low, gold has lost its lustre as safe-haven which is benefitting the US dollar</blockquote>

        <p>4) Why did Asian currencies, including the peso, experience extreme volatility last August? Will it happen again?</p>

        <p>The threat that the Fed will taper anytime soon (previously thought to be as early as September) exposed the vulnerability of Asian economies with huge current account deficits and high inflation such as India and Indonesia (see <i>The Best House in a Bad Neighborhood,</i> August 31, 2013).</p>

        <p>The threat that the Fed will taper anytime soon (previously thought to be as early as September) exposed the vulnerability of Asian economies with huge current account deficits and high inflation such as India and Indonesia (see The Best House in a Bad Neighborhood, August 31, 2013).</p>
        <p>Since then, the Indonesian rupiah continued to weaken while the Indian rupee has stabilized. The peso which reached a low of 44.82 to a dollar last August 28 has stayed in our sweet spot and closed at 43.948 last Friday.</p>
        <p>Year-to-date, this is how Asian currencies have performed against the US dollar. The peso remains in the middle of the pack, together with the Malaysian ringgit and the Thai Baht.</p>

