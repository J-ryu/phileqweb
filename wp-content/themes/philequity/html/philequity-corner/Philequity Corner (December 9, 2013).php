        <p>5) Should the peso appreciate now because of remittances coming from relatives of Yolanda-victims, foreign aid and the Christmas season?</p>

        <p>Many expect the peso to appreciate because remittances will surge after Yolanda from OFWs helping their families rebuild after the disaster, plus the foreign aid that poured in to help the victims. In addition, many are looking at the seasonal pattern of higher remittances during Christmas.</p>

        <p>While higher than normal remittances will definitely have an impact of the peso, we at Philequity believe that global foreign exchange trends will have a bigger influence on the peso’s direction.</p>

        <p>Note that most currencies have formed short-term tops against the US dollar last October. Moreover, in the past month, the Japanese yen and the Australian dollar have started to resume their weakness against the greenback.</p>

        <p>Even the peso, which settled in the 43 to 43.50 range in September to October, is now trying to break above 44. A break above 44 may see the peso retesting the previous year-low of 44.82, and possibly 45. Next week, we will look at the charts in detail when we point out potential target levels for the peso and other currencies for the coming year 2014.</p>


