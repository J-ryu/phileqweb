        <p align="center"><b>EM Currency Contagion</b></p>
        <p align="justify">Last week, emerging market currencies suffered another vicious beating after the Banco Central de la Republica Argentina (BCRA) pulled out its support on the peso, triggering its single biggest day drop against the dollar since the country defaulted on its debt in 2002. The Argentine Peso devalued by 13.2 percent on Thursday spreading turmoil in neighbouring Brazil, Latin America and across the emerging world. By Friday, the Argentine peso, Turkish lira, Russian ruble and the South African rand were among the worst performers reaching multi-year lows.</p>

        <p align="center"><b>Global risk sell-off spreads to other asset classes</b></p>
        <p align="justify">Continued volatility and fears of contagion in emerging markets prompted investors to sell other risk assets in a broad sell-off last Friday which spooked even the developed world. The Dow Jones Industrial Average fell 318 points or nearly 2 percent, recording its worst week since May 2012. In Europe, the Stoxx 50 index lost 88 points or 2.85 percent, while Japan’s Nikkei declined 304 points or nearly 2 percent last Friday alone.</p>

        <p align="center"><b>Stampede to safety</b></p>
        <p align="justify">While the jury is still out whether last week’s sell-off was caused by the weak Chinese Purchasing Managers index (PMI) numbers or the devaluation of the Argentine peso, what is clear is that investors have stampeded to safety prior to the scheduled US Federal Open Market Committee (FOMC) meeting on January 28-29, 2014 when incoming Fed chairwoman Janet Yellen takes over.</p>

        <p>US Treasuries headed for its biggest monthly advance since May 2012 as the benchmark 10-year yield fell to 2.72 percent. Meanwhile, the Japanese yen and the Swiss franc rallied 1.9 percent and 1.7 percent, respectively week-on-week, as the sell-off in riskier assets stoked demand for haven assets.</p>

        <p>The demand for protection against a US equity sell-off soared as the CBOE VIX Index or the so-called “fear index” jumped 30 percent on Friday and nearly 44 percent for the week.</p>

        <p align="center"><b>Incipient signs</b></p>
        <p align="justify">Interestingly, the peso showed incipient signs of recovery last week when it fell by a just a muted 0.7 percent when other emerging market currencies where falling sharply (see table below). What is more surprising is that ASEAN currencies declined the least during last week’s turmoil. In fact, even the political crisis and escalating social unrest in Thailand failed to bruise the baht which stayed nearly flat against the US dollar last week.</p>

        <p>In addition, what we saw last Friday is that our stock market is also resilient. We were expecting it to drop sharply following the Argentine peso devaluation and the global stock market sell-off the prior day. But after falling 74 points or 1.2 percent in early trade, the Philippine market finished strongly and was even up 21 points or 0.3 percent by the close. Meanwhile, Thailand’s stock market also finished the day 0.5 percent higher.</p>

