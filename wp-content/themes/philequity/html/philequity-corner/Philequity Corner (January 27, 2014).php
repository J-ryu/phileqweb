        <p align="center"><b>Stronger support at 46 seen</b></p>
        <p align="justify">All along, we in Philequity, were saying that the peso topped out at 40.55 when major foreign investment banks were forecasting the peso to reach 37 to 38 last year (see<i>Win-win for the Peso,</i> April 8, 2013 and<i> Peso Tops Out,</i> May 27, 2013).</p>
        <p>Today, foreign banks are now saying that the peso will reach 48. A look at the longer-term chart, however, shows that the peso is nearing strong support at the 45.50 to 46 level. If ASEAN markets continue to show resiliency and if China improves, then we may see a rebound in the peso.</p>
        <p>At this point, however, we continue to believe that the country in general is better off with a slightly weaker peso than an overly strong peso. For us in Philequity, the peso at current levels is better than the peso back at 40.</p>
        <p>We believe that the peso is now at a new trading range between 43 and 46 where we expect it to consolidate for some time. We also believe that the peso is now at the upper zone of new trading range.</p>

