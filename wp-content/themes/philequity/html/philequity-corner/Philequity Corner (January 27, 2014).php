        <p align="center"><b>China stock market also higher</b></p>
        <p align="justify">Interestingly, China which was the cause of the drop in global stock markets after its disappointing PMI
results also ended Friday with positive gains in the stock market. The Shanghai Shenzen CSI 300 index
gained 0.6 percent the day after the manufacturing numbers were announced. The index ended higher by
3.1 percent for the week.</p>
        <p>The resilient performance of China and Philippine stocks may indicate the start of a recovery and it is an
encouraging signal. These are signs that we are already watching which may mean that China and the
rest of Asia, including the Philippines, are being differentiated from other emerging countries, especially
those in Latin America.</p>

        <p align="center"><b>Watching the turn</b></p>
        <p align="justify">In addition to the peso’s muted drop last week, note that the slope of the peso’s decline is becoming less
steep since the May 2013 taper speech of Bernanke. Whether the peso and other ASEAN currencies’
resilience will be fleeting or not remains to be seen.</p>
<p>In the case of the Philippines, whose fundamentals we believe are firmly intact, we think it is just the
unwinding of overweight positions rather than the market placing a negative bet.</p>
<p>We will be closely watching for these subtle changes in character, that the Philippine peso will slowly be
differentiated from the genuinely vulnerable currencies like the Argentine peso, Turkish Lira, South
African rand and the Brazilian real.</p>

