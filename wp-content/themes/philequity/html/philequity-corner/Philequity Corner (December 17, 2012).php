        <p align="center"><b>PNoy: Bold and Resolute</b></p>
        <p>We would like to commend President Aquino, his economic team and our enlightened Congress for
being resolute to pass the Sin Tax Bill and RH Bill despite pressures from lobbyists and various groups.
This goes to show that our President is not scared to do what is right for our economy. His recent action
last Friday, to certify the RH Bill as urgent, further demonstrated his resolve to take action on what is
correct even at the risk of disappointing some sectors of society. It has been evident that both foreign
and local investors view his moves as positive for the market. This confidence in PNoy has contributed
immensely to the bullish outlook for the Philippines.</p>

        <p align="center"><b>Five eight, Slow your gait</b></p>
        <p>The index will most likely take a breather after bounding from new all-time high to new all-time high the
past weeks. Realizing gains is all part of the journey in investments. Still, we believe that our country
continues to poise itself for growth. Infrastructure spending next year will invite foreign investments and
will create employment for locals. However, successfully investing in the stock market will require
indentifying those sectors that will benefit the most in this emerging economy. Specific stock picking will
play a more important role in the next phase of this bull market. At this juncture of our market where
the index has reached 5,800, we would advise investors to be more discerning in their stock choices.</p>
    

