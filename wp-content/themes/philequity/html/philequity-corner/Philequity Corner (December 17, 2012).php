        <p align="center"><b>Target Reached!</b></p>
        <p>The PSE Index reached our year-end target of 5,800 last week. In fact, it closed at an all-time high of 5,831.50 on December 11. Now that our target has been reached, we have been receiving many questions from our clients: Is it time to rejoice? Should we take profits? Did Santa come early this year? In this article, we would like to share these questions with our readers.</p>

        <p align="justify"><b>Why did the index go up so much, so quickly?</b></p>

        <p>The most recent catalyst for our market to sky rocket was the surprising GDP growth of 7.1% in the third quarter. GDP growth is used to gauge an economy’s health. The third quarter result, which came out last Nov 28, not only exceeded economists’ expectations (5.4%) but also exceeded the government’s target (6.0%) for the year. The breakdown shows a balanced and robust growth in the economy: government spending (+12%), consumer spending (+6.2%), net exports (+6.9%) and private investments (+4.3%)</p>

        <p align="justify"><b>What do we do now that our year-end target of 5,800 has been reached?</b></p>
        <p>Each investor’s action will depend on his asset allocation:</p>
        <blockquote>a) For investors who are overexposed in equities or who are on margin, now would be an opportune time to top slice their exposure in the local market.</blockquote>
        <blockquote>b) Professional traders who are quick and nimble enough and who watch the market closely can take some profits and buy back later.</blockquote>
        <blockquote>c) For long-term investors, we advise to hold on to positions as the long-term uptrend is intact and our country’s fundamental story remains sound. These people who are not in the market full-time will find it difficult to time the market. That is, liquidating positions runs the risk of not being able to buy back at better prices later on.</blockquote>
        <blockquote>d) For those who have been absent from the bull party, look to buy on dips those stocks with clear growth prospects and good business models. These people can also opt for mutual funds and let professional fund managers invest their money.</blockquote>

        <p align="justify"><b>Why is our market correcting?</b></p>
        <p>In under a month’s time, our index dashed from 5,500 to 5,800. Because the index has moved up so much, so fast, a correction is in fact warranted. Based on technical analysis, the market has also reached overbought levels. Consequently, profit-taking is tempting at these levels. Fund managers may also be rotating to lagging markets such as China’s. Compared to other markets, the Philippine stock market has gone up much higher. Our index has given us a YTD return of 30.5%, second best in the world after Thailand’s 32.5%.</p>

         <p align="justify"><b>What is a corrective phase?</b></p>
        <p>A correction is a decline of a stock price or stock index by five to ten percent in a short amount of time
(approximately one to two weeks). This phase may seem nerve-wracking to neophyte investors.
However, it should be noted that these corrections are normal occurrences even in a bull market such as
ours. In fact, our index has seen two corrective phases this year – one in May and one in July – but has
maintained the long-term uptrend.</p>

        <p align="justify"><b>How deep will this correction be?</b></p>
        <p>Corrections are part of short cycles that occur within major trends. The short-term nature of corrections
makes it difficult to predict how deep they will be. This is similar to how it is practically impossible to
predict the day-to-day movement of the market. We find it easier to forecast the direction of a stock
price over a longer time frame as we tend to look at the growth trajectories of companies that we have
picked.</p>

        <p align="justify"><b>What is a buy-on-dips strategy?</b></p>
        <p>Since the start of the bull market in March 2009 and all throughout 2012, we have advised buying on
dips during market corrections and consolidations. This strategy is employed when the decline in the
stock price is viewed as temporary. That is, the stock is seen to eventually rise. However, not all stocks
that fall should be bought. A refinement of the buy-on-dips strategy would be to buy only those
declining stocks that will have strong earnings growth in the future. When the market is correcting,
equity analysts will have the advantage of conviction in picking up the right stocks.</p>

        <p align="center"><b>Sin Tax and RH bill positive for capital markets</b></p>
        <p>The Sin Tax and Reproductive Health bills are viewed positively by both local and foreign fund managers.
The passage of sin taxes would be an indication that the Philippine government is fiscally responsible.</p> 
        <p>The RH bill, on the other hand, would be a step toward addressing chronic poverty in our country.
Overpopulation in itself is not the root of poverty, but it does exacerbate the already existing poverty in
our nation. The bill has received widespread support from the financial community, numerous groups
and international organizations like the United Nations, World Health Organization and the International
Monetary Fund.</p>
