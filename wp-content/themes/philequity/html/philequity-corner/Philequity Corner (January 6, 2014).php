        <p align="center"><b>Shift from EM to DM</b></p>
        <p align="justify">A major development that happened in 2013 is the shift of capital flows from emerging markets (EM) to developed markets (DM) after Bernanke’s “tapering” speech on May 22, 2013. This triggered large-scale unwinding, deleveraging and repatriation of funds. Hence, erstwhile emerging market leaders such as Turkey, Mexico and the ASEAN-3 (Indonesia, Philippines, Thailand) which led from 2012 up to the 2nd quarter of 2013, ended the year flat or slightly down.</p>

        <p>At the same time, developed markets such as the US, Japan and even Europe started to show signs of economic improvement which translated to huge price gains in their stock markets last year.</p>

        <p align="center"><b>US dollar strength & Yen weakness</b></p>
        <p align="justify">Another key theme that influenced the markets last year was the US dollar strength and yen weakness. In fact, we wrote several articles about this which was highlighted by our call of a peso top (see <i>Peso Tops Out,</i> May 27, 2013), eight years after we called the bottom of the peso (see Peso, <i>The Strongest Currency in Asia</i>, October 24, 2005).</p>

        <p>Commodity currencies such as the Indonesian rupiah, South African rand, Australian dollar, Brazilian real and the Canadian dollar depreciated sharply as commodity prices softened in 2013.</p>

        <img src="image/010614chart1.jpg" height="300" width="600"><br>
        <i>Source: Wealth Securities Inc.</i>

        <p align="center"><b>Commodities tumbled</b></p>
        <p align="justify">Precious metals and other commodities tumbled in 2013. In fact, 20 out of 24 commodities tracked by the S&P GSCI index declined last year. Silver plunged 35.9 percent and gold plummeted nearly 30 percent to mark their biggest annual declines since 1981.</p>

        <p align="center"><b>EM weaknesses highlighted</b></p>
        <p align="justify">The exodus of funds that was triggered by the talks of QE tapering highlighted the weaknesses of economies with huge current account deficits and high inflation, such as India and Indonesia. This exacerbated the declines in their respective currencies which inevitably affected regional currencies such as the peso.</p>

        <p align="center"><b>PSEi: Post-taper talk & Post-Yolanda</b></p>
        <p align="justify">Last week, we discussed the reasons why the PSEi and the peso swung from being the best to worst in a span of one year (see <i>From Best to Worst</i>, December 30, 2013). But aside from the Fed’s tapering of QE which affected everyone, the Philippines was affected by a barrage of problems towards the latter part of the year (torrent of share issuances, pork barrel scam, Zamboanga siege, earthquake, and the straw that broke the camel’s back, Yolanda).</p>

        <img src="image/010614chart2.jpg" height="300" width="700"><br>
        <i>Source: Wealth Securities Research</i>

        <p>Like we said in our article last week, market sentiment can change easily. Last year, we saw how
sentiment can turn against us in such a short period of time. Similarly, sentiment changed in Indonesia
when its huge current account deficit was exposed and in Thailand because of political turmoil and
prospect of a coup d’etat. Equity markets from these ASEAN countries which run up “too fast too soon”
in prior years, pulled back last year with double digit losses.</p>

        <p>But from the bear market bottom of 2009, Indonesia, Thailand and the Philippines still performed very
well, up 232%, 211% and 206%, respectively. Meanwhile, the S&P 500 index is up 170 percent from
the March 6, 2009 low. Hence, from a longer term perspective, the US and other equity markets are just
catching up with us.</p>

<p align="center"><b>Peso: Post-taper talk & Post-Yolanda</b></p>
        <p align="justify">While the PSEi dropped sharply post-taper talk and post-Yolanda, the peso was more stable relative to the
region. Going forward, the peso remains vulnerable to further weakness given the continued strength of
the US dollar, and the weakness of the yen and other ASEAN currencies.</p>

        <img src="image/010614chart3.jpg" height="300" width="700"><br>
