        <p align="center"><b>Big Picture 2014</b></p>
        <p align="justify">2013 was a classic breakout year for the US and other developed markets such as Japan and Germany.
With the exception of the Nasdaq, the major US indices have all broken to new all time highs and many
by over double digits. The Nikkei made the biggest yearly gain in four decades. Meanwhile, stock
markets in troubled EU countries like Spain and Italy showed much improvement.</p>

        <p>Our view is that if the breakout holds for these three major markets (US, Japan and Europe) and the US continues its strong economic recovery this year then we will see a rebound in emerging markets in 2014. In addition, China should show signs of resolving its structural problems (e.g. liquidity squeezes, ballooning local government debts, etc.). If these happen, expect the peso and Philippine stocks to recover together with other emerging market currencies and equities.</p>
            
