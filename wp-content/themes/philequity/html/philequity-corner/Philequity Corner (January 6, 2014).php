        <p align="center"><b>2013 Scorecard</b></p>
        <p align="justify">In an article we wrote exactly one year ago (see Global Bull Market, January 7, 2013), we said that:</p>
        <p><i>“In a period of slow global economic growth and looming deflation brought about by the European debt
crisis and the US subprime crisis, we now consider quantitative easing (QE) and low interest rates as
the greatest gift to us by Bernanke and other central bankers...</i></p>
        <p><i>Thanks to the central banks, we are now witnessing a global bull market in stocks.”</i></p>

        <p align="center"><b>The year of the bull</b></p>
        <p align="justify">Looking back, global equities undeniably went on a bull rampage in 2013. Japan’s Nikkei led the way with a 56.7 percent return, its biggest gain in 41 years. The US S&P 500 Index and the Dow Jones Industrial Average had their biggest yearly gains since 1996, up 29.6% and 26.5%, respectively.</p>

        <p>The S&P 500 Index and the DJIA also registered new record highs. In Europe, the German DAX index surged 25.5 percent en route to a new record high.</p>

        <p>A number of emerging markets including the Philippines, Indonesia, Malaysia, Thailand, Mexico, Turkey and South Africa also recorded new all-time equity highs in 2013. Most have corrected since May 2013 after Bernanke first hinted “QE tapering” (with the exception of Malaysia and South Africa).</p>

        <p>Overall, more than 80 percent of indices that are listed in Bloomberg’s World Equity Indices (WEI) list showed positive returns for 2013.</p>

