        <p align="center"><b>The 2nd Upgrade</b></p>
        <p>Last Thursday, May 2, 2013, our country received its 2nd investment grade (IG) rating from Standard &
Poor’s (S&P). Like Fitch Ratings, S&P upgraded our credit rating to BBB- from BB+. Though S&P’s
upgrade was expected to come within 2-3 months, investors welcomed the 2nd upgrade even as it came
earlier than expected. S&P’s upgrade caused our PSE Index to end the week on a strong note and close
at a new all-time high of 7,215.</p>

        <p align="center"><b>Fully Upgraded</b></p>
        <p>Before, we stressed the importance of getting our country’s 1st IG rating. Being upgraded into IG status
is an important milestone that will make people aware of the progress that the country is experiencing.
However, in terms of investment flows, we believe that the 2nd upgrade is at least equally as important
as the 1st upgrade. After a country is given its 1st IG rating, most foreign funds would usually wait for the
2nd upgrade before they take action. In other words, being fully upgraded to IG status means that the
country is IG-rated by at least 2 of the 3 major credit rating agencies.</p>

        <p align="center"><b>Mainstream Investment</b></p>
        <p>Since our country has been given the IG status by S&P and Fitch, we will start being considered as a
mainstream investment alternative. We will become visible to global investors who have not even heard
of the Philippines. Moreover, funds that are restricted to investing in IG countries can now start putting
investments in the country. Lastly, funds that are already invested in the country can now allocate a
bigger portion of their assets to their Philippine investments.</p>

        <p align="center"><b>Rising Star</b></p>
        <p>S&P’s recent upgrade leaves Moody’s Investors Service as the only major credit rating agency which has
not given the IG status to the country. Recently, however, Moody’s Analytics, a sister company of
Moody’s Investors Service, had some good things to say about the Philippines. In one of its reports,
Moody’s Analytics described the Philippines as one of the few bright spots of a generally sluggish global
economy. The think tank said that it expects the country to continue growing between 6.5 to 7.0% over
the next 2 years, outperforming most developed and even fast-growing emerging markets. Considering
these, we think that an IG upgrade from Moody’s might be forthcoming.</p>

        <p align="center"><b>A Virtuous Cycle</b></p>
        <p>In a past article, we talked about the tangible benefits of getting the IG status (<i>Investment Grade,</i> April
1, 2013). We summarize below some of the things that we mentioned in that article.</p>
    <p><b>1. Lower borrowing costs.</b>&nbsp;The IG status is a widely-recognized badge of approval, affirming that the
country is a safe and viable investment alternative. This will result to lower borrowing costs for the government. Since borrowing rates are pegged against the government’s borrowing rates, we expect borrowing costs of local corporates, small businesses and consumers to also go down.</p>
    <p><b>2. More investments.</b>&nbsp;A combination of lower borrowing costs and increased visibility will encourage
investors to increase their investments in the country. If everyone does his part, we expect the
country to benefit from an investment-driven growth over the next few years.</p>

    <p><b>3. Higher income and more spending.</b>&nbsp;A surge in investments will likely create more jobs. This, along
with lower borrowing costs, will result to higher earning power and higher disposable income. Given
these, we think that the spending power of Filipinos will only increase moving forward. This will
boost the robust domestic consumption that has been the foundation of our strong economy.</p>

        <p align="center"><b>Ahead of the Pack</b></p>
        <p>The country’s unique structural growth story has reaped praise and recognition from the international
community. This is because of the magnificent long-term structural transformation that our country has
gone through in the past 2 decades (<i>Secular Bull Market</i>, January 28, 2013). Although there is still much
to be done, past governments and our current administration have implemented meaningful structural
reforms that have significantly strengthened our country’s growth profile.</p>
        <p>As such, we believe that we are ahead in terms of structural reforms compared to most countries.
Countries in Europe and countries such as the US and Japan are doing a lot in attempting to revive their
economies while contending with high government debt. Even emerging countries such as China, Korea
and Brazil have shown vulnerability because of their heavy dependence on exports and on the state of
the global economy. Considering that we are in a relatively good position, we believe that the
government should take the opportunity to further strengthen and diversify our economy.</p>

