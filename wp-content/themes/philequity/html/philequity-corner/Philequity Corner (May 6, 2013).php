        <p align="center"><b>Valuing Growth</b></p>
        <p>There is a persistent concern among investors and major investment houses that Philippine stocks are
already expensive. While it is true that our stock market is not cheap based on Price-to-Earnings (PE),
this has been the case for many years already. The fact that the PSE Index has gone up a lot last year and
continues to move higher this year shows that our stock market’s high PE multiple has not deterred
those who would like to invest in our country. Many investors and market players are puzzled by this.</p>

<p>In our view, the PE multiple that is given to a country or a company is based on 2 things: growth and risk.
There are stock markets with low PE multiples such as those of China and Korea. These are countries
whose economies are heavily dependent on exports. Since global economic growth has been particularly
sluggish, the economic prospects of China and Korea are considered relatively risky. Hence, they are
cheap for a reason. In contrast, the Philippine economy has been extraordinarily resilient amidst a global
economic slowdown. Our strong domestic consumption, which has been the key underpinning of our
economy, has kept our economic growth insulated from external weaknesses. Because of this, our
growth profile is considered stronger and less risky compared to export-oriented countries such as China
and Korea. This is why our stock market is given a higher PE multiple.</p>

        <p align="center"><b>A Powerful Bull Market</b></p>
        <p>Although valuation will likely remain a concern for some investors, we think that the trajectory for
Philippine stocks is very clear. As we have always said, we believe that we are in a bull market that will
last for many years to come. The recent upgrade of S&P, which gave our country its fully upgraded IG
status, is an affirmation of our thesis. Considering this, we encourage investors to use corrections as
chances to increase their positions or enter the stock market. We firmly believe that this powerful bull
market will continue for as long as our country’s fundamentals remain intact.</p>
    
