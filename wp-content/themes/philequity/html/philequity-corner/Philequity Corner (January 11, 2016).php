        <p align="center"><b>China pummels global markets</b></p>
        <p align="justify">A number of factors knocked global financial markets to its worst start in history. These were the turmoil in the Middle East between Saudi Arabia and Iran, the detonation of a hydrogen bomb by North Korea, but the most far-reaching is the sharp depreciation of the yuan and the collapse in Chinese stocks.</p>
        <p>Every global equity index were beaten down while a multitude of currencies hit new all-time lows against the US dollar. Oil and commodity prices registered multi-decade lows.</p>

        <p align="center"><b>Yuan’s abrupt and sharp decline</b></p>
        <p align="justify">While we were having a holiday, China let the yuan depreciate quickly and sharply which caught everyone off-guard. The PBOC has set the yuan lower against the dollar for 9 straight days since December 25.</p>
        <p>Since China devalued the yuan last August, their currency has already lost 6.4 percent against the US dollar. The yuan has been under pressure to depreciate as part of China’s currency liberalization process and their bid to establish the yuan as a global currency reserve.</p>

        <p align="justify"><b>USDCNY Rate (Jan. 2015 – Jan. 2016)</b></p>
        <img src="image/011116chart.jpg" height="400" width="800">

        <p align="center"><b>Circuit breaker short-circuits</b></p>
        <p align="justify">China’s stock market triggered the circuit breaker and was halted twice this week. The Shanghai stock market stopped trading altogether last Monday and Thursday when the Shanghai Stock Exchange Composite Index declined 7 percent on those days.</p>
        <p>But in an ironic twist, it was the circuit breaker mechanism that was halted last Friday. The mechanism itself caused a world-wide panic in global markets.</p>
        <p>The Shanghai index ended the week down by 10 percent. <b>This is the biggest first week loss ever for a major stock market index in history.</b></p>
        <img src="image/011116chart1.jpg" height="400" width="800">

        <p align="center"><b>Worst first week in stocks</b></p>
        <p align="justify">Global equities sold off sharply in unison last week. All markets were in the red during the first week of trading in 2016. Moreover, many markets (such as the US, Germany, China) suffered their worst start in history.</p>
        <p>In the US, S&P 500 Index was down 6.0 percent, the Dow Jones Industrial Average lost 6.2 percent while the Nasdaq Composite index declined 7.3 percent.</p>
        <img src="image/011116chart2.jpg" height="400" width="800">

        <p align="center"><b>Historic 1st week drops for developed markets</b></p>
        <p align="justify">The German DAX led the declines in developed markets with an 8.3 percent loss in the first week of 2016. Italy’s MIB index and Japan’s Nikkei 225 were down 7.2 percent and 7 percent, respectively.</p>
        <img src="image/011116chart3.jpg" height="300" width="600">

        <p align="center">Historic 1st week drops for Asian indices</p>
        <p align="justify">China’s SSEC was the worst performer not only in Asia, but also globally, with a 10 percent decline. Other Asian indices such as the Hang Seng and the PSEi were down 6.7 percent and 5.4 percent, respectively.</p>
        <img src="image/011116chart4.jpg" height="300" width="600">

        <p align="center"><b>Currencies and commodities collapse to historic lows</b></p>
        <p align="justify">Currencies such as the Russian ruble, the South African rand and the Mexican peso have hit all-time lows
        against the US dollar. A multitude of other currencies are either near record-lows or multi-year lows. Both
        Brent and WTI crude prices are below their 2008 lows. Copper broke below $2 a pound for the first time
        since 2009.</p>

        <p align="center"><b>The final trigger to a perfect storm</b></p>
        <p align="justify">With the dollar becoming strong because of the Fed hike, divergent policies of central banks and global
        economies still weak, China’s apparent decision to depreciate the yuan appears to be the final trigger to this
        perfect storm.</p>
        <p>At the same time, this also proves that China’s influence to the world economy is far more extensive than
        most investors previously imagined. Given the size of China’s economy and its importance to global trade,
        the spillover to economies and financial markets has more impact than Greece, Portugal, Spain, Italy or
        even Europe.</p>

        <p align="center"><b>Navigating thru turbulent waters</b></p>
