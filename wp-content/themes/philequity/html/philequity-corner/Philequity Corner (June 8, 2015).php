        <p align="center"><b>Abnormally low levels</b></p>
        <p align="justify">As seen in the table above, the recent move has been exacerbated by the fact that yields are bouncing off extremely low or even negative levels. What is interesting to note is that although Philippine bond yields also moved higher in recent weeks, the move has not been as drastic. This is because our bond yields are trading at more normal levels compared to our developed market (DM) counterparts.</p>

        <p align="center"><b>ECB to stay the course</b></p>
        <p align="justify">According to European Central Bank (ECB) President Mario Draghi, the recent move of bond yields reinforces the importance of ECB’s quantitative easing (QE) program. He conceded that high volatility is something that investors would have to contend with especially in a period of ultra-low interest rates. Nonetheless, Draghi said that the ECB will continue with its bond-buying program until its growth and inflation targets are achieved.</p>

        <p align="center"><b>Initial stage of rate normalization</b></p>
        <p align="justify">The recent move in DM bonds appears disproportionately big precisely because bond yields are bouncing from extremely low levels. What we are now witnessing is the initial stage of interest rate normalization. This may result in volatility across markets over the short-term, as global markets and various economies adjust to more normal interest rate levels. It could be said that this is the start of the weaning process from zero or negative interest rates caused by unprecedented monetary policy easing around the globe.</p>

        <p align="justify"><b>Chart 3 – S&P 500 and Fed funds rate</b></p>
            <img src="image/060815chart3.jpg" height="300" width="800"><br>
            <i>Source: Bloomberg, Wealth Securities Research</i>

        <p>As we show in the table above, interest rate normalization is a long process that may take years to complete. And as we have seen in the past, the stock market tends to be volatile during the initial phases of rate normalization. However, after the initial volatility, the stock market continues to move higher as economic growth solidifies with a stronger and clearer trajectory.</p>

        <p align="center"><b>Equities are best-positioned asset class over the long term</b></p>
        <p align="justify">We agree with Draghi’s warning that investors should be prepared for volatility especially as interest rates normalize from ultra-low levels. For as long as the global recovery continues, interest rates will inevitably rise and eventually track long-term growth and inflation expectations. Consequently, bonds would become less attractive, as the normalization of bond yields would result in lower bond prices. And while equities may not be spared from the volatility that accompanies the initial stages of rate normalization, we expect the bull market in stocks to continue if global growth remains in a clear and solid trajectory. Hence, we believe that over the long-term, equities are still the best-positioned asset class to invest in.</p>



