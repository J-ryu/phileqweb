        <p align="center"><b>Global move in bond yields</b></p>
        <p align="justify">Although the bond rout seems most pronounced in German bunds, other countries such as Italy, France and Japan also experienced sharply rising yields. In the table below, we show how the yields of various sovereign bonds moved over the past weeks.</p>

        <p align="justify"><b>Table 1 – Yields of various sovereign bonds across tenors</b></p>
