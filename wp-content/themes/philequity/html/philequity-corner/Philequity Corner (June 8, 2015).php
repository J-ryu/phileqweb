        <p align="center"><b>Bond yields rising</b></p>
        <p align="justify">Last week, bond yields across the globe moved sharply higher. This selloff in bonds has resulted in losses for many fixed income investors. This has also caused heightened volatility in global markets, including stocks. Many investors are now pondering if the global economy is ready for higher interest rates.</p>

        <p align="center"><b>Rout in German bunds</b></p>
        <p align="justify">The bond selloff started with a sharp move in German bund yields. This is a result of extremely and abnormally low interest rates in Europe, especially Germany. The gradual pickup of growth and inflation expectations in Europe may also have stoked the uptick in bund yields. Below, we show the 6-month chart of the 10-year German bund yield.</p>

