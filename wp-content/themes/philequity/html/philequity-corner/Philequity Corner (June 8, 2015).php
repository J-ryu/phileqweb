        <p align="center"><b>Spillover to US Treasuries</b></p>
        <p align="justify">Though US Treasury yields are more attractive than German bund yields, US Treasuries also experienced a selloff due to the rout in German bunds. This has also resulted in higher yields for US Treasuries. Shown below is the 6-month graph of the 10-year US Treasury yield.</p>

        <p align="justify"><b>Chart 2 – 10-year US Treasury yield – 6-month chart</b></p>
