        <p align="center"><b>Global Markets in Tables and Graphs</b></p>
        <p align="justify">Last year, we showed you how the Philippine financial markets performed since President Aquino took office (see 
        <i>SONA in Tables and Graphs,</i> July 30, 2012). In a similar manner, we’d like to show you in tables and graphs how global markets have performed the past 12 months.</p>

        <p align="center"><b>US (S&P 500 Index)</b></p>
        <img src="image/072913chart.jpg" height="400" width="800">

        <p>The US market is ahead of the curve because the Fed was the first to implement QE which was a game changer for the markets. And despite recent talks of tapering which caused the sharp pullback last May, the market has surged to new highs. The S&P 500 is up 24.2 percent since end-June 2012.</p>

        <p align="center"><b>Europe (STOXX 50 Index)</b></p>
        <img src="image/072913chart1.jpg" height="400" width="800">

        <p>One year ago, ECB President Mario Draghi uttered two short sentences that stabilized and turned European markets around. <i>“Within our mandate, the ECB is ready to do WHATEVER IT TAKES to preserve the euro. And believe me, it will be enough,”</i> said Draghi on July 26, 2012 in a speech to bankers in London. Today, Spanish and Italian bond yields have declined considerably. Meanwhile, the Euro Stoxx 50 Index has rallied 13.1 percent the past 12 months.</p>

        <p align="center"><b>Japan (Nikkei Index)</b></p>
        <img src="image/072913chart2.jpg" height="400" width="800">

        <p>In Japan, Abenomics and its 3 arrows (massive fiscal stimulus, more aggressive QE, and structural reforms to improve competitiveness) was introduced by Prime Minister Shinzo Abe to lift Japan out of the deflationary sand trap. The market responded overwhelmingly with the Nikkei rocketing 56.9 percent since end-June 2012, making it the best performing market in the world the past 12 months.</p>

        <p align="center"><b>China (Shanghai Stock Exchange Index)</b></p>
        <img src="image/072913chart3.jpg" height="400" width="800">

        <p>Among the global growth engines, China poses the most risk due to its deteriorating economic numbers. Adding to the volatility is the recent liquidity crunch which sent interbank rates surging. As a result, the Shanghai Composite index is down 9.6 percent since end-June 2012. China is now in the process of restructuring, shifting away from an export-led growth to more of a domestic-led economy. And despite its slowest economic advance since 2009, the market is pinning its hopes on a successful soft-landing.</p>

        <p align="center"><b>Philippines (PSE Index)</b></p>
        <img src="image/072913chart4.jpg" height="400" width="800">

        <p>In a previous article, we said that the PSE index has reached an intermediate low of 5,678 last June 25 after the sharp pullback in May (see 5-6-7-8, July 1, 2013). The chart above confirms our call. Technically, the next resistance is at 6,800 to 6,900 and the market may consolidate and pullback from here. Longer term, however, the market is poised to retest its all-time high of 7,403.</p>

        <p align="center"><b>Global Market Performance</b></p>
        <img src="image/072913chart5.jpg" height="400" width="800">

        <p>Despite the recent pullback, most global market indices still show double-digit percentage advances the past 12 months. In fact, only China was the outlier showing a negative return. If China recovers and all the major engines of global growth (US, Europe, Japan, China) are up and running, emerging markets including the Philippines should likewise follow.</p>

