        <p align="center"><b>US recovery drives the dollar higher</b></p>
        <p align="justify">Speculations that the Fed may strike a more hawkish tone when it meets this week while other major central banks are still aggressively in easing mode drove the US dollar higher for the ninth straight week. This marks the longest US dollar rally in 17 years. Investors are speculating the Fed might shift its tone and language about the timing of interest rate increases ahead of this week’s policy meeting.</p>
        <p>The US Dollar Index, which measures the dollar’s strength against six major currencies, has climbed 5 percent the past nine weeks.</p>

        <img src="image/091514chart.jpg" height="400" width="800">

        <p align="center"><b>Fed may raise rates sooner than expected</b></p>
        <p align="justify">Improving US economic data has raised the expectations that the Fed may raise rates sooner than expected. Compared to the rest of the developed world, the US economy is well ahead in terms of recovery after registering a 4.2 percent GDP growth in the 2nd quarter of 2014. US retail sales data last week showed that spending rose broadly as consumer sentiment hit a 14-month high.</p>

        <p align="center"><b>ECB’s negative interest rates</b></p>
        <p align="justify">Current dollar strength reflects the weakness in Europe where the euro is in a clear downtrend. Sanctions between EU and Russia have taken its toll on economic growth as Eurozone’s 2nd quarter 2014 GDP growth stalls at 0 percent. Moreover, ECB’s decision to cut its interest rates further into negative territory last September 4 led to a sharp one-day decline of 1.64 percent. The euro is down 5.2 percent since July 1.</p>

        <p align="center"><b>Yen at its lowest level in 6 years</b></p>
        <p align="justify">The BOJ took the cue from ECB’s negative interest rates by doing the same. Last week, the BOJ drove short-term rates to below zero for the first time. Although it pioneered “quantitative easing” in the early 2000s, negative yields represent a new territory for the BOJ. As a result, the yen fell 2.16 percent to 107.36 last week, its lowest level against the US dollar in six years.</p>

         <img src="image/091514chart1.jpg" height="400" width="800"><br>
         <i>Source: Bloomberg, Wealth Securities Research</i>   

         <p align="center"><b>Asian currencies vs. the US dollar</b></p>
        <p align="justify">Asian currencies have been relatively more stable. We have not seen sell-offs that resemble last year’s
taper tantrum as Asian central banks have made policy adjustments to address inflation and reduce current
account deficits to more sustainable levels.</p>

        <p>More importantly, it appears that central banks are now coordinating closely with each other to monitor
risks that may cause disruptions of macroeconomic and financial system stability in the region.</p>
<p>The table below shows that on the average Asian currencies have declined by only 0.3 percent against the
US dollar compared to the 5.2 percent decline among major currencies.</p>
<p>In fact, the Indian rupee and the Indonesian rupiah which dropped sharply last year are showing slightly
positive gains against the US dollar since July 1. The Philippine peso is likewise stable at 43.95 vs. the
dollar.</p>

        <img src="image/091514chart2.jpg" height="400" width="800"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i> 

        <p align="center"><b>BSP making all the right moves</b></p>
        <p align="justify">Thanks to the BSP, the peso has been relatively stable. Since early this year, the BSP has taken a hawkish
stance to manage inflation and address the policy developments abroad. Aside from addressing inflation,
BSP’s calibrated moves show that it would not allow real interest rates to turn negative as this would
result to severe and unwanted capital outflows.</p>

        <img src="image/091514chart3.jpg" height="200" width="600"><br>
        <i>Source: BSP</i>

        <p align="center"><b>Another preemptive strike by the BSP</b></p>
        <p align="justify">Seeing early signs of inflation, the BSP took action promptly. Last Thursday, the BSP raised both the overnight borrowing and special deposit account (SDA) rates by 25 basis points to 4 percent and 2.5 percent, respectively.</p>
        <P>This move has also the effect of stabilizing the peso at a time when the dollar’s rally is once again stoking volatility in the forex markets – especially against emerging market currencies most sensitive to US rates.</P>
        <p>Time and again, the BSP has shown that it has a knack for knowing and doing what the economy and the market needs.</p>

        <p align="center"><b>Drop in oil prices - a welcome respite</b></p>
        <p align="justify">A consequence of dollar strength is a drop in oil prices which is also good for our economy. Oil prices fell last week to its lowest level in two years. Coupled with BSP’s tightening moves, the decline in oil prices would lower inflation expectations going forward and further help in stabilizing the peso.</p>

        <p align="center"><b>Raising rates is good for the stock market</b></p>
        <p align="justify">In our presentations to investors and shareholders, we showed that BSP’s pre-emptive moves against inflation thru gradual and calibrated transition towards monetary tightening has actually been welcomed by investors. Similar to India’s experience, hikes in domestic interest rates elicited positive response from the equities market.</p>
        <p>BSP’s moves this year has shown that it is clearly at the helm - capable of reigning in inflation and stabilizing the peso to ensure that economic growth is unimpeded and remains sustainable.</p>


        <p align="center"><b>Major currencies vs. the US dollar</b></p>
        <p align="justify">Other major currencies equaled the drop in the euro and the yen as investors continued to buy dollars to
position for what will be the first rate increase in the US since 2006. On the average, major currencies
have declined by 5.2 percent against the greenback since July 1.
