        <p align="center"><b>Bad Neighborhood Improves</b></p>
        <p align="justify">Two weeks ago, we said that it’s not the fiscal, monetary and economic performance of the Philippines but its geographical location that made it “the best house in a bad neighbourhood” (see <i>The Best House in a Bad Neighborhood,</i> September 2, 2013). Thus, the Philippines was not spared from across-the-board sell-off of risk assets in the region despite its strong macroeconomic fundamentals.</p>

        <p>Last week, however, we noted that emerging market (EM) equities are showing early signs of a bottoming out and that further strength in the equities market would point to some improvement in Asian currencies (see <i>Peso Bull Run is Over,</i> September 9, 2013). What happened next was the strongest rally in EM equities since 2007. As Asian equities rebounded from their August 28 lows, Asian currencies (including the peso) stabilized and improved.</p>

        <p align="center"><b>Positive China data sparks strongest EM rally since 2007</b></p>
        <p align="justify">Muted inflation plus better-than-expected industrial output and export numbers coming from China, triggered a rebound in global equities last week. The iShares MSCI Emerging Markets ETF (symbol: EEM) rose for 10 straight days before pulling back on Thursday. EEM broke above the resistance level at 40.20 last week on high volume which indicates a healthy breakout.</p>

        <img src="image/091613chart.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com, Wealth Securities Research</i>

        <p align="center"><b>BRICS leading the way</b></p>
        <p align="justify">There are signs that the Chinese stock market is coming back to life after three years of consolidation. In fact, the iShares FTSE China 25 Index Fund (symbol: FXI) is up 25% from its June low, showing that it may already be in the early stages of a bull market.</p>

        <img src="image/091613chart1.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com, Wealth Securities Research</i>

        <p>The battered PowerShares India Portfolio ETF (symbol: PIN) which was down as much as 26 percent year-to-date at one point, broke above a major resistance level at 15.50 six trading days ago. PIN’s movement may indicate that the Indian market is also at the initial stages of a new bull phase, since PIN has rallied 21 percent from its August low of 13.50.</p>

        <img src="image/091613chart2.jpg" height="400" width="800"><br>
        <i>Source: Stockcharts.com, Wealth Securities Research</i>
 
        <p>The three other members of the so-called BRICS, Russia, Brazil and South Africa (South Africa joined the former grouping “BRIC” only in 2010) have also rallied strongly this quarter.</p>

        <p>South Africa iShares ETF (symbol: EZA), Market Vectors Russia ETF Trust (symbol: RSX), and Brazil iShares ETF (symbol: EWZ) are up 21 percent, 18 percent and 14 percent, respectively, from their recent lows.</p>

        <p>The fact that the five major emerging markets are leading this rally further adds credibility to our observation that EM equities are indeed bottoming out.</p>

        <p align="center"><b>Asian equities and currencies improve</b></p>
        <p align="justify">Following the BRICS’ lead, Asian equities rebounded strongly led by India and Thailand which gained
16 percent and 8.6 percent, respectively, from their August 28 lows. Meanwhile, the Philippines stock
market increased 6.9 percent over the same period.</p>

        <img src="image/091613chart3.jpg" height="300" width="600"><br>

        <p>Following the equities rally, Asian currencies stabilized and improved. The Indian rupee gained the most
with an 8 percent rebound against the US dollar from its August 28 low, followed by the Korean won
which went up 2.6 percent. Meanwhile, the Philippine peso improved 2 percent against the greenback
over the same period.</p>
        
        <img src="image/091613chart4.jpg" height="300" width="600"><br>

        <p align="center"><b>Recovery in US and Europe is positive for emerging markets</b></p>
        <p align="justify">The stock market is a leading economic indicator. Given that the US and major European equities
markets are at all-time highs, this mean that the recovery in their economies are well under way and
sustainable. In fact, manufacturing output (as measured by the PMI) both in the US and in the Eurozone
is expanding at its fastest rate in more than two years.</p>

        <p>Given the improvement in US and in Europe, we expect the growth in emerging economies (especially
exporting countries such as China and Brazil) to resume. This also bodes well for Asian economies
which are largely dependent on exports.</p>

        <p align="center"><b>Bad neighbours putting their houses in order</b></p>
        <p align="justify">Asian markets have been very volatile lately as vulnerabilities in some countries with huge current
account deficits were exposed. The good news is that they are now trying to put their houses in order.</p>
        <p>Indonesia has decisively raised its benchmark lending rate for the second time in two weeks to 7.25 percent, the highest in more than four years. Last June, it cut its huge fuel subsidies for the first time since 2008. This is similar to what the Philippines more than a decade ago.</p>
        <p>Meanwhile, Mexico is planning to privatize its oil and electricity industries, a major structural reform that the Philippines accomplished back in 1990s.</p>

        <p align="center"><b>Philippine stock market on its path towards recovery</b></p>
        <p align="justify">It has been a tough year for Asian markets especially after the Fed “taper” talk last May 22 erased most, if not all, of the year’s gains. But with major global economies showing signs of sustainable growth, we remain positive that Asian markets will soon recover.</p>

        <p>Given that the BRICS are showing early signs of entering a new bull phase, we expect Asian markets to also bottom out. Once the markets recover from this current consolidation, we believe that the Philippines, being the “best house in an improving neighbourhood,” will resume its path towards new highs.</p>
        
