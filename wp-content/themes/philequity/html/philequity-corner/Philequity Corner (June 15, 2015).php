        <p align="center"><b>Verbal intervention from Kuroda</b></p>
        <p align="justify">In past articles, we argued that the Fed and other central banks may start to intervene if the strong dollar becomes counter-productive to the stability of the global economy (see <i>It’s the Strong Dollar, Stupid! – Part 2,</i> October 6, 2014 and <i>Philippine Peso: A Haven of Stability,</i> Feb. 2, 2015).</p>
        <p>Last March, Fed Chair Janet Yellen implicitly hinted that too much US dollar strength may not be good for US corporates causing the dollar to pull back.</p>
        <p>Recently, BOJ Governor Haruhiko Kuroda said that Japan’s real effective exchange rate was “very weak” and unlikely to fall further, prompting the yen to strengthen to 123.42 from a low of 125.86.</p>
        <p>Yellen and Kuroda’s “verbal interventions” has slowed the rate of US dollar appreciation against major currencies. It can also possibly arrest further spikes in EM currency volatility.</p>

