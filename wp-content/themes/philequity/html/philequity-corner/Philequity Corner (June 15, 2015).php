        <p align="center"><b>Renewed EM currency volatility</b></p>
        <p align="justify">Three weeks ago we wrote that the US dollar appears to be consolidating after staging one its longest dollar bull-runs since the 1990s (see <i>Is the US dollar bull run over?,</i> May 25. 2015). But while the US dollar index steadied as the Euro rallied the past three weeks, EM currencies continued their decline against the greenback.</p>
        <p>The strong US jobs data in May and the increased likelihood of higher US interest rates later this year have rekindled concerns over EM countries with high fiscal account and current-account deficits.</p>

        <p align="center"><b>US monetary policy normalization</b></p>
        <p align="justify">In last week’s article (see <i>Bond yields rising,</i> June 8, 2015), we mentioned that we are now seeing the initial stages of interest rate normalization. This may result in increasing volatility across markets over the short-term. As bond yields rise due to the prospect of the Fed rate hike later this year, global investors have slowly been deleveraging and moving money out of riskier EM assets this year.</p>

        <p align="center"><b>EM equity markets suffers worst outflows</b></p>
        <p align="justify">According to funds tracker EPFR Global, emerging market stock funds registered a total of $9.3 billion outflow last week, the most since 2008. Investors have pulled out $26 billion from emerging market stock funds this year, already surpassing the $24 billion total for 2014. This record outflow in emerging market stock funds coupled with the sell-off in global bonds the past few weeks further exacerbates the volatility in EM currencies.</p>  

