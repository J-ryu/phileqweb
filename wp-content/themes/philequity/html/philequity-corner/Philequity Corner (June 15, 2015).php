        <p align="center"><b>Fragile 5 back under pressure</b></p>
        <p align="justify">Morgan Stanley coined the phrase “Fragile 5” in August 2013 to represent vulnerable emerging market economies that have become too depended on unreliable foreign investment to finance their growth and cover current account deficits.</p> 
        <p>This year, the Fragile 5 currencies are back under pressure (with the exception of the Indian rupee) similar to what happened during the 2013 taper tantrum. Among the members of the Fragile 5, only India has made significant improvements in its current account, hence the rupee’s outperformance.</p>
        <img src="image/061515chart1.jpg" height="300" width="600"><br>
        <i>Source: Yahoo Finance, Wealth Securities Research</i>

        <p align="center"><b>Asian currencies weaker amid interest rates cuts</b></p>
        <p align="justify">Asian currencies (with the exception of the Taiwanese dollar) are weaker against the US dollar this year as Asian central banks continue to combat weak economic demand by cutting rates. China and South Korea have cut rates twice this year while Japan has maintained rates at zero. Other Asian central banks have also maintained their easing stance amid the growing likelihood of a US policy rate increase.</p>
        <img src="image/061515chart2.jpg" height="300" width="600"><br>
        <i>Source: Yahoo Finance, Wealth Securities Research</i>

        <p>Among Asian EM currencies, the Malaysian ringgit has fared worse this year, down 7.2 percent. The
ringgit has dropped to 3.748 last Friday, a level last seen during the financial crisis in 2009.</p>
        <p>Meanwhile, the Indonesian rupiah which is down 7 percent year-to-date, hit a 17-year low of 13,310 last
Friday.</p>

        <p align="center"><b>Peso tracks EM currencies lower</b></p>
        <p align="justify">The peso has weakened 1.4 percent year-to-date against the US dollar, tracking the decline of EM
currencies. Short-term, the peso may retest its post-2013 tantrum low of 45.405 where it previously
found support.</p
    
        <p>While the peso may not be spared from the volatility arising from heightened risk aversion and EM
currency pressure, we believe that it will continue to be one of the more stable currencies in Asia. Our stable OFW remittances, robust BPO revenues and high foreign reserves should help cushion peso volatility.</p>

