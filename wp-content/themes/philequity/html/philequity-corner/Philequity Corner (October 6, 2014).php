        <p align="center"><b>Other Emerging Mkt Currencies vs. US Dollar</b></p>
        <p align="justify">Other emerging market countries continue to have their own set of domestic problems, causing their
respective currencies to underperform. Note that the Russian ruble is down a huge 21 percent because of
economic sanctions brought about by the Ukraine crisis.</p>

<img src="image/100614chart4.jpg" height="400" width="800"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p align="center"><b>Peso near strong support at 45 – 45.50</b></p>
        <p align="justify">While the US dollar continues to be strong, we must not lose sight of the fact that we are near very strong
support for the euro at 120 which was the low in 2010 and 2012. Global central banks may start to react
if the US dollar becomes too strong as it may become counter-productive to the world economy.</p>
        <p>Similarly, the peso is also near strong support at the 45 to 45.50 levels which were the lows last year.
What’s more important for the peso, however, is the absence of indiscriminate selling of emerging market
assets and currencies last year which led to the sharp drop of the peso. It appears that investors have now
learned to separate wheat from the chaff.</p>

    
