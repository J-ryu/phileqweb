        <p align="center"><b>It’s the Strong Dollar, Stupid! - Part 2</b></p>
        <p align="justify">Many have been asking these questions recently. Why is the peso weak? Why is the peso back at the 45 level vs. the US dollar (even reaching an intraday low of 45.267 last week) when just two months ago it was threatening to break below 43?</p>

        <p>The answer: THE STRONG US DOLLAR.</p>

        <p>The US dollar index is currently up 8 percent year-to-date after registering a record 12 consecutive weekly gains. The US dollar index has climbed to a four-year high last week and is on track for its best yearly gain in nine years.</p>

        <p align="center"><b>Dollar strength, not peso weakness</b></p>
        <p align="justify">Since last year, we have written several articles about the resurgence of the dollar (<i>The Peso Tops Out</i>, May 27, 2013) and (<i>It’s the Strong Dollar</i>, Stupid, June 24, 2013). While we don’t see any change in the fundamental underpinnings of the peso, the inherent strength of the US dollar is causing the peso to depreciate along with other currencies.</p>

        <p align="center"><b>Peso vs. the euro</b></p>
        <p align="justify">Since most people only monitor the peso-dollar rate, many are not aware of what is happening to the peso relative other currencies. Below is euro-peso chart. As you can see, the peso has appreciated 7.6 percent against the euro from 61.20 to 56.43 in less than a year.</p>

