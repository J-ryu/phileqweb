        <p align="center"><b>Major Currencies vs. US Dollar</b></p>
        <p align="justify">Among the major currencies, the Swiss Franc has depreciated the most since recording its year high, down 11.2 percent vs. the US dollar. The 2nd and 3rd worst performers were the euro and the yen which are down 10.6 percent and 8.9 percent from their year highs, respectively.</p>
        <p>On the average, major currencies have weakened 8.7 percent from their highs and are down an average of 5.6 percent year-to-date vis-a-vis the US dollar.</p>

        <img src="image/100614chart2.jpg" height="400" width="800"><br>
