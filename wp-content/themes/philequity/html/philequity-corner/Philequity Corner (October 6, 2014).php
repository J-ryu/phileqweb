        <p align="center"><b>Asian Currencies vs. US Dollar</b></p>
        <p align="justify">Asian currencies were also down against the US dollar with an average of 4.1 percent drop from their highs. The Indonesian rupiah has weakened the most because of recent political problems. Meanwhile, the peso is in the middle of the pack with a 3.7% decline from its year high as can be seen from the table below.</p>
        <p>On the other hand, Asian currencies were basically unchanged on a year-to-date basis, declining by only 0.7 percent vs. the greenback.</p>

        <img src="image/100614chart3.jpg" height="400" width="800"><br>
