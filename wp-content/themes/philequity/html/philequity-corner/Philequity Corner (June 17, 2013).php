        <p>According to EPFR Global, EM equity funds posted $10.8 billion outflows in the last two weeks ending June 12, 2013. This has halved the YTD EM inflows from $22 billion to $11.8 billion. On the debt side, dedicated EM funds registered $2.5 billion outflows the past week, the largest outflow week since September 2011.</p>

        <p align="center"><b>Markets turned topsy-turvy</b></p>
        <p>The markets have turned topsy-turvy. Markets trading at all-time highs (Philippines, Indonesia, Thailand and Turkey) were the hardest hit. Indiscriminate selling from hedge funds that are deleveraging has turned the best performing markets to worst.</p>

        <img src="image/061713chart4.jpg" height="190" width="700"><br>
