        <p align="center"><b>Window of opportunity</b></p>
        <p>The 15.5% drop in Philippine stocks since Bernanke spoke about QE tapering opens a window of opportunity for investors to pick-up stocks at cheaper valuations.</p>
        <p>What is good is that the indiscriminate selling which caused the market to drop and the peso to depreciate sharply has nothing to do with fundamental reasons but because of the unwinding and indiscriminate selling by foreign funds which at some point will end. Fundamentals not only remain the same, but have actually improved.</p>
        <p>Hence, while volatility still abounds, investors with long-term view should start nibbling or buying stocks or equity funds in tranches because valuations have become cheaper. At the end of the day - when deleveraging ends and normalcy returns - we believe that fundamentals will prevail.</p>
    
