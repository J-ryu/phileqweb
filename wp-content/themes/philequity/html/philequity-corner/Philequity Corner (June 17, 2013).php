        <p align="center"><b>Deleveraging spreads to emerging markets</b></p>
        <p>The volatility in the JGBs and US treasury has reverberated across the globe. What is happening now is a de-leveraging of funds from emerging markets back to the US and unwinding of trades which caused the sudden appreciation of the yen, the US dollar and the euro against EM currencies.</p>
        <p>This forced liquidation is evident on the Bloomberg USD Emerging Market Sovereign Bond Index and the iShares MSCI Emerging Market Index Fund (see chart below) which have declined by 5 percent and 8 percent, respectively, from their May 22 prices.</p>

