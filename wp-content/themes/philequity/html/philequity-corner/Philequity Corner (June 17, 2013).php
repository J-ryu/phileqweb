        <p align="center"><b>The Big Unwind</b></p>
        <p>Throughout much of 2013, global equities rose on the back of decreasing risks in Europe, China’s apparent soft-landing, the resurgence of Japan because of “Abenomics”, and the sustained economic recovery in the US. The S&P 500 index and the DJIA registered new all-time highs, while the Nasdaq Composite index went to a 12-year high. In Europe, the Stoxx 50 Index and the German DAX Index made new all-time highs. Meanwhile, the Nikkei climbed to a 5-year high and became the best performing market in the world.</p>

        <p>All these happened in an environment of ultra-low interest rates brought about by quantitative easing as investors shrugged of any weak data knowing that the Fed, the ECB, BOJ and other central banks are always there to provide more liquidity.</p>

        <p align="center"><b>QE tapering spooks the market</b></p>
        <p>Equities markets started to correct when Bernanke hinted of QE tapering last May 22. In his own words, Bernanke said: <i>“If we see continued improvement and we have confidence that it is going to be sustained then in the next few meetings we could take a step down in the pace of purchases.”</i></p>
        <p>Equities markets started to correct when Bernanke hinted of QE tapering last May 22. In his own words, Bernanke said: “If we see continued improvement and we have confidence that it is going to be sustained then in the next few meetings we could take a step down in the pace of purchases.”</p>
        <p>Thus, in a case of “good news is bad news”, markets worldwide started to sell-off on concerns that the Fed will scale back its stimulus efforts if the US economy and labor market continue to improve.</p>

        <p align="center"><b>Spike in US Treasury & JGB yields</b></p>
        <p>Prior to Bernanke’s May 22 “Fed tapering” speech and the subsequent sell-off in equities markets, the bond markets were already providing a clue of what was to come. The 10-year JGB yield bottomed out at 0.45% last April 4, 2013. The crucial level for 10-yr JGB yields is 1%. Breaking above that level would mean much higher yields thereafter.</i></p>

