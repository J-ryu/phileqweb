        <p align="center"><b>Unwinding of the yen-carry trade</b></p>
        <p>Several months back, when the Bank of Japan carried out Abenomics (doubling of monetary base, inflation target of 2% and purposeful depreciation of the yen), investors and fund managers around the world flocked to what was a “no brainer” carry trade – short yen and long Nikkei. This meant borrowing yen which was purposely being weakened by the BOJ, and simultaneously buying Japanese stocks which would benefit from higher inflation and the resurgence of export-driven Japanese economy.</p>
        <p>Thus from a low of 8,534 last Oct 2012 the Nikkei zoomed 83 percent to a five-year high of 15,627 last May 22. The USDJPY rate gained 32 percent from 78 to a high of 103.16 over the same period.</p>
        <p>This trade worked perfectly until Bernanke’s ominous May 22 speech regarding the impending Fed tapering. With bond yields on the rise and the Fed signalling a tapering of QE, the Nikkei has since slumped 19% to 12,686. Meanwhile, the USDJPY rate has since declined 8.6% to 94.31.</p>

        <img src="image/061713chart2.jpg" height="400" width="800"><br>
