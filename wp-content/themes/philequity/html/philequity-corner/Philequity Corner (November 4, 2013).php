        <p align="center"><b>The Goldilocks Peso</b></p>
        <p align="justify">In the children’s fairytale “Goldilocks and the Three Bears”, a little girl named Goldilocks went wandering in the forest where she soon came upon a friendly little cottage which belonged to three bears.</p>
        <p>Inside the little cottage she found three bowls of porridge. Since she was hungry, she decided to take a spoonful from the first bowl which belonged to Papa Bear but she found it too hot.</p>
        <p>Then she tried a spoonful from the second bowl which belonged to Mama Bear but she found it too cold.</p>
        <p>Finally, she tried a spoonful from the third bowl which belonged to Baby Bear and found it just right. And Goldilocks happily ate it all up.</p>

        <p align="center"><b>The Sweet Spot</b></p>
        <p align="justify">In a previous article, we have written about the peso’s sweet spot (see <i>Peso’s Sweet Spot,</i> January 30, 2012). This sweet spot for the peso which lies between 42 and 45 is what we think is best for the economy. This is also what Goldilocks would prefer, not too hot, not too cold.</p>
        <p>Papa Bear’s porridge which was too hot is similar to a peso that is too strong. Too hot a peso is bad for the economy because it hurts the OFWs, the export sector and the BPOs.</p>
        <p>Mama Bear’s porridge which was too cold is like a peso that is too weak. Too weak a peso is also bad because it can exacerbate inflation especially when prices of oil and agricultural commodities start creeping up.</p>
        <p>Meanwhile, Baby Bear’s porridge which was just right refers to the peso at its sweet spot. The BSP wants the exchange rate to stay at these levels because it is good for the economy. It is where inflation would remain benign and robust economic growth would be sustained. This is the Goldilocks Peso.</p>

        <p align="center"><b>“Too hot” a peso is bad</b></p>
        <p align="justify">Last March 2013, the peso became “too hot” that it almost broke below the P40/$1 level. This is the time when most foreign banks were forecasting the peso to reach P37/$1.</p>

        <p>While the strong peso reflected a strong Philippine economy, we in Philequity have time and again pointed out that too strong a peso will actually hurt the country in the long run. In fact, we have been warning about the danger of the peso becoming too strong since last year.</p>

        <p>We said that the risk to the economy is a peso that is too strong.<i> “A stronger peso at this point would affect the OFWs, the exporters and the BPOs which are the major sources of our current account surplus and foreign exchange reserves (see Be Careful What You Wish For,</i> May 7, 2012).”</p>

        <p>Too much of a good thing can also be bad. <i>“Most worrying is the peso’s sharp appreciation against the rupee, the currency of India which is our competitor in the booming Business Process Outsourcing (BPO) industry (see Too Much of a Good Thing,</i> July 9, 2012).”</p>

        <p>We also said that the Philippines should learn from what happened to Japan during their lost decades. Due to a strong yen in past years,<i> “many of Japan’s export industry became unprofitable, the Japanese stock market stagnated, and the economy never recovered (see Is the Peso Too Strong?,</i> December 31, 2012).”</p>

        <p align="center"><b>Peso tops out</b></p>
        <p align="justify">By mid-2013, everything reversed for the peso. We noticed that the US dollar has gotten fundamentally strong as the US economy recovered. Meanwhile the Japanese yen have sharply weakened due to Abenomics.</p>

        <p>When the peso broke above 42, we concluded that <i>“the peso’s long-term uptrend against the US dollar was over (see Peso Tops Out,</i> May 27, 2013).” At that point, the risk of the peso making new highs has been reduced dramatically.</p>

        <p>This, we said, is actually <i>“healthy for the economy and not a cause of a concern”</i> as the peso swings back to its sweet spot (see <i>Back to the Sweet Spot,</i> June 3, 2013).</p>

        <p align="center"><b>Peso suffers a “cold” spell</b></p>
        <p align="justify">However, rising interest rates brought about by imminent Fed tapering <i>“exposed the vulnerabilities of Asian countries with high current account deficits and high inflation such as India and Indonesia (see The Best House in a Bad Neighborhood</i>, September 2, 2013).”</p>
        <p>This triggered a regional asset sell-off which caused Asian currencies, especially the rupee and the rupiah to drop sharply. The peso was not spared as it hit a year low of 44.82 which nearly touched the upper limit of its sweet spot against the greenback on August 28, 2013.</p>

        <p align="center"><b>Rally in EM equities triggers currency rebound</b></p>
        <p align="justify">By September, emerging equity markets started to rally led by the BRICs (Brazil, Russia, India and China), up more than 20% from their year lows which technically put the markets back to bullish mode. Likewise, the PSEi has bounced 18% from the August 28, 2013 intraday low of 5,562.</p>

        <p>This triggered a rebound in EM currencies which we pointed out in a previous article:</p>

        <p><i>“There are indications that EM equities are turning. If the strength in EM equities continues, we see the peso stabilizing and even improving towards the 43 level.” (see Peso Bull Run is Over,</i> September 9, 2013)</p>

        <p align="center"><b>Not too hot, not too cold</b></p>
        <p align="justify">The announcement by the Fed last September 18 that there will be “no taper yet” further provided a boost to EM markets and currencies. The result is a more stable peso which has since traded in a narrow range between 43 and 43.50 which is right in the middle of its sweet spot.</p>
        <p>For us in Philequity this is exactly the best scenario for the Philippine economy… the Goldilocks Peso that is not too hot and not too cold.</p>
        <p>The more it stays there, the better for everybody. Just the way the BSP, Baby Bear and Goldilocks like it.</p>

