        <p>Both the OFW and BPO sectors are key drivers of the country’s resilient consumption spending.
Rising revenues from these sectors have resulted in rising living standards across the country as
household consumption increase – from basic needs, such as food, shelter and clothing, to
leisure, some luxuries and higher quality of education.</p>

        <p>These two key sectors have also proven to be major macroeconomic stabilizers especially during
times of external global shocks. Recent experience show that remittances and BPO revenues
continued to grow despite the US subprime crisis and the European sovereign debt crisis.</p>

        <p align="center"><b>OFW remittances recover strongly in March</b></p>
        <p align="justify">During the last couple of months, there have been concerns that the growth in OFW remittances
may have stalled after the January 2015 data showed that it only grew by 0.5 percent. The latest
data from the BSP, however, indicate that cash remittances rose 11.3 percent to $2.1 billion last
March 2015. This marked the fastest growth in five years, recovering from the slower growth in
previous months. This also brings cumulative cash remittances for the 1st quarter of 2015 to $5.8
billion, up 5.1 percent year-on-year.</p>

