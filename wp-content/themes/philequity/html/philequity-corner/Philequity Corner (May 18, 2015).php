        <p align="center"><b>Current account surplus, high GIR</b></p>
        <p align="justify">Thanks to the massive OFW and BPO revenue inflows, our country enjoys a comfortable current account surplus which amounted to $1.38 billion in December 2014. Gross international reserves, meanwhile, stood at $80.8 billion as of April 2015. This resulted in a steady peso (trading between 43.20 and 45.30 against the US dollar) at a time when the US dollar index surged by more than 25 percent from May 2014 to March 2015.</p>

        <p align="center"><b>Has the dollar topped out?</b></p>
        <p align="justify">While the peso has remained steady, the volatility in other currencies have increased recently which could be a prelude to a US dollar topping out. Note that since the US dollar index peak of 100.39 set in March, it has shed 7.2 percent. It now appears that the US dollar has either topped out or is now consolidating, rather than getting too strong.</p>
        <p>This should be good for the peso because we don’t want the peso to get too strong against other currencies such as the yen or the euro. This, however, will be the topic for a future article.</p>

        <p align="center"><b>The global Pinoy</b></p>
        <p align="justify">El Gamma Penumbra and the other Filipino performers in Asia’s Got Talent have shown that the Filipino talent and artistry are world class, just like Manny Pacquiao who brought the Philippines to the forefront of world boxing. Similarly, our OFWs and BPO workers have shown that they can compete in the world stage. The ever-growing remittances and expanding BPO revenues are a testament that Filipinos are now being recognized and appreciated globally.</p>
        <p>But moreover, our bull market in stocks, the stable peso, investment grade credit rating and sustained economic expansion are signs that the Filipino and the Philippine economy have truly stamped their mark on the global arena.</p>

       
