        <p align="center"><b>Thanks to all Filipinos all over the world!</b></p>
        <p align="justify">Congratulations to Filipino group El Gamma Penumbra who won the first Asia’s Got Talent show held in Singapore. The shadow play group was voted best among the nine Asian grand finalists, four of which came from the Philippines. The talent show featured singers, dancers, acrobats, magicians and other performers of all ages across 20 countries in Asia.</p>

        <p>In an emotional speech during the awarding, the group dedicated their victory to all Filipinos in all parts of the world. <i>“To all our supporters, in all parts of the world, in Asia, especially to all Filipinos, maraming salamat po!”</i></p>

        <p align="center"><b>The OFW</b></p>
        <p align="justify">Like what El Gamma Penumbra said, Filipinos now are all over the world. Filipinos have long been recognized world-wide for their talents and skills, not only in the field of entertainment, but in other fields as well.</p>
        <p>According to the Commission on Filipinos Overseas, there is an estimated 10.5 million Filipinos working overseas as doctors, physical therapists, nurses, accountants, IT professionals, engineers, architects, entertainers, technicians, teachers, military servicemen, seafarers, students, domestic helpers and caregivers. They represent more than 10 percent of the Filipino population.</p>
        <p>Throughout the years, these overseas Filipino workers (OFWs) have served as our ambassadors of goodwill, promoting the image of the Filipino as a competent, hard-working and dependable worker. OFWs have contributed immensely to the socio-economic development of their respective communities abroad and our country as well.</p>

        <p align="center"><b>The BPO worker</b></p>
        <p align="justify">In addition to the OFW sector, the fast-growing BPO sector has likewise put the Filipino talent on the global map. Advances in telecommunications and internet connectivity have allowed the Filipino to export his skills and talents without leaving for abroad.</p>
        <p>In a past article, we mentioned that Filipino workers possess unique traits that allow them to thrive and excel in different locations and in different situations. <i>“Aside from having a good grasp of the English language, Filipino workers are respectful, compassionate, amiable, courteous, flexible and adaptable. Moreover, Filipinos are kind, tolerant, patient and enduring… These are the reasons that propelled the Philippines to become the #1 call center destination in the world.”</i> (see <i><b>Present Day Heroes, August 23, 2013</b></i>)</p>

        <p align="center"><b>Two tables driving the Philippine bull market</b></p>
        <p align="justify">In our previous investor presentations, we have shown two tables driving our economic boom and the bull market in stocks.</p>
        <p>We have often said that the Philippines would not have experienced a secular bull market and resilient economic growth if not for the OFW and BPO sectors. Revenues from these two sectors combined amounted to $42.7 billion last year.</p>
        <p>From just $1 billion in 1989, the cash remittances of OFWs to the country have steadily grown to
$24.3 billion by 2014, representing a 25-year CAGR of 13.6 percent. As of end-2014, OFW
remittances amounted to 8.1 percent of Philippine GDP.</p>

