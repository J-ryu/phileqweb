        <p>Meanwhile, the growth of the BPO sector has been astounding with revenues below $1 billion in
2004 to $18.4 billion as of end-2014 or a 10-year CAGR of 27 percent. It is now equivalent to
5.4% of GDP as of end-2014.</p>

    <p><b>BPO Revenues ($ billions)</b></p>
