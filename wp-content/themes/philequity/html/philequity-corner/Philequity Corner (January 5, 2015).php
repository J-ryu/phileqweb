        <p align="center"><b>And the 2014 winners are…</b></p>
        <p align="justify">In our article last week, we showed the best performers in the Philippine Stock Exchange for the year 2014. Though there was still one trading day that came after our article, the tally and the final score did not change significantly. In this article, we show how the PSE Index and the Philippine peso performed last year vs. their global counterparts.</p>

        <p align="center"><b>China surges, Russia lags</b></p>

        <div align="center">
        <img src="image/010515chart.jpg" height="700" width="400"></div>

        <p><b>Best Performing Stock Market of 2014: China (Shanghai Composite).</b>&nbsp;China was the best performing stock market last year, posting a return of 52.9% for 2014. This might have been a surprise to many, as economists and investors have been concerned about a potential hard-landing in China. As the country was able to avert this scenario, analysts started to focus on China’s cheap valuations and how it was a compelling buy for global investors. China also recently cut its interest rate and is expected to come up with its own monetary stimulus, following the lead of the US, Europe and Japan.</p>
   
        <p><b>Worst Performing Stock Market of 2014: Russia (Micex).</b>&nbsp;Russia was the worst performing stock market last year, dropping by as much as 21.4% before ending 2014 with a 7.1% loss. Economic sanctions arising from the Ukraine conflict took their toll on the Russian economy. This was compounded by declining oil prices, which caused further weakness in the country’s oil-dependent economy.</p>

        <p align="center"><b>Philippine stock market outperforms</b></p>
        <p align="justify">In the table above, we show that the PSE Index outperformed most of its global counterparts by gaining 22.8% in 2014. Our country’s robust domestic consumption continued to drive strong economic growth, insulating our economy from the drop in commodity prices and the prevailing global weakness.</p>
        <p>Next to China, another top gainer is India, which gained 29.9% and reached new all-time highs in 2014. The election of Prime Minister Narendra Modi has been expected to pave the way for the implementation of structural reforms in India. The US also reached new all-time highs and rose 11.4% in 2014 as the US economy continued to show sustained economic growth despite the end of quantitative easing (QE).</p>

        <p align="center"><b>The almighty dollar and the crumbling ruble</b></p>
        <div align="center">
        <img src="image/010515chart1.jpg" height="700" width="400"></div>

        <p><b>Best Performing Currency of 2014: US dollar.</b>&nbsp;The US dollar gained 13.2% in 2014 vs. a basket of global currencies. Though the Fed has pledged to be patient with impending policy changes, the US economy has so far shown that it can sustain its economic recovery even without QE and possibly even with higher interest rates. Hence, the US is ahead of the curve in terms of monetary policy compared to Europe, Japan and China, which are all doing their own versions of monetary stimulus.</p>

        <p><b>Worst Performing Currency of 2014: Russian ruble.</b>&nbsp;Economic sanctions and plunging oil prices took a disastrous toll on the Russian economy. This caused the Russian ruble to crumble last year, dropping 84.8% in 2014.</p>

        <p align="center"><b>Peso stable in 2014</b></p>
        <p align="justify">As seen in the table above, the decline of the Philippine peso vs. the US dollar last year was relatively muted, even as most global currencies weakened vs. the US dollar. While commodity exporting countries have been adversely affected by falling commodity prices, our country’s economy, which is driven by domestic consumption, stands to benefit from lower commodity prices. And while the peso depreciated against the US dollar last year, it actually appreciated against other global currencies, such as the euro and the Japanese yen.</p>

        <p align="center"><b>PSE Index: Gunning for a 7-peat</b></p>
        <div align="center">
        <img src="image/010515chart2.jpg" height="200" width="700">
    </div>

        <p>The PSE Index posted a flattish return in 2013 as Yolanda spoiled a budding stock market recovery. However, our stock market bounced strongly in 2014, delivering a 22.8% return and posting its 6th consecutive year of positive return starting 2009. Philequity Fund has also delivered another year of outperformance by returning 28.7% in 2014. This is proof that the uninterrupted bull market that started in 2009 still continues and remains strong. Given our country’s sustained economic growth and the strong fundamentals of our corporates, we believe that this secular bull market will be sustained. Barring any negative surprises or black swans, our stock market is poised to deliver another positive year in 2015 and is on-track to post its 7th consecutive year of positive returns since the PSE Index reached a bear market bottom in March 2009. Hence, we advise investors and clients to stay the course and to continue riding the historic bull market that we are currently witnessing.</p>

