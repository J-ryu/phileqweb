    	<br><p><b>And the last became first</b></p></span>

        <div align="justify" class="page">
            <p>Emerging market currencies are making a comeback in 2016. After a couple of contagion scares in
August 2015 and January 2016 due to the depreciation of the Chinese yuan, EM currencies have rallied
sharply the past two months on the back of dovish Fed statements and rebounding commodity prices led
by crude oil.</p>
    
        <p align="center"><b>The worst became best</b></p>
        <p align="justify">Indications that the Fed is unlikely to pursue an aggressive interest rate tightening cycle has led the US
dollar to pull back, if not completely reverse. Hence, EM currencies such as the Malaysian ringgit, the
Indonesian rupiah, the Russian ruble and the Brazilian real - which were sold down the most when the
dollar was strong - are now leading the recovery now that the dollar is weaker. What were previously the
world’s worst currencies became the best... THE LAST BECAME FIRST.</p>
<p>Significntly, we wrote about the “R” currencies (the ringgit, rupiah, ruble and real) back in January (see
<i>It’s the Strong Dollar, Stupid!</i> – Episode 3, Jan. 18, 2016). It is now evident that the worst currencies
were the “R” currencies, which are now the best performing currencies.</p>
<p>The tables below will show how currencies performed the past two years where significant highs and
lows were reached.</p>

        <p align="center"><B>Ringgit and rupiah lead recovery in Asian currencies</B></p>
        <p align="justify">In Asia, the worst performing currencies during the past two years were the Malaysian ringgit and the
Indonesian rupiah, falling as much as 41.4 percent and 29.8 percent from their 2014 highs, respectively.</p>
<p>In addition to poor oil and commodity prices last year, the ringgit was plagued by allegations of graft and
mismanagement over indebted state fund 1Malaysia Development Berhad (1MDB). Meanwhile,
President Widodo’s lack of parliamentary support to pursue his reforms weighed on the rupiah.</p>
<p>The Malaysian ringgit and the Indonesian rupiah both hit 17-year lows in September 2015 and have
rebounded since, gaining 12.5 percent and 10.5 percent, respectively.</p>
<td align="left"><img src="image/041116chart.jpg" height="400" width="800"></td>

        <p align="center"><b>Russian ruble and Brazilian real lead EM currencies</b></p>
        <p align="justify">Among EM currencies, the Russian ruble and the Brazilian real suffered the most the past two years,
depreciating as much as 144 percent and 90 percent against the US dollar, respectively.</p>
<p>The drop in oil prices is nowhere more evident than in Russia, which is the 3rd biggest oil producer in the
world and where 50 percent of federal budget and 25 percent of GDP comes from energy exports.
Meanwhile Brazil, which is suffering its worst recession in history, is also facing its deepest political
crisis in decades over a corruption scandal which reaches all the way to the top.</p>
<p>The Russian ruble bottomed out in January 2016 and is now up an impressive 18.6 percent from the low
buoyed by the recent 50 percent rally in crude oil prices.</p>
<p>The Brazilian real is up 14.1 percent from the low established last September 2015 on investor hopes that
regime change is just around the corner.</p>
<td align="left"><img src="image/041116chart1.jpg" height="400" width="800"></td>


        <p align="center"><b>Japanese yen, Canadian dollar & Aussie dollar lead DM currencies</b></p>
        <p align="justify">Among developed market currencies, the Japanese yen strengthened the most against the US dollar,
rallying 14 percent from its June 2015 low despite negative interest rates. Similarly, the Euro and the
Swiss franc are rallying despite negative rates.</p>
<p>The Aussie dollar and the Canadian dollar or the so-called “commodity currencies” have each advanced
more than 10 percent over the US dollar since their January 2016 lows.</p>
<td align="left"><img src="image/041116chart2.jpg" height="400" width="800"></td>

<p align="center"><b>Too strong a dollar is bad</b></p>
        <p align="justify">Since the market turmoil in January, it does appear that major central banks have coordinated and acted on cue, issuing statements one after another on what looks like an effort to contain the strengthening US dollar, stave off the Chinese yuan devaluation shock and stabilize global growth (see <i>Central Banks Strike Back,</i> Feb. 1, 2016).</p>
        <p>Too strong a dollar sparks unwanted global market volatility which we expounded in last week’s article (see <i>She Gets It,</i> April 4, 2016). And we quote:</p>
        <p><i>“Yellen gets it and understands that the US economy is not an island… She gets that dollar strength is detrimental not just to emerging economies, but to the US economy as well. Yellen understands that stability in global equities, currencies and commodities are of paramount importance before she can resume raising interest rates.”</i></p>

        <p align="center"><b>Biding time</b></p>
        <p align="justify">Whether this is just a temporary pause or an eventual reversal in the US dollar is beside the point. What’s important is that the message of Fed chair Yellen to defer future rate hikes until certain conditions are met and to contain US dollar strength is biding time for other economies to catch up on their own recovery. Additionally, it creates room for a little more policy easing from the ECB, BOJ and other CBs without risking new US dollar highs.</p>

        <p align="center"><b>A stable peso is good</b></p>
        <p align="justify">The pause in US dollar strength and the recovery of EM and Asian currencies is a welcome development for EM central banks as well as the BSP. In the previous article, we said that peso has reached bottom at 48.05 and we see it trading between 45.50 and 47.50 in the short- to medium-term (see <i>Peso stabilizes below 47,</i> March 21, 2016). Indeed a stable peso is good for the Philippine economy.</p>

    
