        <p align="center"><b>Historic Stock Market Milestones</b></p>
        <p align="justify">People normally don’t like to invest in stocks during the month of August because it usually coincides
with the Chinese Ghost month and is believed to be unlucky. In fact, people refer to the month of August
as AU-GHOST. But on the contrary, last month (August 2014) was a historic month for both global and
local stocks where milestones were set and records were broken.</p>

        <p align="center"><b>Milestone #1 - S&P 500 Index reach 2,000</b></p>
        <p align="justify">The S&P 500 index surpassed the psychological 2,000 level for the first time on August 26 (Tuesday) and
it is still above it as of August 29 (Friday) to close the month at 2,003.37. This set a new all-time high for
the S&P 500 index. Likewise, the Dow Jones Industrial Average closed at a record high of 17,098.85.</p>

        <p align="center"><b>Milestone #2 – S&P 500 Index triples in value</b></p>
        <p align="justify">The 2,000 level marked another milestone because it is the level where the S&P 500 index has tripled
since it bottomed at 666 on March 6, 2009. It took 25 months for the index to double from 666 and
another 40 months to triple in value.</p>

        <p>We wrote extensively about this historic bottom (see <b><i>Opportunity of a Generation</i></b>, November 8, 2008,
<b><i>2009: A Year of Opportunity</i></b>, January 12, 2009, <b><i>666</i></b>, March 23, 2009, and <b><i>666 on 3-6-9</i></b>, April 13, 2009).</p>

        <p>And we have, time and again, reminded our readers and investors to stay the course (see <b><i>Global Bull
Market</i></b>, Jan. 7, 2012, <b><i>Staying the Course</i></b>, February 6, 2012, <b><i>Secular Bull Market</i></b>, January 28, 2013, and
<b><i>The Global Bull Market Continues</i></b>, July 7, 2014).</p>
        
        <p align="center"><b>Milestone #3 - Best August performance since year 2000</b></p>
        <p align="justify">August 2014 also marked the best performance for the month of August in 14 years by the S&P 500
index. The index gained 3.8 percent last month compared with August gains that ranged from -6.4
percent to 3.4 percent the past 14 years.</p>

