            <p align="center"><b>The rule of round numbers</b></p>
        <p align="justify">In technical analysis, a rule states that when round numbers are breached (like 10,000 for the Dow Jones
or 5,000 for the Nasdaq Composite) they become major psychological support or resistance areas. Hence, many are expecting the US markets to correct after the S&P 500 hit the 2,000 level because of the socalled “rule of round numbers.”</p>
<p>But as we have mentioned many times in the past, corrections are hard to predict. The US markets may
correct from here not so much because the S&P 500 hit 2,000 but because the market has become shortterm
extended and moderately overbought, or another geopolitical event may occur that will cause the
market to react.</p>

            <p align="center"><b>Respecting the trend</b></p>
        <p align="justify">One of the basic tenets of technical analysis is that a “trend has a higher probability of continuation than
reversal (Dow Theory).” And that once established, it takes considerable power and time to turn it. So,
while the market may take time to get comfortable at these new levels (i.e. consolidate from here), we
have to respect the fact that the US markets are still in a long-term uptrend.</p>

            <p align="center"><b>Corrections in a bull market</b></p>
        <p align="justify">Meanwhile, the PSE Index slid 1.15% last week despite the record volume and foreign inflow. Note that
the market itself was short-term overextended following a 4-week run-up which took it from 6,850 on
July 28 to 7,160 on August 27. In the process, the PSEi also hit our 1Q15 target of 7,147 which implies
18x PER 2015.</p>
    <p>Nonetheless, we maintain our bullish stance on Philippine stocks. While short-term corrections are hard
to predict, we believe that they are part and parcel of a bull market. Hence, a correction in the market is
actually healthy and warranted because of the swift run-up which hit our target well ahead of time.</p>
    <p>Looking at the bigger picture, the long-term uptrend in Philippine stocks remain very much intact. So we
have to STAY THE COURSE and RESPECT THE TREND, until such time that we see a greater reaction
in the opposite direction that would nullify that trend.</p>

  
