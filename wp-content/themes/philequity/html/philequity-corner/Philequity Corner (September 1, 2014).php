            <p>Driving the gains was the strong earnings season, the second-quarter GDP revision (+20 basis points to
4.2 percent), and the still-strong consumer confidence (U. Michigan index came in at 82.5 percent vs. an
expectation of 80.0).</p>
<p>Investors shrugged off rising geopolitical tensions, in particular, the heightened risks of war in the Middle
East and Eastern Europe and focused on the historic low interest rates and the continuation of the
recovery started over the past few years.</p>

            <p align="center"><b>Milestone #4 – Global equities reach record market cap</b></p>
        <p align="justify">According to Bloomberg, the value of equities globally reached a record of $66 trillion when the S&P 500
hit 2,000. This compares with only $25 trillion in March 2009 and $63 trillion at the 2007 peak.</p>

            <p align="center"><b>Milestone #5 - Record volume in PSEi</b></p>
        <p align="justify">The Philippine Stock Exchange saw record volume last August 29 as value turnover amounted to
Php32.55 billion even without the benefit of huge block sales. Shares traded in Philippine Long Distance
Telephone Co. totaled Php16.28 billion on the last day of MSCI rebalancing. PLDT’s weighting on the
MSCI Philippine Index was doubled following MSCI’s August 2014 Quarterly Index Review.</p>

