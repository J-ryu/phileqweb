        <p align="center"><b>Coal Prices</b></p>
        <p align="justify">With the decline in crude oil prices, the price of coal also tagged along. CAPP coal for Dec. 2014 delivery fell 19.7 percent to $53.25 per short ton from a peak of $66.28 in May.</p>

