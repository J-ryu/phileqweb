        <p align="center"><b>Positive effects on the economy</b></p>
        <p align="justify">Lower oil prices, if sustained, should have a positive effect on domestic growth. A fall in oil prices is effectively like a free tax cut. In theory, the fall in oil prices could lead to higher purchasing power and consumer spending and hence add to real GDP.</p>
        <p>Lower inflation expectations, in turn, would give the BSP ample room to refrain from monetary tightening which should further boost domestic demand. Last week, the BSP decided to keep policy rates steady as inflation is expected to settle within its target of 3 to 5 percent for this year.</p>

        <p align="center"><b>“Trick or treat” from Kuroda</b></p>
        <p align="justify">Last Friday was Halloween and “trick or treat” is a customary practice for children. It was also the day when Bank of Japan Governor Kuroda played a “trick” on short-sellers and delivered a “treat” to global investors. The BOJ announced an unexpected major increase in its quantitative easing (QE), bringing it to 80 trillion yen from 50 trillion.</p>
        <p>Japan’s $1.2 trillion Government Pension Investment Fund (GPIF) also announced new allocations for its portfolio, which includes raising both domestic stock holdings to 25 percent from the current 12 percent. Likewise, holdings of foreign stocks will be increased to 25 percent from the current 12 percent. Meanwhile, holdings of domestic bonds will be cut to 35 percent from around 60 percent.</p>
        <p>The treat from Kuroda and GPIF sent the Nikkei up by 4.83 percent and the US benchmark S&P 500 index by 1.17 percent on Friday. Stock markets all over the world went up substantially with many making new historic highs. Meanwhile, the PSE index was up by 0.62 percent as it hurdled past previous resistance of 7,200 to close at 7,215.</p>


