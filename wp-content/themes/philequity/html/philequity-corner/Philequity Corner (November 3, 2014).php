        <p align="center"><b>Lower cost of living, higher savings & higher consumption</b></p>
        <p align="justify">The impact of lower oil prices is broadly positive to oil-importers like the Philippines. The Philippines imports roughly 110 million barrels of oil per year. Hence, every $10 reduction in oil prices will translate to an annual windfall of $1.1 billion in savings.</p>
        <p>Lower oil prices helps reduce the cost of living by lowering transport costs. In fact, the prices of diesel and gasoline (at retail levels) have already dropped by 14 percent and 11 percent, respectively since June.</p>

