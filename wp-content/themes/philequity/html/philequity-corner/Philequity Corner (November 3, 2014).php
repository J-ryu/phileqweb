        <p align="center"><b>Lower inflation</b></p>
        <p align="justify">Falling oil prices should help also help bring down inflation. Lower oil prices pass through directly into lower fuel costs and retail electricity prices. Headline inflation already fell to 4.4 percent in September from a peak of 4.9 percent in July and August.</p>
        <p>The Bangko Sentral ng Pilipinas, in the latest assessment, expects sustained easing in inflation pressures as prices of the Asian benchmark, Dubai crude, slid to an average of $87.86 per barrel on Oct 1 to 24 from an average of $96.59 per barrel in September.</p>

        <img src="image/110314chart3.jpg" height="400" width="800"><br>
