        <p align="center"><b>Falling oil prices: a boon to the Philippine economy</b></p>
        <p align="justify">Oil prices have dropped by more than 25% since June on the back of weak global oil demand, a supply glut from North America and a strong US dollar.</p>
        <p>The International Energy Agency (IEA) disclosed in its recent oil market report that world oil demand is softening at a remarkable pace as the European and Chinese economies falter. Supplies surged, particularly in the US, where the shale boom expanded total oil output to a 28-year high of 8.5 million barrels per day in July. In addition, there was no material impact on crude supplies by geopolitical events in Ukraine and Iraq.</p>

       <p align="center"><b>Crude Oil Prices</b></p>
        <p align="justify">The global benchmark Brent crude oil, fell to four-year low to close at $85.86 per barrel last month. Meanwhile, the US West Texas Intermediate (WTI) oil fell to a 2-year low to close at $80.70 per barrel.</p>
