        <p align="center"><b>Example of a Real-Life Investor</b></p>
        <p align="justify">In this article, we discuss Peso Cost Averaging as an investment strategy. We center on an actual investor of the Philequity Fund who has been employing this strategy since 2003. In fact, this is the same investor whose statement of account we presented during our Investors’ Briefing last March 8, 2014.</p>

        <p align="center"><b>What is Peso Cost Averaging?</b></p>
        <p align="justify">As we said in our article last week (<i>The Time to Invest is Now</i>, March 24, 2014), Peso Cost Averaging “is an investing technique which involves buying a fixed peso amount of stocks on a regular schedule.” To illustrate, an investor applying Peso Cost Averaging can invest P10,000 on a monthly basis. Another investor can opt to invest P5,000 every week. The amount and the frequency of investing will depend on one’s financial capacity and tolerance for risk.</p>

        <p align="center"><b>Why is Peso Cost Averaging Stress-Free?</b></p>
        <p align="justify">Investing can be stressful especially for first time investors who have never experienced volatility in the markets. Also, many are daunted by the thought of investing a large amount of money in one go. But with Peso Cost Averaging, as the following example will show, one can reach a gross investment of P1 million in a short amount of time by investing as little as P5,000 on a weekly basis.</p>

        <p align="center"><b>A Real-Life Investor</b></p>
        <p align="justify">During our Investors’ Briefing last March 8, 2014, we included in our presentation the actual statement of account of Mr. Cecilio Castro, Jr. In our article today, we narrate how Peso Cost Averaging has allowed him to build his investment in the Philequity Fund.</p>
        <p>Mr. Cecilio began investing in Philequity Fund in September 2003 with an initial investment of P31,000. Since then, he has been investing regularly – investing around P4,000 to P10,000 weekly or biweekly. Even throughout the vicious bear market of 2008, he continued Peso Cost Averaging.</p>
