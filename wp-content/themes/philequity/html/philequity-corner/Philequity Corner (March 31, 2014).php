            <p>We interviewed Mr. Cecilio over the phone and here is what he had to say about his experience as an investor – from choosing the right fund to investing consistently even during bear markets.</p>

            <p><b>Q:</b>&nbsp;How did you start investing in mutual funds?</p>
            <p><b>A:</b>&nbsp;I worked in Philequity from 1998 to 1999 and in a brokerage firm prior to that so I had knowledge on investments. I started investing once I had enough income to set aside. My first choice was Philequity because I knew how the fund invested…</p>
            <p><b>Q:</b>&nbsp;Which was?</p>
            <p><b>A:</b>&nbsp;Value investing. I also chose Philequity because it was not affiliated with any investment bank or insurance company. But most of all, I had full belief in Wilson Sy. For me, the fund manager’s reputation is a very critical aspect in choosing a mutual fund.</p>
            <p><b>Q:</b>&nbsp;What was the market like when you started investing in 2003?</p>
            <p><b>A:</b>&nbsp;I don’t remember. But I do remember the NAV at which I first entered the fund: P4.42 per share.</p>
            <p><b>Q:</b>&nbsp;What was your experience as an investor during the bear market?</p>
            <p><b>A:</b>&nbsp;I just kept investing. For those who set aside funds to invest regularly, you should not look at what the index level is. Investing consistently has proven to give you a good average cost in the long run. You cannot catch peaks and bottoms in the market.</p>
            <p><b>Q:</b>&nbsp;What can you say about financial literacy in the Philippines?</p>
            <p><b>A:</b>&nbsp;It still has a long way to go but people are much more knowledgeable now. Back in 2003 and much more in 1994 when Philequity started, only those who were in the industry of capital markets knew about mutual funds. But now, people are more aware as seen, I think, in how Philequity Fund has grown. I think Philequity has made investing in mutual funds very accessible by making the minimum additional investment only P1,000.</p>

