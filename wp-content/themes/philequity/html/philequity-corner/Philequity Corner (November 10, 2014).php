        <p align="center"><b>Resilience of Markets, Resilience of Men</b></p>
        <p align="justify">There are many lessons that can be learned from our country’s experience with Yolanda. As we saw again in this episode, there are opportunities in crises, especially if one is an astute student of the capital markets. Moreover, our experience with Yolanda has shown that despite the short-term pain, it is better to have a long-term perspective. If a country’s fundamentals are intact, the stock market will recover strongly after a crisis, no matter how devastating the calamity may be. Though Yolanda dented our economy and stock market’s performance, it did not change our country’s long-term structural growth story. Lastly, we learned never to discount the resilience of Filipinos and the resilience of the Philippine stock market. Time and again, we have seen both our countrymen and our stock market recover from extremely painful setbacks only to emerge better and stronger than before.</p>




