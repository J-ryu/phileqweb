        <p align="center"><b>A Stormy Outlook</b></p>
        <p align="justify">At first, people did not realize the magnitude of Yolanda’s damage. However, when images and videos of destruction were repeatedly flashed on local and international news channels, investors began to focus a bit too much on Yolanda’s devastation. Many fears surfaced about the extent of the damage, the possible impact to our GDP growth and the duration of the recovery. Many probably thought that it was the end of the bull market or that it would take a long time before our stock market recovers. As such, investors started to become gun-shy in buying Philippine stocks. Moreover, this gloomy scenario was probably the reason why many fund managers and investment houses made bearish calls on the PSE Index earlier this year. When they focused too much on the painful short-term picture, what they missed was the opportunity to buy low, in time for the ensuing recovery.</p>

        <p align="center"><b>Post-Yolanda Recovery</b></p>
        <p align="justify">We made presentations to various institutional clients shortly after the PSE Index hit its post-Yolanda bottom. When most investors were too focused on Yolanda’s damage, we urged them to welcome the impending recovery and not to lose sight of the positive long-term picture. Moreover, we said that in every crisis, there is an opportunity. We showed examples of how stock markets eventually recovered strongly after being hit by the most disastrous natural calamities. We show this in the table below.</p>

