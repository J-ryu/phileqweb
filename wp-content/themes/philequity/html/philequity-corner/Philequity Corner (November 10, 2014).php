        <p align="center"><b>Yolanda: 1-Year After</b></p>
        <p align="justify">One year ago, our country was hit by Yolanda, one of the strongest typhoons to ever make landfall. Its devastation was well-known and was closely chronicled by most broadsheets and TV stations. Its damage inevitably dented our economy and stock market’s performance. After Yolanda struck, we wrote the following articles to share our conviction about the eventual recovery of our stock market.</p>

        <p>- <i>Eye of the Storm</i>, November 18, 2013<br>- <i>Yolanda – A Black Swan</i>, November 25, 2013<br>- <i>Rebuilding Process for Country and Stock Market</i>, December 2, 2013<br>- <i>Yolanda and the Peso</i>, December 9, 2013</p>

        <p>In this article, we look back at Yolanda’s effect on our stock market and what has happened since it came to our shores one year ago.</p>

        <p align="center"><b>Triple Bottom</b></p>
        <p align="justify">Before Yolanda, our stock market had just recovered from two painful corrections caused by QE tapering (<i>The Tale of the Taper</i>, July 22, 2013) and the Asian scare (<i>The Best House in a Bad Neighborhood</i>, September 2, 2013). The PSE Index had just bounced from intraday bottoms of 5,678 (<i>5-6-7-8</i>, July 1, 2013) and 5,562. It was poised to move higher as it hovered around the 6,400 to 6,500 level. However, images of Yolanda’s damage and devastation prompted heavy foreign selling, causing our index to drop 10% to a bottom of 5,709. A triple bottom, which is a bullish chart pattern in technical analysis, was formed when the previous bottoms were retested and not broken. The chart below shows this.</p>

