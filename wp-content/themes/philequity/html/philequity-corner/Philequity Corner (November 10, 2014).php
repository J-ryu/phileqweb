        <p align="justify">We reached our post-Yolanda bottom on December 13, 2013. From the way the market is moving, it looks like our stock market will be substantially higher one year after the Yolanda bottom. In fact, despite the extent of Yolanda’s damage, the PSE Index is up 22.3% year-to-date while Philequity Fund is up 27.6% year-to-date.</p>

