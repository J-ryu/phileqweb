        <p>It is worth noting that the overnight placements and IPOs that were completed in the past 1 ½ months
amounted to P59.6b. These big-ticket placements and IPOs that were conducted over a very short
period of time have sapped our market’s liquidity and have caused the stock market’s recent weakness.</p>

        <p align="center"><b>Announcement of Stock Rights Offerings</b></p>
        <p align="justify">On top of what we have already shown, two of the biggest local banks announced that they will conduct
stock rights offerings. BPI will raise P25.0b while PNB will raise P15.0b. Note that both bank stocks fell
more than 5% after their respective announcements. These moves contributed to the prevailing
weakness of our stock market, which is already suffering from the indigestion of the massive share
issuances and placements. Moreover, investors have been relatively tentative and have become wary
that there may be more placements and share issuances that will be conducted soon.</p>

        <p align="center"><b>Weathering the Storms</b></p>
        <p align="justify">Our stock market has weathered a lot of global and local headwinds this year. Globally, we had to deal
with the prospect of QE tapering (<i>The Tale of the Taper,</i> July 22, 2013), the Asian scare (<i>The Best House in a Bad Neighborhood,</i> September 2, 2013) and the US government shutdown and debt ceiling problems (<i>Shutdowns and Debt Ceilings,</i> October 14, 2013). Locally, we had to endure natural disasters in the form of super typhoons and a strong earthquake.</p>
    
        <p align="center"><b>Napoles and Yolanda</b></p>
        <p align="justify">Aside from natural calamities, our country also had to witness man-made disasters like the Zamboanga siege, the Napoles scam and the ongoing pork barrel fiasco. Last weekend, our country was ravaged by Typhoon Yolanda, the strongest typhoon in history ever to make landfall. Notwithstanding all these, our stock market continues to perform strongly compared to our Asian neighbors. As we show in the table below, the PSE Index is still the best performer among the ASEAN-5.</p>

