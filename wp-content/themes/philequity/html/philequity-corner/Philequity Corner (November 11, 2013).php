        <p align="center"><b>Time to Digest</b></p>
        <p align="justify">Despite the resilience of our stock market, we are seeing that it is clearly suffering from indigestion. The torrent of share issuances and placements and the prospect of more to come have caused the balance of demand and supply to tip over the side of the latter. The resulting short-term weakness of our stock market has caused it to consolidate for the meantime. Nonetheless, we remain confident that the strong fundamentals of our country and corporates are intact. This is a temporary setback and it will not derail the bull market that we are experiencing. Given time, the market will be able to digest the avalanche of new shares and recover from the natural and man-made disasters that we have experienced. After this, we believe that the market will return to its upward path.</p>

        <p align="center"><b>Time to Help</b></p>
        <p align="justify">Our country is no stranger to natural disasters and super typhoons. Somehow, our people can cope with these things because of the magnanimity of Filipinos. Given the widespread damage that Typhoon Yolanda has dealt to our countrymen in the Visayas region, we expect that Filipinos will again share their generosity to help those who were severely affected by the typhoon. Helping each other has allowed us to bounce back from severe calamities and has transformed us into the resilient nation that we are known to be. And now is the time to help.</p>


