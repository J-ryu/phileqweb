        <p align="center"><b>A Torrent of Share Issuances and Placements</b></p>
        <p align="justify">Our stock market rallied strongly after hitting an intraday low of 5,562 on August 28. However, after that
strong run-up, our market has entered a consolidation mode. Though it briefly advanced after the US
government shutdown and debt ceiling problems were solved, the PSE Index pulled back shortly after.
Given our market’s recent movement, many are probably wondering why our stock market has not
rallied even as global headwinds have abated and our country’s fundamentals remain solid.</p>

        <p align="center"><b>Supply Overpowers Demand</b></p>
        <p align="justify">Listed local companies continued to take advantage of the favorable outlook for our stock market by
raising fresh capital. Though the additional capital will help the companies grow, the size, timing and
frequency of the share issuances and placements can cause a temporary weakness in the stock market.</p>

        <p>As we mentioned in a previous article, big-ticket share issuances and placements that occur within a
short span of time tend to sap stock market liquidity (<i>An Avalanche of Overnight Secondary Placements,</i>
July 23, 2012). In effect, these take away the money that could otherwise be used to buy stocks in the
market. These cause the stock market to be weak in the short-term, as the oversupply of shares
overpowers existing demand. The market corrected when it experienced an avalanche of overnight
placements last year and we are seeing something very similar this time around.</p>

        <p align="center"><b>A Bigger Avalanche</b></p>
        <p align="justify">The continued strong performance of the Philippine stock market provides an attractive background for
equity raising. Given favorable market conditions, corporates can price equity issuances higher and raise
more cash. We saw this happen last year, as P125.2b in fresh capital was raised from the overnight
placements, stocks rights offerings and initial public offerings (IPOs) in FY12.</p>

