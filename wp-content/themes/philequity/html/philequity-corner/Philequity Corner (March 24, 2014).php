        <p align="center"><b>The time to invest is… NOW</b></p>
        <p align="justify">In this article, we come out with the most frequently asked questions that we encounter, as well as some of the other questions that were discussed in our investors’ briefing last March 8, 2014.</p>

        <p><b>1. When is the right time to invest?</b></p>
        <p>We encourage everyone to save so that they can build their own investment portfolios. With that said, we believe that everyone should have a certain part of their investments in equities. Therefore, for those who are not invested in the stock market or are underinvested in equities, the right time to start investing is now.</p>

        <p><b>2. Why should we buy stocks?</b></p>
        <p>We have always said that buying stocks is like being a part-owner of a business. Buying stocks therefore allows one to partner with the greatest minds and the most profitable businesses in the country. If one wants to invest in local mall operators, he can buy SM Prime Holdings (SMPH) or Robinson’s Land (RLC). If one wants to invest in a snack food producer or a fast food chain, he can buy Universal Robina (URC) or Jollibee (JFC). If one wants to invest in diversified property companies, he can buy Ayala Land (ALI) or Megaworld (MEG). These are a few examples of companies that have strong business models and have continued to perform well over time. Consequently, their stock prices have also appreciated over time.</p>

        <p><b>3. How much of our money should we put in stocks?</b></p>
        <p>The amount of money one should put in stocks will depend on his investment goals and risk tolerance. One should always follow an asset allocation plan that involves diversifying one’s investments across various asset classes. Among the various asset classes that one should have are real estate properties, cash, deposits, fixed income, stocks or even businesses for some.</p>

        <p><b>4. Should we buy everything now?</b></p>
        <p>Timing the market in the short-term is extremely difficult even for professionals. Instead of buying all your equity exposure in one shot, we recommend buying in tranches or using peso cost averaging. Peso cost averaging is an investing technique which involves buying a fixed peso amount of stocks on a regular schedule. Depending on one’s total investment, this can mean buying every day, every other day, once a week or once a month. Doing this would eliminate the timing aspect while allowing investors to gradually increase their stock market exposure.</p>

        <p><b>5. With so much uncertainty around the globe, shouldn’t we wait for the stock market to come down before we start investing?</b></p>
        <p>The short-term movement of the stock market is impossible to predict. Nobody really knows how certain events will unfold in Russia, Ukraine, China or the US. We do not know what Putin or Yellen will say in their next public announcements. We also do not know if another company will declare default in China. What we do know is that there are local companies which have solid business models and will continue to thrive in any environment. Though the short-term picture may be murky, we are confident that the long-term upward trajectory of our stock market is intact. Further, we believe that many local stocks are currently at reasonable valuations given their attractive growth prospects.</p>

        <p><b>6. Some technical analysts point that our stock market is currently overbought and a correction is
imminent. Should we now wait for better levels to buy?</b></p>
        <p>Though we prefer to buy on a dip or correction, it is extremely difficult to predict when a correction will
occur, how deep it will be or how long it will last. Moreover, a correction can come in different forms –
it can be a quick but sharp drop, a relatively shallow pullback, a prolonged sideways movement or even
a rotation among the different sectors in our stock market. One is never certain of the correct levels to
buy or if one can buy all the shares that he wants at that price. Rather than wait for a correction, it
might be better for one to gradually build his equity exposure over time.</p>
        
        <p><b>7. What happened to our stock market last Friday? Why do you refer to it as the “Friday madness
closing-out sale?”</b></p>
        <p>Last Friday (March 21, 2014) was the last day for FTSE rebalancing. Our market dropped substantially at
the last second of trading as foreign fund managers scrambled to align their portfolios with the specific
indices that they are following. Days like this are generally marked by heightened volatility and high
volume market orders at the close. However, this is a rare fire sale where one can buy stocks at
discounted prices. One may see blue chip stocks sold down by as much as 5% for no apparent reason.
This one-day, one-shot selling is like a closing-out sale in retail stores or malls, as foreign fund managers
are practically giving away 2-5% discounts to the lucky buyers. See Wealth Report dated March 21, 2014.</p>

        <p><b>8. Should we invest directly in stocks or in mutual funds?</b></p>
        <p>Those who do not have the time or expertise to invest on their own can invest through mutual funds
such as Philequity Fund. Professional fund managers will then take care of studying which stocks to buy,
when to buy them and how to diversify your investments into various stocks and sectors.</p>
        <p>Those who are willing to spend more time and are quite familiar with the market can invest directly in
stocks. Although stock brokers may provide some advice or recommendations, it is still important for
one to do his homework and study the stocks that he buys or plans to buy. This will help one pick stocks
better and avoid possibly painful losses.</p>

        <p><b>9. You recently launched a dividend yield fund. What is the difference between this and your
flagship Philequity Fund?</b></p>
    <p>The Philequity Dividend Yield Fund is structured to focus its investments in Philippine stocks that provide
high dividend yields. Aside from this, the fund will also concentrate on dividend growers or companies
that can grow their dividends on a regular basis. We expect these stocks to continue providing steady dividends even when the stock market is volatile. Further, these stocks tend to perform relatively better
when the stock market experiences protracted downturns or long consolidation phases.</p>

