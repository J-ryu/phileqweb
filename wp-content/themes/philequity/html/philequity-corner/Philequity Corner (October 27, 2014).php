        <p align="center"><b>October 2014 follows eerie history</b></p>
        <p align="justify">This year, we were again reminded of October’s historically spooky performance. This October, the S&P 500 dropped 9.8% from its recent all-time high on concerns of a global slowdown and Ebola. However, note that since that low, the S&P 500 has rallied 8.0%, showing signs of a possible V-shaped recovery.</p>

        <p align="center"><b>Springboard for strong December and January performance</b></p>
        <p align="justify">The S&P 500’s historically scary performance in October and our stock market’s spooky performance this month may actually be springboards for a possibly strong performance in the months of December and January. In one of our earlier articles, we explained the reasons why the stock market is usually strong during the months of December and January (<i>Santa Claus Rally and the January Effect</i>, December 26, 2005).</p>

        <p align="center"><b>The Santa Claus Rally and the January Effect</b></p>
        <p align="justify">One of the main reasons behind the Santa Claus Rally is yearend window dressing. This happens as fund managers and institutions go on a buying spree to pump up their annual results for investors. Further, investors generally have more money during this period, as most people receive their Christmas and yearend bonuses during this time. These, combined with the generally jovial holiday mood, tend to push stock markets higher during these months. Positive sentiment spills over to January, as the new year brings new hope that things will be better, thereby providing an encouraging backdrop for more investments.</p>

        <p align="center"><b>77% Batting Rate for Halloween to Valentine’s</b></p>
        <p align="justify">Since today is October 27, we ran the numbers to see how the stock market performs from Halloween (October 27) up to Valentine’s Day of the next year (February 14). We found that the PSE Index delivered positive returns during this period 77% of the time (20 out of 26 years) with an average return of 11%. We show this in the table below.</p>

