        <p align="center"><b>Yolanda upsets odds in 2013</b></p>
        <p align="justify">Last year, the market was poised to go up after October. However, Yolanda happened in November and dampened our stock market’s performance for the rest of the year.</p>
        <p>We also note that similar to 2013, we saw some divergence or outliers in the table above, and most of these can be explained. In December of 1989, our country experienced a violent military coup. In 2002, the world was gripped with fears of an impending Iraq war. In 2007 to 2008, we saw the US subprime mortgage crisis unfold. And in late-2010, the Arab spring erupted. These events explain why our stock market performed poorly in a typically strong period for stocks.</p>
        <p>Barring any black swans such as these, statistics tell us that we can expect the PSE Index to perform strongly in the coming months.</p>

        <p align="center"><b>From Halloween to Valentine’s</b></p>
        <p align="justify">Our stock market’s spooky performance this month prompted us to look at historical performance during this period. We chose Halloween and Valentine’s because these are dates that are easy to remember. Based on our study of past performance, it seems that there is a bigger chance of making money from this point up to February. With a batting rate of 77% and an average return of 11%, the odds show that Halloween to Valentine’s will likely be a profitable period for investors. Moreover, this period captures the historically strong months of December and January, which are buoyed by the Santa Claus Rally and the January Effect.</p>

        

