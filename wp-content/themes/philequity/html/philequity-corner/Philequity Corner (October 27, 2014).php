        <p align="center"><b>From Halloween to Valentine’s</b></p>
        <p align="justify">Halloween is coming this Friday, October 31. Since it is Halloween season, we think it is timely to write about how the stock market behaves during Halloween and the following months. In this article, we look at history and analyze statistics to determine how the stock market performs from Halloween up to the first few weeks of the new year.</p>

