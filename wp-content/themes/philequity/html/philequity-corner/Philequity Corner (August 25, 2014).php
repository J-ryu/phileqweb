        <p><b>5. Why is the stock market going up even as certain stocks are going down sharply?</b></p>

        <p>What we are seeing now in our stock market is a divergence between stocks with strong earnings and those with
challenging growth prospects. Stocks with clear and strong earnings growth trajectories continue to perform
strongly, with some even making new highs. Further, stocks whose weights were increased by the MSCI have
also gone up. Note that foreign investors closely follow MSCI indices, and this is why stocks with increased
weightings have gone up significantly on the back of strong net foreign buying. However, stocks which have
delivered disappointing earnings and have murky earnings outlook were consequently sold down.</p>

        <p>This shows that we cannot be complacent with investing even though we are currently in a bull market. Our thesis
is that earnings and earnings expectations will drive the movement of local stocks. Hence, we at <i>Philequity</i> prefer
stocks which have delivered strong earnings growth and can continue to do so. Consequently, we lightened up
on stocks which have shown disappointing earnings and those which may encounter challenges to their growth
prospects over the mid-term.</p>

        <p><b>6. Why has your flagship Philequity Fund consistently outperformed for the past 20 years?</b></p>

        <p><i>Philequity</i> was founded by stock brokers in 1994. To date, <i>Philequity</i> continues to be guided by one of the oldest
and wisest boards in the country, with an average age of 64. Further, most of our directors have been in the stock
market for most of their professional careers. Likewise, our two principal fund managers have 40 and 50 years’
experience in equities, respectively. This weight of experience has allowed <i>Philequity</i> to properly navigate
through the various bear and bull cycles that our market has experienced. This level of experience also gives us
an advantage when evaluating how certain global or local events would affect the direction of our stock market.</p>

        <p>Aside from this, we at <i>Philequity</i> focus more on stock picking instead of timing the market or predicting when the
next correction would occur. Time and again, we admit that corrections are extremely hard to predict, even for
professionals. Hence, we think that it is more practical to study companies and businesses. We focus on investing
in companies that have solid business models and strong fundamentals. If these companies continue to do well,
their earnings will continue to grow and consequently, their stock prices will move higher over the long-term.</p>
    
        <p><b>7. Where is the stock market headed over the long-term?</b></p>

        <p>We mentioned in our presentations that our target for the index is 8,220 by March 2016. Beyond 2016, we believe
that our country will continue to reap the benefits of the various structural reforms that have been implemented
over the past years (<i>Beyond 2016</i>, June 9, 2014). Further, we expect that important structural changes that are
crucial to maintaining our growth trajectory – such as improved infrastructure or additional investments in the
power sector – will be addressed by the government in the succeeding years. This should pave the way for the
gradual development of other important sectors in our economy, such as manufacturing, agriculture, tourism and
mining. Considering these, our long-term growth trajectory is intact and we see the PSE Index hitting 10,000 by
2018.</p>

