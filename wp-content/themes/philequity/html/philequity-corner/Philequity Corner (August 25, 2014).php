        <p align="justify">PHILEQUITY CORNER By Valentino Sy (The Philippine Star) | August 25, 2014</p>

        <p>In our article last week, we answered some of the questions that were asked in our recent investor briefing.
Unfortunately, we could not answer all the 60+ questions that were asked by the audience during the briefing. In
this article, we will try to answer some of those questions as well as other questions that our clients and investors
often ask us.</p>

        <p><b>1. When is the right time to buy? I have been waiting for a correction but it hasn’t come.</b></p>

        <p>This is one of the most common questions that we encounter, regardless of market condition. In fact, many
investors often end up not investing when they become too concerned with timing. In a choppy market, some
postpone additional investments in the hope that the market will continue falling and allow them to invest at lower
price levels. However, in a rising market, investors often put off initial or additional investments while waiting for
a correction to occur.</p>

<p>The problem is that corrections, even for professionals, are very hard to predict. There are many unpredictable
factors that may trigger a correction, such as an off-the-cuff remark of a central bank head or a wrong-buttonpush
that launched a missile strike on an unsuspecting civilian airplane. As such, we always stress the
importance of taking a long-term view and making investment decisions based on this.</p>

<p>Though one’s equity exposure should depend on one’s asset allocation strategy and risk appetite, we believe
that everyone should have a certain portion of their investments in equities. We encourage investors who still do
not have equity exposure or are still underweight equities to start buying on a staggered basis now.</p>

        <p><b>2. How do we buy stocks or mutual funds on a staggered basis?</b></p>

        <p>We have always advocated buying in tranches for people who do not have the time to monitor the stock market
religiously. A more specific variant of this buying technique would be peso cost averaging. This would entail
buying a fixed peso amount of equities or mutual funds at a regular frequency. This can be done every week,
every two weeks, every month or even every quarter.</p>

        <p>Peso cost averaging may also be a very useful buying technique in times when the stock market continues to go
up and would just not go down. This eliminates the timing aspect of investing and instead allows investors to
focus on gradually increasing their exposure to the stock market. We have concrete examples of clients and
investors who have successfully applied peso cost averaging and have immensely benefited from it (Example of
a Real-Life Investor, March 31, 2014).</p>

        <p><b>3. Currently, what gives you confidence to keep buying in tranches or pullbacks?</b></p>

        <p>As we discussed in our previous article, there are many risks that have surfaced lately, both globally and locally.
However, our stock market has not gone down significantly despite the emergence of these risks (<i>Lessons
Learned from 20 Years of Outperformance</i>, Aug.t 18, 2014). And as we pointed out in our recent investor
briefing, <b>a market that refuses to go down on bad news is poised to move higher</b>. This is what we are seeing
with our stock market now and this is clearly a sign of our stock market’s inherent strength. This gives us
confidence to continue buying in tranches or pullbacks.</p>

        <p>Moreover, there are many global stock markets that continue to reach all-time or multi-year highs. There are also
some indices that have performed poorly before but are now starting to turn for the better. While this is happening,
the secular bull market (<i>Secular Bull Market</i>, Jan. 28, 2013) in Philippine stocks remains intact, as our country
and corporates continue to deliver strong economic and earnings growth.</p>

