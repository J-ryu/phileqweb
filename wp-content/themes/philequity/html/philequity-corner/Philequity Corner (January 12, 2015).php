        <p align="center"><b>Explaining the Numbers</b></p>
        <p align="justify">An argument can be made that listed companies that pay attention to explaining their numbers and
giving guidance on forecasted performance along with a context of management strategy and direction
do better in terms of their stock price than some others who prefer to “just let their numbers do the
talking.” Stock analysts may have their own interpretation of business models and ratios but a
spokesman for the company can present a narrative that connects the dots in a positive way. He is also
open to questions about unusual spikes in certain numbers—that’s a one-time, non-recurring write-off
of stale inventory.</p>
    <p>Do companies that win the best annual report awards also perform better in the market? And
conversely, do those that prefer to let rumors swirl around them uncorrected or even enhanced get to
be undervalued by the market?</p>
    <p>Just like the character discount or premium we wrote about in a previous column here, there may be a
communications gap deflator that haunts particular stocks.</p>
    <p>Maybe because Chief Financial Officers (CFO) are not usually known for their communications skills or
verbosity, except when making a case for credit worthiness to banks or rating agencies, they tend to just
release the numbers and leave it at that. (If the ratios and returns are great, do you need to say more?)</p>
    <p>The relatively new function of investor relations, which listed companies are now assigning to more
senior executives, is an exercise in getting numbers to tell a consistent story. The narrative that emerges
is supposed to have a happy ending (or at least nothing too disastrous) which is believable, and no fairy
tale. That this function by default ends up under Finance rather than corporate communications means
that the one in charge of making sense of the metrics tends to be from Accounting. Even with pie charts
and slides, numbers by themselves can be blah, not to mention capable of giving nose bleeds when
released in a flood. Without a story, a deluge of numbers can be as exciting as watching a performance
of "Merchant of Venice" without costumes or sets, and a missing Shylock.</p>
    <p>Disclosure rules of the stock exchange and the inherent secretiveness of finance people (that's
confidential) as well as a built-in reluctance to explain anything, except revenue projections with banks,
conspire to belittle the importance of a compelling narrative for the company.</p>
    <p>Should the function of investor relations be part of corporate communications instead of Finance?
Maybe then the full narrative of the company will have some consistency and context, so that the
spokesperson answering questions on reorganization, union unrest, market share, bidding prospects,
regulatory challenges, and fraud investigations also understands the impact of all these on the numbers.
A single story teller can present a more cogent plot that connects all the relevant ingredients to come up
with a more edible narrative stew.</p>
    <p>Even investors with assets under management in the tens of billions of pesos and a fully functioning
research department tend to characterize companies that they track, buy, or dismiss outright with a
single phrase. These phrases become stereotypes after a while, similar to racial profiles that feed on our prejudices: Japanese are punctual; Swiss bankers can keep secrets; Filipinos are easy-going and tardy,
also gossipy and soft in negotiations. For companies, the caricatures can be just as sweeping and unfair:
too much debt; no rhyme or reason for acquisitions; badly managed; quarrelling siblings and factions
with a patriarch having bouts of dementia; sunset industry; no succession plan; one-man Excom.</p>

