    <p>The default reporting of the investor relations function to finance rather than communications, mainly
because of access to the numbers, needs to be reviewed. However, the skills set for the right
communicator needs to veer away from mere glibness and cosy relationship with media. It has to
expand to familiarity and even comfort with numbers so that a compelling narrative supported by facts
can be credibly promoted. The numerate individual, who is also literate and can quote Bassanio in
<i>Merchant of Venice</i>, can best explain what the numbers mean.</p>

    <p>As in regular PR, the importance of constant communications and putting the company in a favorable
light or even controlling the damage of unfavorable events is underrated. It seems that only in a crisis
when firemen and bomb squads are on the scene is PR called in, if only to get the CEO out of the line of
fire. By this time, it is crisis PR practitioners that take over. And these are a different breed of
communicators used to frenzy and mayhem and costing more in terms of money and reputation for the
besieged company. (Get the stuffed envelopes ready.)</p>
    
    <p>A well-argued narrative for investor relations is really directed at a very small and knowledgeable
audience of researchers and large funds, maybe a business program on TV. The goal is not to hype up a
stock but to allow it to be priced for its fair value. While majority stockholders of listed companies may
scoff at the cosmetics of communication (I know what my company is worth) some appreciation of the
value of the narrative begins to sink in when the stock price is trapped in a 52-week low in the midst of a
bull market. Maybe, it is the ego, and the grating realization that other less worthy companies are
trading at premiums that spurs the insulated CEO to give financial communications a more serious look.</p>

    <p>Anyway, the "theory of efficient markets" states that even if the market is uninformed, it is still right.
Giving the market additional information can't bring a stock price any lower. Like good deeds, upbeat
underlying numbers also need to be explained...lest they be punished by a PE ratio that is a third of the
market average.</p>

    
    <p>Even without embellishments, telling a story well should at least ensure a rapt audience...with deep
pockets.</p>

     

       
