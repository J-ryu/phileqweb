        <p align="center"><b>Peso is back where it should be</b></p>
        <p align="justify"><i>“We monitor it among a band of currencies and it is back where it should be,”</i> said Secretary Purisima.</p>

        <p>Secretary Purisima was quoted by Bloomberg News last week and he is referring to the peso going back to the 43 to 44 levels, a range which he deems acceptable. This also refers to the peso “sweet spot” that we have written in the past (see<i> Back to the Sweet Spot,</i> June 3, 2013).</p>

        <p><b>USDPHP Rate Daily Chart</b></p>
        <img src="image/071513chart.jpg" height="400" width="800"><br>
        <i>Source: Wealth Securities Research</i>

        <p>Last week, the peso closed at 43.395 against the US dollar. It has lost 5.5 percent so far this year in consonance with the declines of regional and global currencies vis-à-vis the US dollar.</p>

        <p align="center"><b>Keeping pace with the Indian rupee</b></p>
        <p align="justify">Among Asian currencies, the Philippine peso is the 2nd weakest this year. The peso, however, is only keeping pace with the Indian rupee which weakened by 8.39 percent against the US dollar year-to-date. The peso’s depreciation therefore is appropriate and crucial to our country’s efforts of keeping our Business Process Outsourcing (BPO) companies competitive against their Indian counterparts.</p>

        <img src="image/071513chart2.jpg" height="400" width="800"><br>
        <i>Source: Bloomberg, Wealth Securities Research</i>

        <p>Unknown to most Filipinos, even many of those in the BPO industry, the peso has already gained almost
40 percent against the Indian rupee during the past three years. In fact, we have warned about this as
early as last year (see <i>Strong Peso - Too much of a Good Thing,</i> July 9, 2012). If the peso does not keep
up with the Indian rupee, our BPO industry will continue to lose pricing competitiveness.</p>

        <p align="justify"><b>USDINR Rate Daily Chart</b>
