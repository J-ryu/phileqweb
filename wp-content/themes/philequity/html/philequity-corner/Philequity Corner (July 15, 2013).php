        <p align="center"><b>Fundamentals remain sound and inflation well-anchored</b></p>
        <p>Despite the slightly weaker peso, BSP Gov. Tetangco maintains that <i>“inflation expectations continue to
be well-anchored”</i> and<i> “underlying fundamentals remain sound.”</i> Philippine inflation remains subdued
at 2.8 percent in June. It has averaged 2.9 percent the past six months.</p>

        <p>In contrast, emerging countries like India and Brazil, which also experienced sharp currency depreciations
these past couple of months, are also suffering from high inflation. India’s inflation rate last June was at
9.9 percent, while Brazil’s inflation rate was at 6.7 percent.</p>

        <p align="center"><b>Peso depreciation is beneficial</b></p>
        <p align="justify">Currency depreciation puts more pressure on countries which have chronic current account deficits like India and Brazil.</p>
        <p>India registered a current account deficit equivalent to 4.8 percent of GDP in 2012, while Brazil had a current account deficit equivalent to 2.4 percent of GDP over the same period. The Philippines, meanwhile, had a current account surplus equivalent to 2.8 percent of GDP in 2012.</p>
        <p>So while the currency depreciation is bad for India and Brazil, it is actually beneficial to the Philippines because of the continued strength in BPO revenues and OFW remittances, which would eventually translate to higher domestic consumption.</p>

        <p align="center"><b>Fed policy to remain highly accommodative in the foreseeable future</b></p>
        <p align="center"><i>“Highly accommodative monetary policy for the foreseeable future is what is needed.”</i>&nbsp;- Ben Bernanke</p>

        <p>This is another important quote that happened last week which propelled stock markets worldwide and caused currencies to rally against the US dollar. US stocks ended at record levels as the S&P 500 index and the Dow Jones Industrial Average registered new all-time highs while the Nasdaq ended at its best close since 2012.</p>

        <p>Emerging markets also rebounded with the iShares MSCI Emerging Markets Index (symbol: EEM) rallying 4.9 percent in Thursday’s trading. This reinforces our view that PSE Index has indeed made an intermediate bottom last June 25 when it hit an intraday low of 5,678 (see our article <i>5-6-7-8,</i> July 1, 2013).</p>

        <p>From a technical standpoint, the chart below shows a potential Head and Shoulders reversal formation in the PSE Index. If the 6,600 level is broken to the upside, this means that the PSE index will now go to the next resistance at 7,000 and potentially retest the all-time high of 7,403 registered last May.</p>

