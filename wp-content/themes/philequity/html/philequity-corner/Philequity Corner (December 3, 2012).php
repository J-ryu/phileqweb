        <p align="center"><b>The Bull Market Surges On</b></p>
        <p>In a prior article, we showed you President Aquino’s SONA in graphical form. In a similar manner, we will show you the 4-year Philippine bull market in tables and graphs.</p>

        <p align="center"><b>PSE Index (July 2007 to present)</b><br></p>
            <img src="image/120312chart.jpg" height="400" width="800"><br>
            <i>Source: Technistock</i>

        <p>The PSE index continues to surge on, reaching a new record high of 5,640.45 last week. It is on track to close the year within our target range of 5,600 to 5,800. The PSEi is already up 29 percent year-to-date which makes the Philippine stock market the best performing market in Asia.</p>

        <p align="center"><b>EPHE (since inception)</b><br></p>
            <img src="image/120312chart1.jpg" height="400" width="800"><br>
            <i>Source: Stockcharts.com</i>

        <p>The iShares MSCI Philippines Investable Market Index Fund (symbol: EPHE), which is an exchange traded fund (ETF) tracking the Philippine stock market, also closed at an all-time high of 33.60 last week. EPHE is traded at the New York Stock Exchange.</p>

        <p align="center"><b>Philippine GDP growth</b><br></p>
            <img src="image/120312chart2.jpg" height="400" width="800"><br>
            <i>Source: TradingEconomics.com</i>

        <p>Fuelling the stock market’s rise is the robust Philippine economy which posted a stronger-than-expected GDP growth of 7.1 percent in 3Q2012. The expansion in economic output was driven by domestic household consumption and government spending. The Philippine economy is the 2nd fastest growing economy in Asia in 3Q2012, next only to China’s 7.4 percent growth.</p>

        <p align="center"><b>Philippine GDP growth</b><br></p>
            <img src="image/120312chart3.jpg" height="300" width="800"><br>
            <i>Source: Bloomberg</i>

        <p>Accommodative monetary policies by the world’s central banks to jumpstart global growth, including our own BSP, have pushed the 10-year Peso benchmark yields to historic lows. The yield is down to 4.445 percent from as high as 10 percent four years ago. The low yields have caused local banks and traditional investors to gradually shift from fixed income to equities.</p>

        <p align="center"><b>Philippine Government Debt-to-GDP</b><br></p>
            <img src="image/120312chart4.jpg" height="300" width="800"><br>
            <i>Source: TradingEconomics.com</i>

        <p>The Philippine fiscal position continues to improve as debt-to-GDP is down to 40.5 percent in 2012 from a high of 71.4 percent in 2004. Foreign investors normally reward economies and countries with responsible and sound fiscal management.</p>

        <p align="center"><b>Peso Chart (2008 to present)</b><br></p>
            <img src="image/120312chart5.jpg" height="400" width="800"><br>
            <i>Source: Technistock</i>

        <p>The peso is the best performing Asian currency vs. the US dollar year-to-date. It reached an intraday high of 40.85 against the US dollar, before closing at 41 last week. The strong peso has been driven by OFW remittances and BPO revenues, sharp increase in the country’s current account surplus (up 48.5 percent in 1H2012) and record GIR ($82 billion as of 3Q2012).</p>

        <p align="center"><b>Macro Fundamentals will drive PSEi higher</b></p>
        <p>As we mentioned before, external headwinds such as the US fiscal cliff, the sovereign debt problems in Europe and the concerns of a hard landing in China will just cause our market to pause or correct. We also said that “these corrections are only temporary and should be seen as opportunities to buy issues with good growth prospects and sustainable business models (see <i>Cliffhanger, </i>November 19, 2012)”</p>

        <p>In the long run, we believe that our strong macroeconomic fundamentals will prevail over the external headwinds. Strong GDP growth, low interest rates, strong peso and strong investor confidence in the government and their ability to manage its finances and the economy should propel the stock market higher.</p>

        <p align="center"><b>Philequity Fund reaches New High</b></p>
        <p>Philequity Fund reached a new record high last week to close with a NAV per share of Php 27.8519. Philequity Fund is up 29.95 percent year-to-date compared to the PSEi’s 29.01 percent return. We reiterate our recommendation that investors continue to buy on dips.</p>

   
