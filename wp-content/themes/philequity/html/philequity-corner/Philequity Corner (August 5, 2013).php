        <p align="center"><b>Santa Claus Vanquishes Ghosts</b></p>
        <p align="justify">We conclude that the “Santa Claus Rally” seems to be more statistically dependable than a “Ghost
Month Spook”. The 10th and 11th lunar months, which coincide with the months of December and
January have demonstrated a 70 percent probability of a gain.</p>
        
        <p align="center"><b>Placement Saps Liquidity</b></p>
        <p align="justify">Ghosts may not actually be the reason for a stock market decline in the Ghost Month. A more practical
explanation would be that European fund managers go on vacation during their summer months.
However, this time around, a bigger factor would be SM Investments Corp.’s overnight placement of
7.25 million shares at P900 per share or a 6 percent discount to market. This share sale resulted in many
funds suddenly becoming fully invested or overexposed to Philippine equities. In order for funds to
manage their asset allocation, they sold their other holdings. With the objective of rebalancing, funds
may disregard the price at which they sell.</p>

        <p align="center"><b>Electric Shock</b></p>
        <p align="justify">Aside from SM, Meralco, the largest power distributor in the country, had a secondary placement of
64.3 million shares at P270 apiece only 2 weeks ago. Combined, the two transactions raised almost P24
billion, zapping the market rally. The market, which was on its way to 7,000, suddenly reversed its
course with these two placements.</p>
        <p>Notwithstanding their short-term effects, these placements allowed investors to increase their holdings
in two important index stocks. SM and Meralco are core holdings in the portfolios of mutual funds as
these two companies are directly tied to our consumer-driven economy.</p>

        <p align="center"><b>Who’s Afraid of Au-ghosts?</b></p>
        <p align="justify">We expect the market to enter a consolidation phase within the next two months. During our investors
briefing last Saturday, we showed that the market usually trades sideways after huge equity placements
but eventually goes higher after the market has digested the shares. As we have seen in the past, the
market should resume its long-term upward trajectory.</p>

