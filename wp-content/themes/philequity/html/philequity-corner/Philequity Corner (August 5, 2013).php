        <p align="center"><b>Au-ghost</b></p>
        <p align="justify">This Wednesday, August 7, will mark the beginning of the 7th month in the Chinese Lunar calendar which will last until the fourth of September. The 7th lunar month is also known as the “Hungry Ghost Month” or simply the “Ghost Month.”</p>

        <p>Our most recent Philequity Corner articles that were about Chinese festivities were “Snakes and Stocks” published on Chinese New Year, February 10, and “The Friendly Ghost” written August 20 last year. We incorporate superstitions and traditions such as these into our articles because it is undeniable that the Chinese culture has a huge influence in our country.</p>

        <p>We recently received a call from one of our bigger investors who wished to know if they should be concerned about the so-called Ghost Month. Certainly, there would be new and old investors who are wondering the same. Hopefully, this article would be interesting to our readers especially because our research team crunched some numbers to help support any conclusion we may make.</p>

        <p align="center"><b>Hungry Ghost Month</b></p>
        <p align="justify">The Hungry Ghost Month is believed to be the time when spirits of dead relatives return to the land of the living. To pay respect and homage, the Chinese dedicate their time and efforts offering food and prayers during this month. The Chinese refrain from doing activities like travelling, moving homes, starting up a new business and getting married. During the Ghost Month, it is not advisable to go swimming or to go on evening strolls as spirits lurking in the water or in the darkness may harm you. Perhaps, trading stocks, which by itself poses a significant amount of risk, becomes one of those activities to stave off during the Ghost Month.</p>

        <p align="center"><b>What the numbers show</b></p>
        <p align="justify">We examined our Philippine Stock Exchange (PSE) Index from 1987 to 2012. Hopefully, 26 years worth of data can shine some light on any possible Ghost Month phenomenon. For a given month, we counted how many times our index declined in the past 26 years. The extreme cases would be 26 times (always) or zero times (never). We also calculated each month’s average return.</p>

        <p>Here are the most interesting facts we have gathered:</p>

        <blockquote>1) The PSE Index declined at an average of 2 percent in 14 out of 26 Ghost Months. This is only slightly more than half of the time. Notice that the 1st and 8th months also resulted in 14 declines in the past 26 years.</blockquote>

        <blockquote>2) The months where the PSE Index rose the most were the 10th, 11th and 12th lunar months, yielding an average of 3 percent. Moreover, the 10th and 11th months only declined 8 times in</blockquote>

        <blockquote>the past 26 years. That is, there is a 70 percent chance that they will yield positive returns. This
probability is more significant than the “coin-toss” odds seen in the 7th lunar month.</blockquote>

